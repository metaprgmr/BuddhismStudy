佛告跋陀惒：「是菩薩若從比丘、比丘尼、優婆塞、優婆夷所，得聞是三昧當視如佛，所聞是三昧處當尊敬。」

佛告跋陀惒：「菩薩所聞是三昧處，不當持諂意，向是菩薩不得諂意，常當樂獨處止，不惜身命，不得希望人有所索，常行乞食，不受請，不嫉妬，自守節度如法住，所有趣足而已，經行不得懈，不得臥出。如是，跋陀惒，如是經中教其棄愛慾作比丘學是三昧者，當如是守如是。」

跋陀惒菩薩白佛：「難及天中天所說法！若有後世懈怠菩薩，聞是三昧已不肯精進，其人自念：『我當於後世當來佛所索是三昧耳！』云言：『我曹身羸極有病瘦恐不能求。』聞是經已懈怠不精進。若復有菩薩精進者，欲學是經，當教之隨是經中法教。用是經故，不惜軀命，不望人有所得者，有人稱譽者不用故喜，不大貪缽，震越無所愛慕，常無所欲，聞是經不懈怠常精進。其人不念：『我當於後當來佛所乃求索。』自念：『使我筋髓骨肉皆使枯腐，學是三昧終不懈怠。』自念：『我終不懈怠死，聞是經已無不歡樂。』」

時佛言：「善哉！善哉！跋陀惒，所說者無有異。我助其歡喜，過去、當來、今現在佛悉助歡喜。」

