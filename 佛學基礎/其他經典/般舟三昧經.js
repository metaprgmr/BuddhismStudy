佛告跋陀惒：「是菩薩三昧當云何？譬如佛今於若前說經，菩薩當作是念：『諸佛悉在前立。』當具足念諸佛端正，悉欲逮見一一相，當想識無有能見諸佛頂上者，悉具足作是想見諸佛，當作是念：『我身亦當逮得如是，亦當逮得身相如是，亦當逮得持戒三昧如是。』當作是念：『我當從心得？從身得？』當復更作念：『佛亦不用心得，亦不用身得，亦不用心得佛，亦不用色得佛。何以故？心者佛無心，色者佛無色，不用是心色，得阿耨多羅三藐三菩提。何以故？佛色以盡，佛痛癢、思想、生死、識了盡。佛所說盡者，愚癡不見不知，智者曉了之。』作是念：『當持何等念得佛？當持身得佛？當持智慧得佛？』復作是念：『亦不用身得佛，亦不用智慧得佛。』何以故？智慧索不可得，自復索我了不可得，亦無所得，亦無所見。一切法本無所有，念有因著，無有反言有亦著；是兩者亦不念，亦不復適得其中。但用是故，亦不在邊，亦不在中，亦不有亦不無。何以故？諸法空，如泥洹，亦不壞，亦不腐，亦不堅，亦不在是間，亦不在彼邊，無有想，不動搖。何等為不動搖？智者不計，是故不動搖。如是，跋陀惒，菩薩見佛已，菩薩心念無所著。何以故？說無所有，經說無所有，中不著，壞本絕本，是為無所著。如是，跋陀惒，是菩薩守是三昧，當作是見佛，不當著佛。何以故？設有所著為自燒。譬如大叚鐵著火中燒正赤，有智者不當以手持。何以故？燒人手。如是，跋陀惒，菩薩見佛不當著，色、痛癢、思想、生死、識不當著。何以故？著者為燒身。見佛但當念其功德，當索摩訶衍。」

