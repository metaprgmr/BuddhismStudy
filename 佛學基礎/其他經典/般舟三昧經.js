佛告跋陀惒：「比丘尼求摩訶衍三跋致，是三昧學守者，當謙敬，不當嫉妬，不得瞋恚，去自貢高，去自貴大，卻於懈怠，當勤精進棄於睡眠不得臥出，悉卻財利，悉當淨潔護，不得惜軀命，常當樂於經，當求多學，當棄淫恚癡出魔羅網去，當棄所好服飾珠環，不得惡口，不得貪愛好缽、震越，當為人所稱譽不得有諛諂。學是三昧時，當敬善師視如佛，當承是經中教守是三昧。」

