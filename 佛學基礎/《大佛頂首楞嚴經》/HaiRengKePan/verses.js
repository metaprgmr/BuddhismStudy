F037: 世尊！我復以此聞熏聞修，金剛三昧，無作妙力，與諸十方三世六道一切眾生，同悲仰故；令諸眾生，於我身心，獲十四種無畏功德。
F038: 一者，由我不自觀音，以觀觀者；令彼十方苦惱眾生，觀其音聲，即得解脫。
F039: 二者，知見旋復，令諸眾生，設入大火，火不能燒。
F040: 三者，觀聽旋復，令諸眾生，大水所漂，水不能溺。
F041: 四者，斷滅妄想，心無殺害；令諸眾生，入諸鬼國，鬼不能害。
F042: 五者，熏聞成聞，六根銷復，同於聲聽。能令眾生，臨當被害，刀段段壞；使其兵戈，猶如割水，亦如吹光，性無搖動。
F043: 六者，聞薰精明，明遍法界，則諸幽暗，性不能全，能令眾生，藥叉羅剎，鳩槃茶鬼，及毗舍遮，富單那等，雖近其傍，目不能視。
F044: 七者，音性圓銷，觀聽返入，離諸塵妄；能令眾生，禁系枷鎖，所不能著。
F045: 八者，滅音圓聞，遍生慈力，能令眾生，經過險路，賊不能劫。
F046: 九者，熏聞離塵，色所不劫，能令一切多婬眾生，遠離貪欲。
F047: 十者，純音無塵，根境圓融，無對所對；能令一切忿恨眾生，離諸瞋恚。
F048: 十一者，銷塵旋明，法界身心，猶如琉璃，朗徹無礙；能令一切昏鈍性障，諸阿顛迦，永離癡暗。
F049: 十二者，融形復聞，不動道場涉入世間，不壞世界。能遍十方，供養微塵諸佛如來；各各佛邊，為法王子。
      能令法界，無子眾生，欲求男者，誕生福德智慧之男。
F050: 十三者，六根圓通，明照無二，含十方界，立大圓鏡，空如來藏。承順十方，微塵如來秘密法門，受領無失；能令法界無子眾生；
      欲求女者，誕生端正福德柔順，眾人愛敬有相之女。
F051: 十四者，此三千大千世界，百億日月，現住世間諸法王子，有六十二恒河沙數，修法垂範，教化眾生；隨順眾生，方便智慧，各各不同。
      由我所得圓通本根，發妙耳門，然後身心微妙含容，周遍法界，能令眾生持我名號，與彼共持六十二恒河沙諸法王子，二人福德，正等無異。
      世尊：我一名號，與彼眾多名號無異，由我修習，得真圓通。
F052: 是名十四無畏力，福備眾生。
F053: 世尊，我又獲是圓通，修證無上道故，又能善獲四不思議無作妙德。
F054: 一者，由我初獲妙妙聞心，心精遺聞，見聞覺知，不能分隔，成一圓融清淨寶覺，故我能現眾多妙容，能說無邊秘密神咒。
F055: 其中或現，一首三首，五首七首，九首十一首，如是乃至一百八首，千首萬首，八萬四千爍迦羅首。二臂四臂，六臂八臂，十臂十二臂，
      十四十六十八，二十至二十四，如是乃至一百八臂，千臂萬臂，八萬四千母陀羅臂。二目三目，四目九目，如是乃至一百八目，千目萬目，
      八萬四千清淨寶目。或慈或威，或定或慧，救護眾生，得大自在。
F056: 二者，由我聞思，脫出六塵；如聲度垣，不能為礙。故我妙能現一一形，誦一一咒；其形其咒，能以無畏，施諸眾生。
      是故十方微塵國土，皆名我為施無畏者。
F057: 三者，由我修習本妙圓通，清淨本根；所遊世界，皆令眾生，捨身珍寶，求我哀愍。
F058: 四者，我得佛心，證於究竟。能以珍寶種種，供養十方如來；傍及法界六道眾生。
      求妻得妻，求子得子，求三昧得三昧，求長壽得長壽，如是乃至，求大涅槃得大涅槃。
F059: 佛問圓通，我從耳門圓照三昧，緣心自在；因入流相，得三摩地，成就菩提，斯為第一。
F060: 世尊，彼佛如來，嘆我善得圓通法門，於大會中，授記我為觀世音號。由我觀聽十方圓明，故觀音名，遍十方界。
F061: 爾時世尊，於師子座，從其五體，同放寶光，遠灌十方微塵如來，及法王子諸菩薩頂，彼諸如來，亦於五體同放寶光，從微塵方來灌佛頂，
      並灌會中諸大菩薩，及阿羅漢。
F062: 林木池沼，皆演法音，交光相羅，如寶絲網，是諸大眾，得未曾有，一切普獲金剛三昧。
F063: 即時天雨，百寶蓮華，青黃赤白，間錯紛糅，十方虛空，成七寶色。此娑婆界，大地山河，俱時不現；
      唯見十方，微塵國土，合成一界，梵唄詠歌，自然敷奏。
