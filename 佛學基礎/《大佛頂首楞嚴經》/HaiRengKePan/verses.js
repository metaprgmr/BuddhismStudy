C029: 即嗅與香，二處虛妄；本非因緣，非自然性。
C030: 阿難，汝常二時，眾中持缽，其間或遇，酥酪醍醐，名為上味。
C031: 於意云何？此味為復生於空中？生於舌中？為生食中？阿難，若復此味，生於汝舌，在汝口中，祇有一舌，其舌爾時，已成酥味；遇黑石蜜，應不推移。
