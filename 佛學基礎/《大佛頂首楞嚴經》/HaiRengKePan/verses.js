C038: 則意與法，二俱虛妄；本非因緣，非自然性。
C039: 復次阿難，云何十八界，本如來藏，妙真如性。
C040: 阿難，如汝所明，眼色為緣，生於眼識。
C041: 此識為復因眼所生，以眼為界；因色所生，以色為界。
C042: 阿難，若因眼生，既無色空，無可分別，縱有汝識，欲將何用？汝見又非青黃赤白，無所表示，從何立界？
