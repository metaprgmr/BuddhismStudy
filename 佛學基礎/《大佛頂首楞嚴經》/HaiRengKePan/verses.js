C047: 是故當知，耳聲為緣，生耳識界，三處都無；則耳與聲，及聲界三，本非因緣，非自然性。
C048: 阿難，又汝所明，鼻香為緣，生於鼻識。
C049: 此識為復因鼻所生，以鼻為界；因香所生，以香為界。
