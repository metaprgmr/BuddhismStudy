C072: 阿難，如水成冰，冰還成水。
C073: 汝觀地性：麤為大地，細為微塵；至鄰虛塵，析彼極微，色邊際相，七分所成，更析鄰虛，即實空性。阿難，若此鄰虛析成虛空；當知虛空，出生色相。
