F069: 歸元性無二，方便有多門，勝性無不通，順逆皆方便；初心入三昧，遲速不同倫。
F070: 色想結成塵，精了不能徹；如何不明徹，於是獲圓通。音聲雜語言，但伊名句味；一非含一切，云何獲圓通？
      香以合中知，離則元無有；不恒其所覺，云何獲圓通？味性非本然，要以味時有；其覺不恒一，云何獲圓通？
      觸以所觸明，無所不明觸；合離性非定，云何獲圓通？法稱為內塵，憑塵必有所；能所非遍涉，云何獲圓通？
F071: 見性雖洞然，明前不明後；四維虧一半，云何獲圓通？鼻息出入通，現前無交氣；支離匪涉入，云何獲圓通？
      舌非入無端，因味生覺了；味亡了無有，云何獲圓通？身與所觸同，各非圓覺觀；涯量不冥會，云何獲圓通？
      知根雜亂思，湛了終無見；想念不可脫，云何獲圓通？
F072: 識見雜三和，詰本稱非相；自體先無定，云何獲圓通？心聞洞十方，生於大因力；初心不能入，云何獲圓通？
      鼻想本權機，祇令攝心住；住成心所住，云何獲圓通？說法弄音文，開悟先成者；名句非無漏，云何獲圓通？
      持犯但束身，非身無所束；元非遍一切，云何獲圓通？神通本宿因，何關法分別？念緣非離物，云何獲圓通？
F073: 若以地性觀，堅礙非通達；有為非勝性，云何獲圓通？若以水性觀，想念非真實；如如非覺觀，云何獲圓通？
      若以火性觀，厭有非真離；非初心方便，云何獲圓通？若以風性觀，動寂非無對，對非無上覺，云何獲圓通？
      若以空性觀，昏鈍先非覺；無覺異菩提，云何獲圓通？若以識性觀，觀識非常住；存心乃虛妄，云何獲圓通？
      諸行是無常，念性元生滅；因果今殊感，云何獲圓通？
F074: 我今白世尊：佛出裟婆界，此方真教體，清淨在音聞；欲取三摩提，實以聞中入。
F075: 離苦得解脫，良哉觀世音；於恒沙劫中，入微塵佛國；得大自在力，無畏施眾生。
      妙音觀世音，梵音海潮音，救世悉安寧，出世獲常住。
F076: 我今啟如來，如觀音所說，譬如人靜居，十方俱擊鼓，十處一時聞，此則圓真實。
F077: 目非觀障外，口鼻亦復然，身以合方知，心念紛無緒；隔垣聽音響，遐邇俱可聞，五根所不齊，是則通真實。
F078: 音聲性動靜，聞中為有無，無聲號無聞，非實聞無性；聲無既無滅，聲有亦非生，生滅二圓離，是則常真實。
      縱令在夢想，不為不思無，覺觀出思惟，身心不能及。
F079: 今此娑婆國，聲論得宣明，眾生迷本聞，循聲故流轉。阿難縱強記，不免落邪思，豈非隨所淪，旋流獲無妄？
F080: 阿難汝諦聽，我承佛威力，宣說金剛王，如幻不思議，佛母真三昧。
      汝聞微塵佛，一切秘密門；欲漏不先除，蓄聞成過誤。將聞持佛佛，何不自聞聞？
      聞非自然生，因聲有名字，旋聞與聲脫，能脫欲誰名？一根既返源，六根成解脫。
F081: 見聞如幻翳，三界若空華，聞復翳根除，塵銷覺圓淨。
      淨極光通達，寂照含虛空；卻來觀世間，猶如夢中事，摩登伽在夢，誰能留汝形？
F082: 如世巧幻師，幻作諸男女，雖見諸根動，要以一機抽；息機歸寂然，諸幻成無性。
      六根亦如是，元依一精明，分成六和合；一處成休復，六用皆不成；
      塵垢應念銷，成圓明淨妙，餘塵尚諸學，明極即如來。
F083: 大眾及阿難，旋汝倒聞機，反聞聞自性，性成無上道，圓通實如是。
F084: 此是微塵佛，一路涅槃門，過去諸如來，斯門已成就。現在諸菩薩，今各入圓明；
      未來修學人，當依如是法。我亦從中證，非惟觀世音。
F085: 誠如佛世尊，詢我諸方便，以救諸末劫，求出世間人，成就涅槃心，觀世音為最。
      自餘諸方便，皆是佛威神，即事捨塵勞，非是長修學，淺深同說法。
F086: 頂禮如來藏，無漏不思議；願加被未來，於此門無惑，方便易成就。
      堪以教阿難，及末劫沈淪，但以此根修，圓通超餘者，真實心如是。
F087: 於是阿難及諸大眾，身心了然，得大開示，觀佛菩提，及大涅槃；
      猶如有人，因事遠遊，未得歸還，明了其家，所歸道路。
      普會大眾，天龍八部，有學二乘，及諸一切，新發心菩薩，其數凡有十恒河沙，皆得本心，遠塵離垢，獲法眼淨。
      性比丘尼，聞說偈已，成阿羅漢。無量眾生；皆發無等等阿耨多羅三藐三菩提心。
F088: 阿難整衣，於大眾中，合掌頂禮，心跡圓明，悲欣交集，欲益未來，諸眾生故，稽首白佛，大悲世尊，
      我今已悟成佛法門，是中修行，得無疑惑。
      常聞如來說如是言，自未得度，先度人者，菩薩發心；自覺已圓，能覺他者，如來應世。我雖未度，願度末劫一切眾生。
      世尊，此諸眾生，去佛漸遠，邪師說法，如恒河沙，欲攝其心，入三摩地，云何令其，安立道場，遠諸魔事？於菩提心，得無退屈？
F089: 爾時世尊，於大眾中稱讚阿難：善哉善哉，如汝所問，安立道場，救護眾生，末劫沈溺，汝今諦聽，當為汝說。阿難大眾，唯然奉教。
F090: 佛告阿難：汝常聞我毗奈耶中，宣說修行，三決定義。所謂攝心為戒，因戒生定，因定發慧，是則名為三無漏學。
F091: 阿難，云何攝心，我名為戒？
F092: 若諸世界，六道眾生，其心不婬，則不隨其生死相績。汝修三昧，本出塵勞，婬心不除，塵不可出。
      縱有多智，禪定現前，如不斷婬，必落魔道。
      上品魔王；中品魔民，下品魔女，彼等諸魔，亦有徒眾，各各自謂成無上道。
      我滅度後，末法之中，多此魔民，熾盛世間。
      廣行貪婬，為善知識，令諸眾生，落愛見坑，失菩提路。
F093: 汝教世人修三摩地，先斷心婬，是名如來，先佛世尊，第一決定清淨明誨。
F094: 是故阿難，若不斷婬，修禪定者，如蒸砂石，欲其成飯，經百千劫，祇名熱砂，何以故？此非飯本，砂石成故。
F095: 汝以婬身，求佛妙果，縱得妙悟，皆是婬根，根本成婬，輪轉三塗，必不能出；如來涅槃，何路修證？
F096: 必使婬機，身心俱斷，斷性亦無，於佛菩提，斯可希冀。如我此說，名為佛說；不如此說，即波旬說。
F097: 阿難，又諸世界，六道眾生，其心不殺，則不隨其生死相續。汝修三昧，本出塵勞，殺心不除，塵不可出；
      縱有多智，禪定現前，如不斷殺，必落神道。
      上品之人，為大力鬼；中品則為飛行夜叉諸鬼帥等；下品當為地行羅剎。彼諸鬼神，亦有徒眾，各各自謂成無上道。
      我滅度後，末法之中多此鬼神熾盛世間，自言食肉得菩提路。
F098: 阿難，我令比丘食五淨肉，此肉皆我神力化生，本來命根；汝婆羅門，地多蒸濕，加以砂石，草菜不生，我以大悲神力所加，
      因大慈悲，假名為肉，汝得其味。奈何如來滅度之後，食眾生肉，名為釋子。
F099: 汝等當知，是食肉人，縱得心開，似三摩地，皆大羅剎，報終必沈生死苦海，非佛弟子。如是之人，相殺相吞，相食未已，云何是人得出三界？
F100: 汝教世人，修三摩地，次斷殺生，是名如來先佛世尊，第二決定清淨明誨。
F101: 是故阿難，若不斷殺，修禪定者；譬如有人，自塞其耳，高聲大叫，求人不聞，此等名為欲隱彌露。
F102: 清淨比丘，及諸菩薩，於歧路行，不蹋生草，況以手拔？云何大悲，取諸眾生血肉充食？若諸比丘，不服東方絲綿絹帛，及是此土，
      靴履裘毳，乳酩醍醐；如是比丘，於世真脫，酬還宿債，不遊三界。何以故？服其身分，皆為彼緣，如人食其地中百谷，足不離地。
F103: 必使身心，於諸眾生，若身身分，身心二塗，不服不食，我說是人，真解脫者。如我此說，名為佛說，不如此說，即波旬說。
F104: 阿難，又復世界，六道眾生，其心不偷，則不隨其生死相續。汝修三昧，本出塵勞，偷心不除，塵不可出。縱有多智，禪定現前，如不斷偷，必落邪道；
      上品精靈，中品妖魅，下品邪人。諸魅所著，彼等群邪，亦有徒眾，各各自謂，成無上道。我滅度後，末法之中，多此妖邪，熾盛世間，潛匿奸欺，
      稱善知識，各自為己，得上人法，誘惑無識，恐令失心，所過之處，其家耗散。
F105: 我教比丘，循方乞食，令其捨貪，成菩提道。諸比丘等，不自熟食，寄於殘生，旅泊三界，示一往還，去已無返。
      云何賊人，假我衣服，裨販如來，造種種業，皆言佛法？卻非出家具戒比丘，為小乘道，由是疑誤，無量眾生，墮無間獄。
F106: 若我滅後，其有比丘，發心決定，修三摩提，能於如來形像之前，身燃一燈，燒一指節，及於身上，爇一香炷。我說是人，無始宿債，一時酬畢，
      長揖世間，永脫諸漏，雖未即明無上覺路，是人於法已決定心。若不為此捨身微因，縱成無為，必還生人；酬其宿債，如我馬麥，正等無異。
F107: 汝教世人，修三摩地，後斷偷盜，是名如來先佛世尊；第三決定清淨明誨。
F108: 是故阿難，若不斷偷，修禪定者，譬如有人，水灌漏卮，欲求其滿，縱經塵劫，終無平復。
F109: 若諸比丘，衣缽之餘，分寸不蓄，乞食餘分，施餓眾生。於大集會，合掌禮眾。有人捶詈，同於稱讚，必使身心，二俱捐捨，身肉骨血，與眾生共。
      不將如來，不了義說，迴為己解，以誤初學，佛印是人，得真三昧。如我所說，名為佛說，不如此說，即波旬說。
F110: 阿難！如是世界，六道眾生，雖則身心，無殺盜婬，三行已圓，若大妄語，即三摩地，不得清淨。成愛見魔，失如來種，所謂未得謂得，未證言證；
      為求世間尊勝第一。謂前人言，我今已得須陀洹果，斯陀含果，阿那含果，阿羅漢道，辟支佛乘，十地地前，諸位菩薩。
      求彼禮懺，貪其供養，是一顛迦，消滅佛種；如人以刀斷多羅木，佛記是人，永殞善根，無復知見，沈三苦海，不成三昧。
F111: 我滅度後，敕諸菩薩，及阿羅漢，應身生彼末法之中，作種種形，度諸輪轉。或作沙門，白衣居士，人王宰官，童男童女，如是乃至，
      婬女寡婦，奸偷屠販，與其同事，稱讚佛乘，令其身心，入三摩地；終不自言，我真菩薩，真阿羅漢，泄佛密因，輕言末學。
      唯除命終，陰有遺付，云何是人，惑亂眾生，成大妄語。
F112: 汝教世人修三摩地，後復斷除，諸大妄語，是名如來，先佛世尊，第四決定清淨明誨。
F113: 是故阿難，若不斷其大妄語者，如刻人糞，為栴檀形，欲求香氣，無有是處。
F114: 我教比丘，直心道場，於四威儀，一切行中，尚無虛假，云何自稱，得上人法？譬如窮人，妄號帝王，自取誅滅；況復法王，如何妄竊？
      因地不真，果招迂曲，求佛菩提，如噬臍人，欲誰成就？
F115: 若諸比丘，心如直弦，一切真實，入三摩地，永無魔事。我印是人，成就菩薩，無上知覺。如我此說，名為佛說；不如此說，即波旬說。

G001: 阿難，汝問攝心，我今先說入三摩地，修學妙門，求菩薩道，要先持此四種律儀，皎如冰霜，自不能生一切枝葉，心三口四，生必無因。
      阿難，如是四事，若不遺失，心尚不緣色香味觸；一切魔事，云何發生？
G002: 若有宿習，不能滅除，汝教是人，一心誦我佛頂光明，摩訶薩怛多般怛羅無上神咒。斯是如來，無見頂相，無為心佛，從頂發輝，坐寶蓮華，所說神咒。
G003: 且汝宿世，與摩登伽，歷劫因緣，恩愛習氣，非是一生，及與一劫。我一宣揚，愛心永脫，成阿羅漢。彼尚婬女，無心修行；神力冥資，速登無學。
      云何汝等，在會聲聞，求最上乘，決定成佛，譬如以塵揚於順風，有何艱險？
G004: 若有末世，欲坐道場，先持比丘，清淨禁戒，要當選擇戒清淨者，第一沙門，以為其師。若其不遇，真清淨僧，汝戒律儀，必不成就。
      戒成已後，著新淨衣，然香閑居，誦此心佛所說神咒，一百八遍。
G005: 然後結界，建立道楊，求於十方現住國土無上如來，放大悲光來灌其頂。阿難！如是末世清淨比丘，若比丘尼，白衣檀越，心滅貪婬，持佛淨戒，
      於道場中，發菩薩願，出入澡浴，六時行道。如是不寐，經三七日，我自現身，至其人前，摩頂安慰，令其開悟。
G006: 阿難白佛言：世尊！我蒙如來無上悲誨，心已開悟，自知修證，無學道成；末法修行，建立道場，云何結界，合佛世尊，清淨軌則？
G007: 佛告阿難，若末世人，願立道場，先取雪山大力白牛，食其山中肥膩香草，此牛唯飲雪山清水，其糞微細，可取其糞，和合栴檀，以泥其地。
      若非雪山，其牛臭穢，不堪塗地；別於平原，穿去地皮，五尺以下，取其黃土，和上栴檀、沈水、蘇合，薰陸、郁金、白膠、青木、零陵、甘松，
      及雞舌香，以此十種，細羅為粉，合土成泥，以塗場地，方圓丈六，為八角壇。
G008: 壇心置一金銀銅木所造蓮華，華中安砵，砵中先盛八月露水，水中隨安所有華葉。取八圓鏡，各安其方，圍繞華砵。
      鏡外建立十六蓮華，十六香爐，間華鋪設，莊嚴香爐，純燒沈水，無令見火。
G009: 取白牛乳，置十六器，乳為煎餅，並諸砂糖，油餅，乳糜，蘇合，蜜姜，純酥，純蜜，於蓮華外，各各十六，圍繞華外，以奉諸佛，及大菩薩。
      每以食時，若在中夜，取蜜半升，用酥三合，壇前別安一小火爐。
      以兜樓婆香，煎取香水，沐浴其炭，然令猛熾，投是酥蜜，於炎爐內，燒令煙盡，享佛菩薩。
G010: 令其四外，遍懸旛華，於壇室中，四壁敷設十方如來，及諸菩薩所有形像。應於當陽，張盧舍那、釋迦、彌勒、阿閦、彌陀，諸大變化，觀音形像。
      兼金剛藏，安其左右。帝釋梵王，烏芻瑟摩，並藍地迦，諸軍茶利，與毗俱胝，四天王等，頻那夜迦，張於門側，左右安置。
      又取八鏡，覆懸虛空，與壇場中，所安之鏡，方面相對，使其形影，重重相涉。
G011: 於初七中，至誠頂禮，十方如來，諸大菩薩，阿羅漢號。恒於六時，誦咒圍壇，至心行道，一時常行一百八遍。第二七中，一向專心，
      發菩薩願，心無間斷，我毗奈耶，先有願教。第三七中，於十二時，一向持佛，般怛羅咒；至第七日，十方如來，一時出現，鏡交光處，承佛摩頂。
G012: 即於道場，修三摩地，能令如是末世修學，身心明淨，猶如琉璃。
G013: 阿難，若此比丘，本受戒師，及同會中，十比丘等，其中有一不清淨者，如是道場，多不成就。
G014: 從三七後，端坐安居，經一百日，有利根者，不起於座，得須陀洹；縱其身心，聖果未成，決定自知成佛不謬。
G015: 汝問道場，建立如是。
G016: 阿難頂禮佛足，而白佛言：自我出家，恃佛憍愛，求多聞故，未證無為。遭彼梵天，邪術所禁，心雖明了，力不自由；賴遇文殊，令我解脫。
      雖蒙如來，佛頂神咒，冥獲其力，尚未親聞。
G017: 惟願大慈，重為宣說，悲救此會，諸修行輩，末及當來，在輪迴者，承佛密音，身意解脫。於時會中，一切大眾，普皆作禮，佇聞如來，秘密章句。
G018: 爾時世尊，從肉髻中，涌百寶光，光中涌出，千葉寶蓮，有化如來，坐寶華中，頂放十道，百寶光明；
      一一光明，皆遍示現，十恒河沙，金剛密跡，擎山持杵，遍虛空界。
      大眾仰觀，畏愛兼抱，求佛哀佑，一心聽佛，無見頂相，放光如來，宣說神咒。
G019: 阿難，是佛頂光聚，悉怛多般怛囉秘密伽陀，微妙章句，出生十方一切諸佛。
G020: 十方如來，因此咒心，得成無上正遍知覺；
      十方如來，執此咒心，降伏諸魔，制諸外道；十方如來，乘此咒心，坐寶蓮華，應微塵國；
      十方如來，含此咒心，於微塵國，轉大法輪；
      十方如來，持此咒心，能於十方，摩頂授記；自果未成，亦於十方，蒙佛授記。
G021: 十方如來，依此咒心，能於十方，拔濟群苦；所謂地獄，餓鬼，畜生，盲聾喑啞，怨憎會苦，愛別離苦，求不得苦，五陰熾盛，大小諸橫，同時解脫。
      賊難兵難，王難獄難，風火水難，飢渴貧窮，應念消散。
G022: 十方如來，隨此咒心，能於十方，事善知識，四威儀中，供養如意；恒沙如來會中，推為大法王子。
G023: 十方如來，行此咒心，能於十方，攝受親因；令諸小乘，聞秘密藏，不生驚怖。
G024: 十方如來，誦此咒心，成無上覺；坐菩提樹，入大涅槃。
G025: 十方如來，傳此咒心，於滅度後，付佛法事，究竟住持，嚴淨戒律，悉得清淨。
G026: 若我說是佛頂光聚般怛羅咒，從旦至暮，音聲相聯，字句中間，亦不重疊，經恒沙劫，終不能盡；亦說此咒，名如來頂。
G027: 汝等有學，未盡輪迴，發心至誠，取阿羅漢。不持此咒，而坐道場，令其身心，遠諸魔事，無有是處。
G028: 阿難，若諸世界，隨所國土，所有眾生，隨國所生，樺皮貝葉，紙素白氈，書寫此咒，貯於香囊。是人心昏，未能誦憶，或帶身上，或書宅中，
      當知是人，盡其生年，一切諸毒，所不能害。
G029: 阿難，我今為汝，更說此咒，救護世間，得大無畏；成就眾生，出世間智。
G030: 若我滅後，末世眾生，有能自誦，若教他誦，當知如是誦持眾生，火不能燒，水不能溺，大毒小毒，所不能害。
      如是乃至，天龍鬼神，精祇魔魅，所有惡咒，皆不能著。心得正受，一切咒詛，厭蠱毒藥，金毒銀毒，草木蟲蛇，萬物毒氣，入此人口，成甘露味；
      一切惡星，並諸鬼神，磣心毒人，於如是人，不能起惡。頻那夜迦，諸惡鬼王，並其眷屬，皆領深恩，常加守護。
G031: 阿難，當知是咒，常有八萬四千，那由他恒河沙俱胝，金剛藏王菩薩種族，一一皆有諸金剛眾，而為眷屬，晝夜隨侍。
      設有眾生，於散亂心，非三摩地，心憶口持，是金剛王，常隨從彼諸善男子，何況決定菩提心者。此諸金剛菩薩藏王，精心陰速，發彼神識；
      是人應時，心能記憶，八萬四千恒河沙劫，周遍了知，得無疑惑。
G032: 從第一劫，乃至後身，生生不生藥叉羅剎，及富單那，迦吒富單那，鳩槃茶，毗舍遮等，並諸餓鬼，有形無形，有想無想，如是惡處。
      是善男子，若讀若誦，若書若寫，若帶若藏，諸色供養，劫劫不生，貧窮下賤，不可樂處。
G033: 此諸眾生，縱其自身，不作福業，十方如來所有功德，悉與此人。由是得於恒河沙阿僧祇不可說不可說劫，常與諸佛，同生一處。
      無量功德，如惡叉聚，同處熏修，永無分散。是故能令破戒之人，戒根清淨；未得戒者，令其得戒；未精進者，令得精進；無智慧者，令得智慧；
      不清淨者，速得清淨；不持齋戒，自成齋戒。
G034: 阿難，是善男子，持此咒時，設犯禁戒於未受時，持咒之後，眾破戒罪，無問輕重，一時銷滅。縱經飲酒，食噉五辛，種種不淨，一切諸佛菩薩，
      金剛天仙鬼神，不將為過；設著不淨，破弊衣服，一行一住，悉同清淨。縱不作壇，不入道場，亦不行道；誦持此咒，還同入壇，行道功德，無有異也。
      若造五逆，無間重罪，及諸比丘、比丘尼，四棄八棄，誦此咒已，如是重業，猶如猛風，吹散沙聚，悉皆滅除，更無毫髮。
G035: 阿難，若有眾生，從無量無數劫來，所有一切輕重罪障，從前世來，未及懺悔；若能讀誦，書寫此咒，身上帶持，若安住處，莊宅園館；
      如是積業，猶湯銷雪，不久皆得悟無生忍。
G036: 復次阿難，若有女人，未生男女，欲求孕者；若能至心憶念斯咒，或能身上帶此，悉怛多般怛囉者，便生福德智慧男女。求長命者，即得長命；
      欲求果報，速圓滿者，速得圓滿。身命色力，亦復如是。命終之後，隨願往生十方國土，必定不生邊地下賤，何況雜形。
G037: 阿難，若諸國土，州縣聚落；飢荒疫癘，或復刀兵賊難鬥諍，兼餘一切厄難之地，寫此神咒安城四門，並諸支提，或脫闍上；
      令其國土，所有眾生，奉迎斯咒，禮拜恭敬，一心供養；令其人民，各各身佩，或各各安所居宅地，一切災厄，悉皆消滅。
      阿難！在在處處國土眾生，隨有此咒，天龍歡喜，風雨順時，五谷豐殷，兆庶安樂。
G038: 亦復能鎮一切惡星，隨方變怪，災障不起；人無橫夭，杻械枷鎖，不著其身，晝夜安眠，常無惡夢。
      阿難！是娑婆界，有八萬四千災變惡星，二十八大惡星，而為上首；復有八大惡星，以為其主。
      作種種形，出現世時，能生眾生，種種災異；有此咒地，悉皆消滅。十二由旬，成結界地；諸惡災祥，永不能入。
G039: 是故如來宣示此咒，於未來世，保護初學諸修行者，入三摩地，身心泰然，得大安隱。更無一切諸魔鬼神，及無始來，冤橫宿殃，舊業陳債，來相惱害。
      汝及眾中，諸有學人，及未來世諸修行者，依我壇場，如法持戒，所受戒主，逢清淨僧；持此咒心，不生疑悔。
      是善男子，於此父母所生之身，不得心通，十方如來，便成妄語。
G040: 說是語已，會中無量百千金剛，一時佛前合掌頂禮，而白佛言：如佛所說，我當誠心，保護如是修菩提者。
G041: 爾時梵王，並天帝釋，四大天王，亦於佛前，同時頂禮，而白佛言：審有如是修學善人，我當盡心，至誠保護，令其一生，所作如願。
G042: 復有無量藥叉大將，諸羅剎王，富單那王，鳩槃茶王，毗舍遮王，頻那夜迦，諸大鬼王；
      及諸鬼帥，亦於佛前，合掌頂禮，我亦誓願，護持是人，令菩提心，速得圓滿。
G043: 復有無量，日月天子，風師雨師，雲師雷師，並電伯等，年歲巡官，諸星眷屬，亦於會中，頂禮佛足，而白佛言：
      我亦保護，是修行人，安立道場，得無所畏。
G044: 復有無量，山神海神，一切土地，水陸空行，萬物精祇，並風神王，無色界天，於如來前，同時稽首，而白佛言：我亦保護是修行人，得成菩提，永無魔事。
G045: 爾時八萬四千那由他，恒河沙俱胝，金剛藏王菩薩，在大會中，即從座起，頂禮佛足，而白佛言：
      世尊，如我等輩，所修功業，久成菩提，不取涅槃；常隨此咒，救護末世，修三摩提，正修行者。
G046: 世尊！如是修心求正定人，若在道場，及餘經行，乃至散心遊戲聚落；我等徒眾，常當隨從侍衛此人，縱令魔王，大自在天，求其方便，終不可得。
      諸小鬼神，去此善人十由旬外；除彼發心，樂修禪者。
G047: 世尊！如是惡魔，若魔眷屬，欲來侵擾是善人者，我以寶杵，殞碎其首；猶如微塵，恒令此人，所作如願。
G048: 阿難即從座起，頂禮佛足，而白佛言：我輩愚鈍，好為多聞，於諸漏心，未求出離；蒙佛慈誨，得正熏修，身心快然，獲大饒益。
      世尊！如是修證佛三摩地，未到涅槃，云何名為，乾慧之地；四十四心，至何漸次，得修行目；詣何方所，名入地中；云何名為，等覺菩薩。
      作是語已，五體投地，大眾一心，佇佛慈音，瞪瞢瞻仰。
G049: 爾時世尊，讚阿難言，善哉善哉，汝等乃能普為大眾，及諸末世一切眾生，修三摩地，求大乘者，從於凡夫，終大涅槃，懸示無上正修行路；
      汝今諦聽，當為汝說。阿難大眾，合掌刳心，默然受教。
G050: 佛言，阿難當知，妙性圓明，離諸名相，本來無有世界眾生。
G051: 因妄有生，因生有滅，生滅名妄；滅妄名真。是稱如來無上菩提，及大涅槃，二轉依號。
G052: 阿難，汝今欲修真三摩地，直詣如來大涅槃者；先當識此眾生世界二顛倒因。顛倒不生，斯則如來真三摩地。
G053: 阿難！云何名為眾生顛倒，阿難，由性明心，性明圓故；因明發性，性妄見生，從畢竟無，成究竟有。此有所有，非因所因；
      住所住相，了無根本，本此無住，建立世界，及諸眾生。
G054: 迷本圓明，是生虛妄，妄性無體，非有所依；將欲復真，欲真已非，真真如性，非真求復，宛成非相。
G055: 非生非住，非心非法；展轉發生，生力發明，熏以成業，同業相感。因有感業，相滅相生，由是故有眾生顛倒。
G056: 阿難！云何名為世界顛倒。是有所有，分段妄生，因此界立；非因所因，無住所住，遷流不住，因此世成。三世四方，和合相涉，變化眾生，成十二類。
G057: 是故世界，因動有聲，因聲有色，因色有香，因香有觸，因觸有味，因味知法，六亂妄想，成業性故，十二區分，由此輪轉。
G058: 是故世間，聲香味觸，窮十二變，為一旋復。
      乘此輪轉，顛倒相故，是有世界，卵生、胎生、濕生、化生、有色、無色、有想、無想、若非有色、若非無色、若非有想、若非無想。
G059: 阿難！
      由因世界虛妄輪迴，動顛倒故；和合氣成，八萬四千飛沈亂想；如是故有，卵羯邏藍，流轉國土；魚鳥龜蛇，其類充塞。
G060: 由因世界雜染輪迴，欲顛倒故；和合滋成，八萬四千橫豎亂想。如是故有胎遏蒲曇，流轉國土；人畜龍仙，其類充塞。
G061: 由因世界執著輪迴，趣顛倒故；和合暖成，八萬四千翻覆亂想。如是故有濕相蔽尸，流轉國土；含蠢蝡動，其類充塞。
G062: 由因世界變易輪迴，假顛倒故；和合觸成，八萬四千新故亂想。如是故有化相羯南，流轉國土；轉蛻飛行，其類充塞。
G063: 由因世界留礙輪迴，障顛倒故；和合著成，八萬四千精耀亂想。如是故有色相羯南，流轉國土；休咎精明，其類充塞。
G064: 由因世界銷散輪迴，惑顛倒故；和合暗成，八萬四千陰隱亂想。如是故有無色羯南，流轉國土；空散銷沈，其類充塞。
G065: 由因世界罔象輪迴，影顛倒故；和合憶成，八萬四千潛結亂想。如是故有想相羯南，流轉國土；神鬼精靈，其類充塞。
G066: 由因世界愚鈍輪迴，癡顛倒故；和合頑成，八萬四千枯槁亂想。如是故有無想羯南，流轉國土；精神化為土木金石，其類充塞。
G067: 由因世界相待輪迴，偽顛倒故；和合染成，八萬四千因依亂想。如是故有非有色相，成色羯南，流轉國土，諸水母等；以蝦為目，其類充塞。
G068: 由因世界相引輪迴，性顛倒故；和合咒成，八萬四千呼召亂想。由是故有非無色相，無色羯南，流轉國土；咒詛厭生，其類充塞。
G069: 由因世界合妄輪迴，罔顛倒故；和合異成，八萬四千回互亂想。如是故有非有想相，成想羯南，流轉國土；彼蒲盧等，異質相成，其類充塞。
G070: 由因世界怨害輪迴，殺顛倒故；和合怪成，八萬四千食父母想。如是故有非無想相，無想羯南，流轉國土。如土梟等，附塊為兒；
      及破鏡鳥，以毒樹果；抱為其子，子成，父母皆遭其食，其類充塞。
G071: 是名眾生，十二種類。

H001: 阿難！如是眾生，一一類中，亦各各具十二顛倒；猶如捏目，亂華發生；顛倒妙圓真淨明心，具足如斯，虛妄亂想。
H002: 汝今修證佛三摩地，於是本因，元所亂想；立三漸次，方得除滅。如淨器中，除去毒蜜，以諸湯水，並雜灰香，洗滌其器，後貯甘露。
H003: 云何名為三種漸次。一者修習，除其助因；二者真修，刳其正性；三者增進，違其現業。
H004: 云何助因？阿難！如是世界，十二類生，不能自全，依四食住，所謂段食，觸食，思食，識食。是故佛說，一切眾生，皆依食住。
      阿難！一切眾生，食甘故生，食毒故死。是諸眾生，求三摩地，當斷世間，五種辛菜。
H005: 是五種辛，熟食發婬，生噉增恚。如是世界，食辛之人，縱能宣說，十二部經；十方天仙，嫌其臭穢，咸皆遠離。
      諸餓鬼等，因彼食次，舐其唇吻，常與鬼住；福德日消，長無利益。是食辛人，修三摩地，菩薩天仙，十方善神，不來守護；
      大力魔王，得其方便，現作佛身，來為說法。非毀禁戒，讚婬怒癡，命終自為魔王眷屬，受魔福盡，墮無間獄。
H006: 阿難，修菩提者，永斷五辛，是則名為，第一增進修行漸次。
H007: 云何正性？阿難！如是眾生，入三摩地，要先嚴持清淨戒律，永斷婬心，不食酒肉；以火淨食，無噉生氣。
      阿難！是修行人，若不斷婬，及與殺生，出三界者，無有是處。
H008: 當觀婬欲，猶如毒蛇，如見怨賊。先持聲聞四棄八棄，執身不動；後行菩薩清淨律儀，執心不起。禁戒成就，則於世間，永無相生相殺之業；
      偷劫不行，無相負累，亦於世間，不還宿債。是清淨人，修三摩地，父母肉身，不須天眼，自然觀見，十方世界，睹佛聞法，親奉聖旨；
      得大神通，遊十方界，宿命清淨，得無艱險。是則名為，第二增進修行漸次。
