C023: 即色與見，二處虛妄；本非因緣，非自然性。
C024: 阿難，汝更聽此，祇陀園中，食辦擊鼓，眾集撞鐘，鐘鼓音聲，前後相續。
C025: 於意云何？此等為是聲來耳邊？耳往聲處？阿難，若復此聲來於耳邊，如我乞食，室羅筏城，在祇陀林，則無有我。
