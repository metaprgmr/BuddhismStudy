C035: 即身與觸，二俱虛妄；本非因緣，非自然性。
C036: 阿難，汝常意中，所緣善、惡、無記三性，生成法則。
C037: 此法為復即心所生？為當離心別有方所？阿難，若即心者，法則非塵，非心所緣，云何成處？若離於心，別有方所，則法自性，為知非知？
