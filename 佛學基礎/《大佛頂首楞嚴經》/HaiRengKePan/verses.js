C089: 阿難，空性無形，因色顯發；如室羅城，去河遙處，諸剎利種，及婆羅門，毗舍、首陀、兼頗羅墮、栴陀羅等。
      新立安居，鑿井求水，出土一尺，於中則有一尺虛空，如是乃至，出土一丈，中間還得一丈虛空，虛空淺深，隨出多少。
C090: 此空為當因土所出？因鑿所有？無因自生？阿難，若復此空，無因自生；未鑿土前，何不無礙？唯見大地，迥無通達。
      若因土出，則土出時，應見空入；若土先出，無空入者，云何虛空，因土而出？若無出入，則應空土，元無異因；無異則同，則土出時，空何不出？
      若因鑿出，則鑿出空，應非出土？不因鑿出，鑿自出土，云何見空？
C091: 汝更審諦，諦審諦觀：鑿從人手，隨方運轉，土因地移，如是虛空，因何所出？鑿、空虛實，不相為用，非和非合；不應虛空，無從自出。
C092: 若此虛空，性圓周遍，本不動搖，當知現前，地、水、火、風，均名五大。性真圓融，皆如來藏，本無生滅。
      阿難！汝心昏迷，不悟四大，元如來藏，當觀虛空：為出？為入？為非出入？
C093: 汝全不知：如來藏中，性覺真空，性空真覺。清淨本然，周遍法界；隨眾生心，應所知量。
      阿難，如一井空，空生一井；十方虛空，亦復如是。圓滿十方，寧有方所，循業發現。
C094: 世間無知，惑為因緣，及自然性；皆是識心，分別計度，但有言說，都無實義。
C095: 阿難！見覺無知，因色空有。如汝今者在祇陀林，朝明夕昏；設居中宵，白月則光，黑月便暗，則明暗等，因見分析。
C096: 此見為復與明暗相，並太虛空，為同一體？為非一體？或同非同？或異非異？
      阿難！此見若復與明與暗，及與虛空，元一體者，則明與暗，二體相亡；暗時無明，明時無暗。若與暗一，明則見亡；必一於明，暗時當滅。
      滅則云何見明見暗？若明暗殊，見無生滅，一云何成？若此見精，與暗與明，非一體者，汝離明暗，及與虛空，分析見元，作何形相？
      離明離暗，及離虛空，是見元同龜毛兔角；明、暗、虛空三事俱異，從何立見？
C097: 明暗相背，云何或同？離三元無，云何或異？分空分見，本無邊畔，云何非同？見暗見明，性非遷改，云何非異？
C098: 汝更細審，微細審詳，審諦審觀：明從太陽，暗隨黑月，通屬虛空，壅歸大地，如是見精，因何所出？見覺空頑，非和非合；不應見精，無從自出？
C099: 若見聞知，性圓周遍，本不動搖，當知無邊不動虛空，並其動搖地、水、火、風，均名六大；性真圓融，皆如來藏，本無生滅。
      阿難！汝性沈淪，不悟汝之見聞覺知，本如來藏；汝當觀此見聞覺知，為生為滅？為同為異？為非生滅？為非同異？
C100: 汝曾不知：如來藏中，性見覺明，覺精明見；清淨本然，周遍法界；隨眾生心，應所知量。
      如一見根，見周法界；聽嗅嘗觸，覺觸覺知，妙德瑩然，遍周法界，圓滿十虛，寧有方所？循業發現。
C101: 世間無知，惑為因緣，及自然性；皆是識心，分別計度，但有言說，都無實義。
C102: 阿難，識性無源，因於六種根塵妄出。汝今遍觀：此會聖眾，用目循歷，其目周視，但如鏡中，無別分析；
      汝識於中，次第標指，此是文殊，此富樓那，此目犍連，此須菩提，此舍利弗。
C103: 此識了知，為生於見？為生於相？為生虛空？為無所因突然而出？
      阿難！若汝識性生於見中，如無明暗，及與色空，四種必無，元無汝見，見性尚無，從何發識？
C104: 若汝識性，生於相中，不從見生，既不見明，亦不見暗，明暗不矚，即無色空；彼相尚無，識從何發？
      若生於空，非相非見；非見無辨，自不能知，明暗色空。非相滅緣，見聞覺知，無處安立。處此二非，空則同無，有非同物，縱發汝識，欲何分別？
      若無所因，突然而生，何不曰中，別識明月？
C105: 汝更細詳，微細詳審：見托汝睛，相推前境，可狀成有，不相成無，如是識緣，因何所出？
      識動見澄，非和非合；聞聽覺知，亦復如是。不應識緣，無從自出。
C106: 若此識心，本無所從，當知了別，見聞覺知，圓滿湛然，性非從所，兼彼虛空，地水火風，均名七大，性真圓融，皆如來藏，本無生滅。
      阿難！汝心麤浮，不悟見聞，發明了知，本如來藏。汝應觀此：六處識心，為同為異？為空為有？為非同異？為非空有？
C107: 汝元不知：如來藏中，性識明知，覺明真識；妙覺湛然，遍周法界。含吐十虛，寧有方所？循業發現。
C108: 世間無知，惑為因緣，及自然性；皆是識心，分別計度，但有言說，都無實義。
C109: 爾時阿難，及諸大眾，蒙佛如來，微妙開示，身心蕩然，得無掛礙。
C110: 是諸大眾，各各自知，心遍十方；見十方空，如觀手中，所持葉物。
C111: 一切世間，諸所有物，皆即菩提，妙明元心，心精遍圓，含裹十方。
C112: 反觀父母所生之身，猶彼十方虛空之中，吹一微塵，若存若亡；如湛巨海，流一浮漚，起滅無從。
C113: 了然自知，獲本妙心，常住不滅。
C114: 禮佛合掌，得未曾有！於如來前，說偈贊佛。
C115: 妙湛總持不動尊，首楞嚴王世希有！
C116: 銷我億劫顛倒想，不歷僧祇獲法身。
C117: 願今得果成寶王，還度如是恒沙眾；將此深心奉塵剎，是則名為報佛恩。伏請世尊為證明，五濁惡世誓先入；如一眾生未成佛，終不於此取泥洹。
C118: 大雄大力大慈悲，希更審除微細惑；令我早登無上覺，於十方界坐道場。
C119: 舜若多性可銷亡，爍迦羅心無動轉。

D001: 爾時富樓那彌多羅尼子，在大眾中，即從座起，偏袒右肩，右膝著地，合掌恭敬，而白佛言。
D002: 大威德世尊！善為眾生，敷演如來第一義諦。
D003: 世尊常推說法人中，我為第一。今聞如來微妙法音；猶如聾人，逾百步外，聆於蚊蚋，本所不見，何況得聞？佛雖宣明，令我除惑。
      今猶未詳斯義究竟，無疑惑地。世尊！如阿難輩，雖則開悟，習漏未除。我等會中，登無漏者，雖盡諸漏，今聞如來，所說法音，尚紆疑悔。
D004: 世尊！若復世間，一切根塵，陰處界等，皆如來藏，清淨本然，云何忽生山河大地、諸有為相？次第遷流、終而復始？
D005: 又如來說：地水火風，本性圓融，周遍法界，湛然常住。世尊！若地性遍，云何容水？
      水性周遍，火則不生，復云何明水火二性，俱遍虛空、不相陵滅？世尊！地性障礙，空性虛通，云何二俱周遍法界？
D006: 而我不知，是義攸往，惟願如來，宣流大慈，開我迷雲，及諸大眾。作是語已，五體投地，欽渴如來，無上慈誨。
D007: 爾時世尊，告富樓那及諸會中漏盡無學諸阿羅漢：如來今日，普為此會，宣勝義中，真勝義性。令汝會中，定性聲聞，及諸一切未得二空，
      迴向上乘，阿羅漢等，皆獲一乘，寂滅場地，真阿練若正修行處。汝今諦聽，當為汝說。富樓那等，欽佛法音，默然承聽。
D008: 佛言富樓那：如汝所言，清淨本然，云何忽生山河大地？汝常不聞，如來宣說，性覺妙明，本覺明妙。富樓那言，唯然，世尊，我常聞佛宣說斯義。
D009: 佛言：汝稱覺明，為復性明，稱名為覺？為覺不明，稱為明覺？富樓那言：若此不明，名為覺者，則無所明。
      佛言：若無所明，則無明覺，有所非覺；無所非明，無明又非覺湛明性。
D010: 性覺必明，妄為明覺；覺非所明，因明立所。所既妄立，生汝妄能。無同異中，熾然成異；異彼所異，因異立同，同異發明；
      因此復立，無同無異，如是擾亂。相待生勞，勞久發塵，自相渾濁，由是引起塵勞煩惱。
D011: 起為世界，靜成虛空，虛空為同，世界為異；彼無同異，真有為法。
D012: 覺明空昧，相待成搖，故有風輪，執持世界。因空生搖，堅明立礙，彼金寶者，明覺立堅；故有金輪，保持國土。
      堅覺寶成，搖明風出，風金相摩，故有火光為變化性。寶明生潤，火光上蒸，故有水輪，含十方界。火騰水降，交發立堅；濕為巨海，乾為洲潬。
      以是義故，彼大海中，火光常起，彼洲潬中，江河常注。水勢劣火，結為高山；是故山石，擊則成焰，融則成水。土勢劣水，抽為草木；
      是故林藪，遇燒成土，因絞成水。交妄發生，遞相為種，以是因緣，世界相續。
D013: 復次富樓那，明妄非他，覺明為咎。所妄既立，明理不逾；以是因緣，聽不出聲，見不超色。色香味觸，六妄成就，由是分開，見覺聞知。
      同業相纏，合離成化。見明色發，明見想成。異見成憎，同想成愛，流愛為種，納想為胎；交遘發生，吸引同業，故有因緣生羯羅藍、遏蒲曇等。
      胎卵濕化，隨其所應；卵唯想生，胎因情有，濕以合感，化以離應。情想合離，更相變易，所有受業，逐其飛沈，以是因緣，眾生相續。
D014: 富樓那，想愛同結，愛不能離，則諸世間，父母子孫，相生不斷。是等則以欲貪為本。貪愛同滋，貪不能止，則諸世間，卵化濕胎，隨力強弱，遞相吞食，
      是等則以殺貪為本。以人食羊，羊死為人，人死為羊。如是乃至，十全之類，死死生生，互來相噉，惡業俱生，窮未來際，是等則以盜貪為本。
      汝負我命，我還汝債；以是因緣，經百千劫，常在生死。汝愛我心，我憐汝色；以是因緣，經百千劫，常在纏縛。唯殺盜婬三為根本，以是因緣，業果相續。
D015: 富樓那，如是三種，顛倒相續，皆是覺明，明了知性，因了發相。從妄見生，山河大地，諸有為相，次第遷流，因此虛妄，終而復始。
D016: 富樓那言：若此妙覺，本妙覺明，與如來心，不增不減；無狀忽生，山河大地，諸有為相。如來今得妙空明覺，山河大地，有為習漏，何當復生？
D017: 佛告富樓那：譬如迷人，於一聚落，惑南為北。此迷為復因迷而有？因悟所出？富樓那言：如是迷人，亦不因迷，又不因悟。何以故？
      迷本無根，云何因迷？悟非生迷，云何因悟？佛言：彼之迷人，正在迷時，焂有悟人，指示令悟。富樓那！於意云何？此人縱迷，於此聚落，更生迷不？
      不也！世尊。富樓那！十方如來，亦復如是。
D018: 此迷無本，性畢竟空；昔本無迷，似有迷覺，覺迷迷滅，覺不生迷。亦如翳人，見空中華，翳病若除，華於空滅。忽有愚人，於彼空華，所滅空地，
      待華更生，汝觀是人，為愚為慧？富樓那言：空元無華，妄見生滅，見華滅空，已是顛倒，敕令更出，斯實狂癡，云何更名如是狂人為愚為慧？
      佛言：如汝所解，云何問言：諸佛如來，妙覺明空，何當更出山河大地？
D019: 又如金礦，雜於精金，其金一純，更不成雜。如木成灰，不重為木。諸佛如來，菩提涅槃，亦復如是。
D020: 富樓那！又汝問言：地水火風，本性圓融，周遍法界；疑水火性，不相陵滅。又徵虛空，及諸大地，俱遍法界，不合相容。
D021: 富樓那！譬如虛空，體非群相，而不拒彼諸相發揮。所以者何？
      富樓那！彼太虛空，日照則明，雲屯則暗，風搖則動，霽澄則清，氣凝則濁，土積成霾，水澄成映。於意云何？如是殊方，諸有為相，為因彼生？
      為復空有？若彼所生，富樓那，且日照時，既是日明，十方世界，同為日色，云何空中，更見圓日？
      若是空明，空應自照，云何中宵，雲霧之時，不生光耀？當知是明，非日非空，不異空日。
D022: 觀相元妄，無可指陳，猶邀空華，結為空果，云何詰其相陵滅義？觀性元真，唯妙覺明。妙覺明心，先非水火，云何復問，不相容者。
      真妙覺明，亦復如是。汝以空明，則有空現：地水火風，各各發明，則各各現；若俱發明，則有俱現。云何俱現？富樓那！如一水中！
      現於日影，兩人同觀，水中之日，東西各行，則各有日隨二人去。一東一西先無准的。不應難言：此日是一，云何各行？各日既雙，云何現一？
      宛轉虛妄，無可憑據。
D023: 富樓那！汝以色空，相傾相奪，於如來藏；而如來藏，隨為色空，周遍法界；是故於中風動空澄，日明雲暗。眾生迷悶，背覺合塵，故發塵勞，有世間相。
D024: 我以妙明，不滅不生，合如來藏；而如來藏，唯妙覺明，圓照法界。是故於中，一為無量，無量為一，小中現大，大中現小；
      不動道場，遍十方界，身含十方，無盡虛空；於一毛端，現寶王剎，坐微塵裏，轉大法輪。滅塵合覺，故發真如妙覺明性。
D025: 而如來藏，本妙圓心，非心、非空、非地、非水、非風、非火；非眼、非耳、鼻、舌、身、意；非色、非聲、香、味、觸、法；
      非眼識界，如是乃至、非意識界。非明、無明、明無明盡；如是乃至，非老、非死，非老死盡。非苦、非集，非滅、非道；非智、非得。
      非檀那、非尸羅，非毗梨耶，非羼提，非禪那，非般剌若，非波羅蜜多。如是乃至，非怛闥阿竭、非阿羅訶、非三耶三菩、非大涅槃、
      非常、非樂、非我、非淨。
D026: 以是俱非，世出世故。
D027: 即如來藏，元明心妙，即心、即空、即地、即水、即風、即火；即眼、即耳、鼻，舌、身，意；即色、即聲、香、味、觸、法；
      即眼識界、如是乃至，即意識界。即明、無明，明無明盡，如是乃至，即老、即死，即老死盡。
      即苦，即集，即滅，即道，即智，即得。即檀那，即尸羅，即毗梨耶，即羼提，即禪那，即般剌若，即波羅蜜多。
      如是乃至即怛闥訶竭，即阿羅訶，三耶三菩，即大涅槃，即常、即樂、即我、即淨。
D028: 以是俱即，世出世故。
D029: 即如來藏，妙明心元，離即離非，是即非即。
D030: 如何世間，三有眾生，及出世間，聲聞緣覺，以所知心，測度如來無上菩提。用世間語言入佛知見。
      譬喻琴、瑟、箜篌、琵琶，雖有妙音，若無妙指，終不能發。汝與眾生，亦復如是。
      寶覺真心，各各圓滿；如我按指，海印發光；汝暫舉心，塵勞先起。由不勤求無上覺道，愛念小乘，得少為足。
D031: 富樓那言：我與如來，寶覺圓明，真妙淨心，無二圓滿。而我昔遭無始妄想，久在輪迴，今得聖乘，猶未究竟；世尊諸妄，一切圓滅，獨妙真常。
      敢問如來：一切眾生何因有妄，自蔽妙明，受此淪溺？
D032: 佛告富樓那：汝雖除疑，餘惑未盡，我以世間現前諸事，今復問汝：汝豈不聞，室羅城中，演若達多，忽於晨朝，以鏡照面，愛鏡中頭，眉目可見；
      瞋責己頭，不見面目；以為魑魅，無狀狂走。於意云何？此人何因，無故狂走？富樓那言：是人心狂，更無他故。
D033: 佛言：妙覺明圓，本圓明妙。既稱為妄，云何有因？若有所因，云何名妄？自諸妄想，展轉相因，從迷積迷，以歷塵劫；雖佛發明，猶不能返。
      如是迷因，因迷自有，識迷無因，妄無所依，尚無有生，欲何為滅？得菩提者，如寤時人，說夢中事；心縱精明，欲何因緣，取夢中物？
D034: 況復無因，本無所有。如彼城中，演若達多，豈有因緣，自怖頭走？忽然狂歇，頭非外得；縱未歇狂，亦何遺失？富樓那！妄性如是，因何為在？
D035: 汝但不隨分別，世間、業果、眾生，三種相續，三緣斷故，三因不生；則汝心中，演若達多，狂性自歇，歇即菩提。
      勝淨明心，本周法界，不從人得；何藉劬勞，肯綮修證。
D036: 譬喻有人：於自衣中，系如意珠，不自覺知；窮露他方，乞食馳走，雖實貧窮，珠不曾失。
      忽有智者，指示其珠，所願從心，致大饒富。方悟神珠，非從外得。
D037: 即時阿難，在大眾中，頂禮佛足，起立白佛：世尊，現說殺、盜、婬業，三緣斷故，三因不生。心中達多，狂性自歇，歇即菩提，不從人得；
      斯則因緣，皎然明白，云何如來，頓棄因緣？我從因緣，心得開悟。世尊！此義何獨我等年少，有學聲聞。今此會中，大目犍連，及舍利弗，
      須菩提等，從老梵志，聞佛因緣，發心開悟，得成無漏。今說菩提，不從因緣；則王舍城，拘舍梨等，所說自然，成第一義。惟垂大悲，開發迷悶。
D038: 佛告阿難：即如城中，演若達多，狂性因緣，若得滅除；則不狂性，自然而出，因緣自然，理窮於是。
D039: 阿難！演若達多，頭本自然，本自其然，無然非自，何因緣故，怖頭狂走。若自然頭，因緣故狂，何不自然，因緣故失？
      本頭不失，狂怖妄出，曾無變易，何藉因緣？
D040: 本狂自然，本有狂怖；未狂之際，狂何所潛？不狂自然，頭本無妄，何為狂走？
D041: 若悟本頭，識知狂走，因緣、自然，俱為戲論。
D042: 是故我言，三緣斷故，即菩提心。
D043: 菩提心生，生滅心滅，此但生滅；滅生俱盡，無功用道。若有自然，如是則明自然心生，生滅心滅；此亦生滅。
      無生滅者，名為自然；猶如世間，諸相雜和成一體者，名和合性。非和合者，稱本然性。
D044: 本然非然，和合非合。
D045: 合然俱離，離合俱非，此句方名，無戲論法。
D046: 菩提涅槃，尚在遙遠，非汝歷劫，辛勤修證。雖復憶持，十方如來，十二部經，清淨妙理，如恒河沙，祇益戲論。
      汝雖談說，因緣、自然，決定明了；人間稱汝，多聞第一。以此積劫多聞熏習，不能免離摩登伽難。
      何須待我佛頂神咒摩登伽心婬火頓歇，得阿那含；於我法中，成精進林，愛河乾枯，令汝解脫？
D047: 是故阿難，汝雖歷劫憶持，如來秘密妙嚴，不如一日，修無漏業，遠離世間，憎愛二苦。
      如摩登伽，宿為婬女，由咒神力，銷其愛欲，法中今名性比丘尼。與羅侯母，耶輸陀羅，同悟宿因。
      知歷世因貪愛為苦！一念熏修無漏善故，或得出纏，或蒙授記，如何自欺，尚留觀聽。
D048: 阿難及諸眾，聞佛示誨，疑惑銷除，心悟實相，身意輕安，得未曾有。重復悲淚！頂禮佛足，長跪合掌，而白佛言：
      無上大悲，清淨寶王，善開我心，能以如是種種因緣，方便提獎，引諸沈冥，出於苦海。
D049: 世尊！我今雖承，如是法音，知如來藏，妙覺明心，遍十方界；含育如來十方國土，清淨寶嚴，妙覺王剎。如來復責，多聞無功，不逮修習。
      我今猶如旅泊之人，忽蒙天王，賜與華屋，雖獲大宅，要因門入。
D050: 惟願如來，不捨大悲，示我在會諸蒙暗者，捐捨小乘，畢獲如來無餘涅槃，本發心路。令有學者，從何攝伏，疇昔攀緣，得陀羅尼，入佛知見！
      作是語已，五體投地。在會一心，佇佛慈旨。
D051: 爾時世尊，哀愍會中，緣覺聲聞，於菩提心未自在者，及為當來，佛滅度後，末法眾生，發菩堤心，開無上乘，妙修行路；宣示阿難，及諸大眾。
D052: 汝等決定發菩提心，於佛如來妙三摩提，不生疲倦；應當先明發覺初心，二決定義。云何初心，二義決定？
D053: 阿難！第一義者，汝等若欲捐聲聞，修菩薩乘，入佛知見。應當審觀因地發心，與果地覺，為同為異。
      阿難，若於因地，以生滅心為本修因，而求佛乘不生不滅，無有是處。
D054: 以是義故，汝當照明，諸器世間可作之法，皆從變滅。阿難！汝觀世間可作之法，誰為不壞？然終不聞，爛壞虛空。
      何以故？空非可作，由是始終，無壞滅故。
D055: 則汝身中，堅相為地，潤濕為水，暖觸為火，動搖為風。由此四纏，分汝湛圓妙覺明心，為視、為聽、為覺、為察，從始入終，五疊渾濁。
D056: 云何為濁？阿難！譬如清水，清潔本然，即彼塵土灰沙之倫，本質留礙，二體法爾，性不相循。
      有世間人，取彼土塵，投於淨水；土失留礙，水亡清潔，容貌汩然，名之為濁。汝濁五重，亦復如是。
D057: 阿難！汝見虛空，遍十方界，空見不分。有空無體，有見無覺，相織妄成，是第一重，名為劫濁。
D058: 汝身現摶，四大為體，見聞覺知，壅令留礙。水火風土，旋令覺知，相織妄成，是第二重，名為見濁。
D059: 又汝心中，憶識誦習，性發知見，容現六塵。離塵無相，離覺無性，相織妄成，是第三重，名煩惱濁。
D060: 又汝朝夕，生滅不停，知見每欲留於世間，業運每常遷於國土，相織妄成，是第四重，名眾生濁。
D061: 汝等見聞，元無異性，眾塵隔越，無狀異生；性中相知，用中相背，同異失准，相織妄成，是第五重，名為命濁。
D062: 阿難！汝今令見、聞、覺、知，遠契如來，常、樂、我、淨。應當先擇，生死根本；依不生滅，圓湛性成。以湛旋其虛妄滅生，伏還元覺；
      得元明覺，無生滅性，為因地心。然後圓成，果地修證。
D063: 如澄濁水，貯於靜器；靜深不動，沙土自沈。清水現前，名為初伏客塵煩惱；去泥純水，名為永斷根本無明。
      明相精純，一切變現，不為煩惱，皆合涅槃清淨妙德。
D064: 第二義者：汝等必欲發菩提心，於菩薩乘，生大勇猛，決定棄捐諸有為相；應當審詳煩惱根本。此無始來，發業潤生，誰作誰受；
      阿難！汝修菩提，若不審觀煩惱根本，則不能知虛妄根塵何處顛倒。處尚不知，云何降伏，取如來位？
D065: 阿難！汝觀世間解結之人，不見所結，云何知解？不聞虛空，被汝隳裂。何以故？空無形相，無結解故。
D066: 則汝現前，眼、耳、鼻、舌，及與身、心，六為賊媒，自劫家寶。由此無始眾生世界，生纏縛故，於器世間，不能超越。
D067: 阿難！云何名為眾生世界？世為遷流，界為方位。汝今當知，東西南北，東南西南，東北西北，上下為界，過去未來，現在為世；方位有十，流數有三。
      一切眾生，織妄相成，身中貿遷，世界相涉；而此界性，設雖十方，定位可明，世間祇目東西南北，上下無位，中無定方。
      四數必明，與世相涉，三四四三，宛轉十二，流變三疊，一十百千。
D068: 總括始終，六根之中，各各功德有千二百。
D069: 阿難，汝復於中，克定優劣。
D070: 如眼觀見，後暗前明，前方全明，後方全暗，左右旁觀，三分之二，統論所作，功德不全，三分言功，一分無德，當知眼唯八百功德。
D071: 如耳周聽，十方無遺，動若邇遙，靜無邊際，當知耳根，圓滿一千二百功德。
D072: 如鼻嗅聞，通出入息，有出有入，而闕中交，驗於鼻根，三分闕一，當知鼻唯八百功德。
D073: 如舌宣揚，盡諸世間，出世間智；言有方分，理無窮盡，當知舌根，圓滿一千二百功德。
D074: 如身覺觸，識於違順，合時能覺，離中不知，離一合雙。驗於身根，三分闕一，當知身唯八百功德。
D075: 如意默容，十方三世，一切世間，出世間法，唯聖與凡，無不包容，盡其涯際，當知意根，圓滿一千二百功德。
D076: 阿難！汝今欲逆生死欲流，返窮流根，至不生滅，當驗此等六受用根：誰合誰離？誰深誰淺？誰為圓通？誰不圓滿？若能於此悟圓通根，
      逆彼無始織妄業流，得循圓通，與不圓根，日劫相倍。我今備顯六湛圓明，本所功德，數量如是；隨汝詳擇其可入者，吾當發明，令汝增進。
D077: 十方如來，於十八界，一一修行，皆得圓滿，無上菩提，於其中間，亦無優劣。但汝下劣，未能於中，圓自在慧，故我宣揚，令汝但於一門深入。
      入一無妄，彼六知根，一時清淨。
D078: 阿難白佛言：世尊！云何逆流，深入一門，能令六根，一時清淨？
D079: 佛告阿難：汝今已得須陀洹果，已滅三界眾生世間見所斷惑。然猶未知，根中積生無始虛習，彼習要因修所斷得。何況此中，生住異滅，分劑頭數？
D080: 今汝且觀：現前六根，為一為六？阿難，若言一者，耳何不見？目何不聞？頭奚不履？足奚無語？
      若此六根，決定成六，如我今會，與汝宣揚，微妙法門，汝之六根，誰來領受？阿難言：我用耳聞。
      佛言：汝耳自聞，何關身口？口來問義，身起欽承？是故應知，非一終六，非六終一，終不汝根，元一元六。
D081: 阿難，當知是根，非一非六，由無始來顛倒淪替，故於圓湛一六義生。汝須陀洹，雖得六銷，猶未亡一。
D082: 如太虛空，參合群器，由器形異，名之異空；除器觀空，說空為一。彼太虛空，云何為汝，成同不同？何況更名，是一非一？
D083: 則汝了知，六受用根，亦復如是。
D084: 由明暗等，二種相形，於妙圓中黏湛發見；見精映色，結色成根。根元目為，清淨四大，因名眼體；如蒲萄朵，浮根四塵，流逸奔色。
D085: 由動靜等，二種相擊，於妙圓中黏湛發聽；聽精映聲，卷聲成根。根元目為，清淨四大，因名耳體；如新卷葉，浮根四塵，流逸奔聲。
D086: 由通塞等，二種相發，於妙圓中黏湛發嗅；嗅精映香，納香成根。根元目為，清淨四大，因名鼻體；如雙垂爪，浮根四塵，流逸奔香。
D087: 由恬變等，二種相參，於妙圓中黏湛發嘗；嘗精映味，絞味成根。根元目為，清淨四大，因名舌體；如初偃月，浮根四塵，流逸奔味。
D088: 由離合等，二種相摩，於妙圓中黏湛發覺；覺精映觸，摶觸成根。根元目為，清淨四大，因名身體；如腰鼓顙，浮根四塵，流逸奔觸。
D089: 由生滅等，二種相續，於妙圓中黏湛發知；知精映法，攬法成根。根元目為，清淨四大，因名意思；如幽室見，浮根四塵，流逸奔法。
D090: 阿難！如是六根，由彼覺明，有明明覺；失彼精了，黏妄發光。是以汝今，離暗離明，無有見體；離動離靜，元無聽質；無通無塞，嗅性不生；
      非變非恬，嘗無所出；不離不合，覺觸本無；無滅無生，了知安寄？
D091: 汝但不循，動、靜、合、離，恬、變、通、塞，生、滅、明、暗，如是十二諸有為相；隨拔一根，脫黏內伏，伏歸元真，發本明耀。
      耀性發明，諸餘五黏，應拔圓脫
D092: 不由前塵，所起知見，明不循根，寄根明發；由是六根互相為用。阿難！汝豈不知？今此會中，阿那律陀，無目而見；跋難陀龍，無耳而聽；
      殑伽神女，非鼻聞香；驕梵砵提，異舌知味；舜若多神，無身覺觸；如來光中，映令暫現，既為風質，其體元無。
      諸滅盡定，得寂聲聞，如此會中摩訶迦葉，久滅意根，圓明了知，不因心念？
D093: 阿難！汝今諸根，若圓拔已，內瑩發光，如是浮塵，及器世間諸變化相，如湯銷冰，應念化成，無上知覺。
D094: 阿難！如彼世人，聚見於眼，若令急合，暗相現前，六根暗然，頭足相類。彼人以手，循體外繞，彼雖不見，頭足一辨，知覺是同。
D095: 緣見因明，暗成無見；不明自發，則諸暗相，永不能昏。根塵既銷，云何覺明，不成圓妙？
D096: 阿難白佛言：世尊！如佛說言：因地覺心，欲求常住，要與果位，名目相應。世尊！如果位中，菩提、涅槃、真如、佛性、菴摩羅識、
      空如來藏、大圓鏡智，是七種名，稱謂雖別，清淨圓滿，體性堅凝，如金剛王，常住不壞。
D097: 若此見聽，離於明、暗、動、靜、通、塞，畢竟無體。猶如念心，離於前塵，本無所有。云何將此畢竟斷滅，以為修因，欲獲如來，七常住果？
D098: 世尊！若離明暗，見畢竟空；如無前塵，念自性滅。進退循環，微細推求，本無我心，及我心所，將誰立因，求無上覺？如來先說：湛精圓常；違越誠言，終成戲論。云何如來，真實語者？惟垂大慈，開我蒙吝！
D099: 佛告阿難：汝學多聞，未盡諸漏；心中徒知顛倒所因，真倒現前，實未能識。恐汝誠心，猶未信伏，吾今試將塵俗諸事，當除汝疑。
D100: 即時如來，敕羅侯羅，擊鐘一聲，問阿難言：汝今聞不？阿難、大眾俱言：我聞。鐘歇無聲，佛又問言：汝今聞不？阿難、大眾俱言：不聞。
      時羅侯羅，又擊一聲，佛又問言：汝今聞不？阿難大眾，又言：俱聞。佛問阿難：汝云何聞？云何不聞？
      阿難大眾，俱白佛言，鐘聲若擊；則我得聞；擊久聲銷，音響雙絕，則名無聞。
D101: 如來又敕羅侯羅擊鐘，問阿難言，汝今聲不？阿難、大眾俱言：有聲。少選聲銷，佛又問言：爾今聲不？阿難、大眾答言：無聲。
      有頃，羅侯羅更來撞鐘。佛又問言：爾今聲不？阿難、大眾俱言：有聲。佛問阿難：汝云何聲？云何無聲？
      阿難大眾，俱白佛言：鐘聲若擊，則名有聲；擊久聲銷，音響雙絕，則名無聲。
D102: 佛語阿難及諸大眾：汝今云何自語矯亂？大眾阿難俱時問佛：我今云何名為矯亂？
      佛言：我問汝聞，汝則言聞；又問汝聲，汝則言聲；唯聞與聲，報答無定，如是云何不名矯亂？
D103: 阿難！聲銷無響，汝說無聞；若實無聞，聞性已滅，同於枯木，鐘聲更擊，汝云何知？知有知無，自是聲塵，或無或有；豈彼聞性，為汝有無？
      聞實云無，誰知無者？是故阿難！聲於聞中，自有生滅；非為汝聞聲生聲滅，令汝聞性為有為無。汝尚顛倒，惑聲為聞；何怪昏迷，以常為斷？
      終不應言：離諸動靜、閉塞、開通，說聞無性。
D104: 如重睡人，眠熟床枕。其家有人，於彼睡時，搗練舂米；其人夢中聞舂搗聲，別作他物，或為擊鼓，或為撞鐘，即於夢時，自怪其鐘，為木石響。
      於時忽寤，遄知杵音，自告家人，我正夢時，惑此舂音，將為鼓響。阿難，是人夢中，豈憶靜搖開閉通塞？其形雖寐，聞性不昏。
      縱汝形銷，命光遷謝，此性云何，為汝銷滅？
D105: 以諸眾生，從無始來，循諸色聲，逐念流轉；曾不開悟，性淨妙常。不循所常，逐諸生滅，由是生生，雜染流轉。
D106: 若棄生滅，守於真常；常光現前，根塵識心應時銷落。想相為塵，識情為垢，二俱遠離；則汝法眼，應時清明，云何不成無上知覺。

E001: 阿難白佛言：世尊！如來雖說第二義門。今觀世間解結之人，若不知其所結之元，我信是人，終不能解。世尊！我及會中有學聲聞，亦復如是。
      從無始際，與諸無明，俱滅俱生，雖得如是多聞善根，名為出家，猶隔日瘧！惟願大慈，哀愍淪溺，今日身心，云何是結？從何名解？
      亦令未來苦難眾生，得免輪迴，不落三有。作是語已，普及大眾，五體投地，雨淚翹誠，佇佛如來，無上開示。
E002: 爾時世尊憐愍阿難及諸會中諸有學者，亦為未來一切眾生，為出世因，作將來眼。
      以閻浮檀紫金光手摩阿難頂，即時十方普佛世界六種震動。微塵如來住世界者，各有寶光，從其頂出。
      其光同時於彼世界，來祇陀林，灌如來頂。是諸大眾，得未曾有！
E003: 於是阿難及諸大眾，俱聞十方微塵如來，異口同音告阿難言：善哉，阿難！汝欲識知俱生無明，使汝輪轉生死結根，唯汝六根，更無他物；
      汝復欲知無上菩提，令汝速證安樂解脫，寂靜妙常，亦汝六根，更非他物。
E004: 阿難雖聞如是法音，心猶未明，稽首白佛：云何令我生死輪迴，安樂妙常，同是六根，更非他物？
E005: 佛告阿難：根塵同源，縛脫無二，識性虛妄，猶如空華。阿難！由塵發知，因根有相，相見無性，同於交蘆。
      是故汝今知見立知，即無明本，知見無見，斯即涅槃無漏真淨，云何是中更容他物？
E006: 爾時世尊，欲重宣此義，而說偈言：真性有為空，緣生故如幻；無為無起滅，不實如空華。言妄顯諸真，妄真同二妄，猶非真非真，云何見所見？
      中間無實性，是故若交蘆。結解同所因，聖凡無二路；汝觀交中性，空有二俱非。迷悔即無明，發明便解脫。
E007: 解結因次第，六解一亦亡；根選擇圓通，入流成正覺。
E008: 陀那微細識，習氣成瀑流；真非真恐迷，我常不開演。自心取自心，非幻成幻法；不取無非幻，非幻尚不生，幻法云何立？
E009: 是名妙蓮華，金剛王寶覺，如幻三摩提，彈指超無學，此阿毗達磨，十方薄伽梵，一路涅槃門。
E010: 於是阿難及諸大眾，聞佛如來無上慈誨，祇夜伽陀，雜糅精瑩，妙理清徹，心目開明，嘆未曾有。
E011: 阿難合掌，頂禮白佛：我今聞佛，無遮大悲，性淨妙常，真實法句。心猶未達，六解一亡，舒結倫次。
      惟垂大慈，再愍斯會，及與將來，施以法音，洗滌沈垢。
E012: 即時如來，於師子座，整涅槃僧，斂僧伽梨，攬七寶几，引手於几，取劫波羅天所奉華巾，於大眾前綰成一結，示阿難言：此名何等？
      阿難大眾，俱白佛言：此名為結。於是如來綰疊華巾，又成一結，重問阿難：此名何等？阿難大眾又白佛言：此亦名結。如是倫次綰疊華巾，總成六結。
      一一結成，皆取手中所成之結，持問阿難：此名何等？阿難大眾，亦復如是，次第詶佛，此名為結。
E013: 佛告阿難：我初綰巾，汝名為結。此疊華巾，先實一條，第二第三，云何汝曹，復名為結？阿難白佛言：世尊！此寶疊華緝織成巾，雖本一體，如我思惟：
      如來一綰，得一結名，若百綰成，終名百結。何況此巾，祇有六結，終不至七，亦不停五，云何如來，祇許初時、第二、第三，不名為結？
      佛告阿難：此寶華巾，汝知此巾，元止一條，我六綰時，名有六結。汝審觀察：巾體是同，因結有異。於意云何？
      初綰結成，名為第一，如是乃至，第六結生。我今欲將第六結名，成第一不？
      不也，世尊！六結若存，斯第六名，終非第一。縱我歷生，盡其明辯，如何令是，六結亂名？
E014: 佛言：如是，六結不同，循顧本因，一巾所造，令其雜亂，終不得成。則汝六根，亦復如是，畢竟同中，生畢竟異。
E015: 佛告阿難：汝必嫌此，六結不成，願樂一成，復云何得？阿難言：此結若存，是非鋒起，於中自生，此結非彼，彼結非此；
      如來今日，若總解除，結若不生，則無彼此，尚不名一，六云何成？
E016: 佛言：六解一亡，亦復如是。由汝無始心性狂亂，知見妄發，發妄不息，勞見發塵；如勞目睛，則有狂華，於湛精明，無因亂起。
      一切世間，山河大地，生死涅槃，皆即狂勞顛倒華相。
E017: 阿難言：此勞同結，云何解除？如來以手，將所結巾，偏掣其左，問阿難言：如是解不？不也，世尊。旋復以手，偏牽右邊，又問阿難：
      如是解不？不也，世尊。佛告阿難：我今以手，左右各牽，竟不能解，汝設方便，云何解成？阿難白佛言：世尊！當於結心解即分散。
      佛告阿難：如是！如是！若欲除結，當於結心。
E018: 阿難！我說佛法，從因緣生，非取世間和合麤相。如來發明，世出世法，知其本因隨所緣出。如是乃至恒沙界外，一滴之雨，亦知頭數；
      現前種種，松直棘曲，鵠白烏玄，皆了元由。是故阿難！隨汝心中，選擇六根，根結若除，塵相自滅，諸妄銷亡，不真何待？
E019: 阿難！我今問汝：此劫波羅巾，六結現前。同時解縈，得同除不。不也，世尊。是結本以次第綰生，今日當須次第而解；
      六結同體，結不同時，則結解時，云何同除？
      佛言：六根解除，亦復如是。此根初解，先得人空；空性圓明，成法解脫；解脫法已，俱空不生。是名菩薩，從三摩地，得無生忍。
E020: 阿難及諸大眾，蒙佛開示，慧覺圓通，得無疑惑。一時合掌，頂禮雙足，而白佛言：我等今日，身心皎然，快得無礙！雖復悟知
      ，一六亡義，然猶未達，圓通本根。世尊！我輩飄零，積劫孤露，何心何慮，預佛天倫，如失乳兒，忽遇慈母；若復因此，際會道成，所得密言；
      還同本悟，則與未聞，無有差別。惟垂大悲，惠我密嚴，成就如來，最後開示。作是語已，五體投地，退藏密機，冀佛冥授。
E021: 爾時世尊，普告眾中諸大菩薩，及諸無漏大阿羅漢：汝等菩薩，及阿羅漢，生我法中，得成無學。
      我今問汝：最初發心悟十八界，誰為圓通？從何方便，入三摩地？
E022: 憍陳那五比丘，即從座起頂禮佛足，而白佛言：我在鹿苑及於雞園，觀見如來最初成道；於佛音聲，悟明四諦。
      佛問比丘，我初稱解，如來印我，名阿若多。妙音密圓，我於音聲，得阿羅漢。佛問圓通，如我所證，音聲為上。
E023: 優波尼沙陀，即從座起，頂禮佛足，而白佛言：我亦觀佛，最初成道。觀不淨相，生大厭離，悟諸色性，以從不淨，白骨微塵，歸於虛空，
      空色二無，成無學道。如來印我，名尼沙陀，塵色既盡，妙色密圓。我從色相，得阿羅漢。佛問圓通，如我所證，色因為上。
E024: 香嚴童子，即從座起，頂禮佛足，而白佛言：我聞如來，教我諦觀，諸有為相。我時辭佛，宴晦清齋；見諸比丘，燒沈水香，香氣寂然，來入鼻中。
      我觀此氣，非木、非空、非煙、非火，去無所著，來無所從；由是意銷，發明無漏。如來印我，得香嚴號。塵氣倏滅，妙香密圓。我從香嚴，得阿羅漢。
      佛問圓通，如我所證，香嚴為上。
E025: 藥王藥上，二法王子，並在會中五百梵天，即從座起，頂禮佛足，而白佛言：我無始劫為世良醫，口中嘗此裟婆世界，草木金石，名數凡有十萬八千。
      如是悉知苦、酢、鹹、淡、甘、辛等味，並諸和合、俱生變異。是冷、是熱、有毒、無毒，悉能遍知。
      承事如來，了知味性，非空，非有，非即身心，非離身心。分別味因，從是開悟。蒙佛如來，印我昆季，藥王、藥上二菩薩名。
      今於會中，為法王子；因味覺明，位登菩薩。佛問圓通，如我所證；味因為上。
E026: 跋陀婆羅並其同伴十六開士，即從座起，頂禮佛足，而白佛言：我等先於威音王佛，聞法出家；於浴僧時，隨例入室，忽悟水因，既不洗塵，亦不洗體，
      中間安然，得無所有。宿習無忘，乃至今時，從佛出家，令得無學，彼佛名我跋陀婆羅，妙觸宣明，成佛子住。佛問圓通，如我所證，觸因為上。
E027: 摩訶迦葉，及紫金光比丘尼等，即從座起，頂禮佛足，而白佛言：我於往劫，於此界中，有佛出世，名日月燈，我得親近，聞法修學；佛滅度後，
      供養舍利，燃燈續明，以紫金光，塗佛形像，自爾以來，世世生生，身常圓滿紫金光聚；此紫金光比丘尼等，即我眷屬，同時發心。我觀世間，六塵變壞，
      唯以空寂修於滅盡，身心乃能度百千劫，猶如彈指。我以空法，成阿羅漢。世尊說我，頭陀為最，妙法開明，銷滅諸漏。佛問圓通，如我所證，法因為上。
E028: 阿那律陀，即從座起，頂禮佛足，而白佛言：我初出家，常樂睡眠，如來訶我，為畜生類。我聞佛訶，啼泣自責，七日不眠，失其雙目。
      世尊示我樂見照明金剛三昧。我不因眼，觀見十方，精真洞然，如觀掌果。如來印我，成阿羅漢。佛問圓通，如我所證，旋見循元，斯為第一。
E029: 周利槃特迦，即從座起，頂禮佛足，而白佛言：我闕誦持，無多聞性，最初值佛，聞法出家，憶持如來，一句伽陀，於一百日，得前遺後，得後遺前。
      佛愍我愚，教我安居，調出入息，我時觀息，微細窮盡，生住異滅，諸行剎那。其心豁然，得大無礙，乃至漏盡，成阿羅漢。住佛座下，印成無學。
      佛問圓通，如我所證，反息循空，斯為第一。
E030: 憍梵缽提，即從座起，頂禮佛足，而白佛言：我有口業，於過去劫，輕弄沙門，世世生生，有牛呵病。如來示我，一味清淨，心地法門，我得滅心，入三摩地，
      觀味之知，非體非物，應念得超世間諸漏。內脫身心，外遺世界，遠離三有，如鳥出籠，離垢銷塵，法眼清淨，成阿羅漢。如來親印登無學道。
      佛問圓通，如我所證，還味旋知，斯為第一。
E031: 畢陵伽婆蹉，即從座起，頂禮佛足，而白佛言：我初發心，從佛入道，數聞如來，說諸世間，不可樂事。
      乞食城中，心思法門，不覺路中，毒刺傷足，舉身疼痛！我念有知，知此深痛；雖覺覺痛，覺清淨心，無痛痛覺。
      我又思惟，如是一身，寧有雙覺，攝念未久，身心忽空，三七日中，諸漏虛盡，成阿羅漢，得親印記，發明無學。
      佛問圓通，如我所證，純覺遺身，斯為第一。
E032: 須菩提，即從座起，頂禮佛足，而白佛言：我曠劫來，心得無礙，自憶受生，如恒河沙，初在母胎，即知空寂，如是乃至，十方成空；
      亦令眾生，證得空性。蒙如來發性覺真空，空性圓明，得阿羅漢。頓入如來，寶明空海，同佛知見；印成無學，解脫性空，我為無上。
      佛問圓通，如我所證，諸相入非，非所非盡，旋法歸無，斯為第一。
E033: 舍利弗，即從座起，頂禮佛足，而白佛言：我曠劫來，心見清淨，如是受生，如恒河沙。世出世間，種種變化，一見則通，獲無障礙。
      我於路中，逢迦葉波，兄弟相逐，宣說因緣，悟心無際；從佛出家，見覺明圓，得大無畏，成阿羅漢，為佛長子，從佛口生，從法化生。
      佛問圓通；如我所證，心見發光，光極知見，斯為第一。
E034: 普賢菩薩，即從座起，頂禮佛足，而白佛言：我已曾與恒沙如來為法王子，十方如來教其弟子，菩薩根者，修普賢行，從我立名。
      世尊！我用心聞，分別眾生，所有知見；若於他方恒沙界外，有一眾生，心中發明普賢行者，我於爾時，乘六牙象，分身百千，皆至其處。
      縱彼障深，未得見我；我與其人，暗中摩頂，擁護安慰，令其成就。佛問圓通，我說本因，心聞發明，分別自在，斯為第一。
E035: 孫陀羅難陀，即從座起，頂禮佛足，而白佛言：我初出家，從佛入道，雖具戒律，於三摩地，心常散動，未獲無漏。世尊教我，及拘絺羅，觀鼻端白。
      我初諦觀，經三七日，見鼻中氣，出入如煙，身心內明，圓洞世界；遍成虛淨，猶如琉璃，煙相漸消，鼻息成白。心開漏盡，諸出入息，化為光明，
      照十方界，得阿羅漢。世尊記我，當得菩提。佛問圓通，我以銷息，息久發明，明圓滅漏，斯為第一。
E036: 富樓那彌多羅尼子，即從座起，頂禮佛足，而白佛言：我曠劫來，辯才無礙，宣說苦空，深達實相。如是乃至，恒沙如來，秘密法門，我於眾中，
      微妙開示，得無所畏。世尊知我，有大辯才，以音聲輪，教我發揚。我於佛前，助佛轉輪，因師子吼，成阿羅漢。世尊印我，說法無上。佛問圓通，
      我以法音，降伏魔怨，銷滅諸漏，斯為第一。
E037: 優波離即從座起，頂禮佛足，而白佛言：我親隨佛，逾城出家，親觀如來，六年勤苦；親見如來，降伏諸魔，制諸外道，解脫世間，貪欲諸漏。
      承佛教戒，如是乃至，三千威儀，八萬微細，性業遮業，悉皆清淨，身心寂滅，成阿羅漢。我是如來眾中綱紀，親印我心，持戒修身，眾推為上。
      佛問圓通，我以執身，身得自在；次第執心，心得通達，然後身心一切通利，斯為第一。
E038: 大目犍連，即從座起，頂禮佛足，而白佛言：我初於路乞食，逢遇優樓頻螺，伽耶、那提三迦葉波，宣說如來，因緣深義，我頓發心，得大通達。
      如來惠我，袈裟著身，鬚髮自落。我遊十方，得無掛礙，神通發明，推為無上，成阿羅漢。寧唯世尊，十方如來，嘆我神力，圓明清淨，自在無畏。
      佛問圓通，我以旋湛心光發宣，如澄濁流，久成清瑩，斯為第一。
E039: 烏芻瑟摩，於如來前，合掌頂禮佛之雙足，而白佛言：我常先憶，久遠劫前，性多貪欲。有佛出世，名曰空王。說多婬人，成猛火聚；教我遍觀，
      百骸四肢，諸冷暖氣。神光內凝，化多婬心，成智慧火。從是諸佛，皆呼召我，名為火頭。我以火光三昧力故，成阿羅漢。心發大願，諸佛成道，
      我為力士，親伏魔怨。佛問圓通，我以諦觀身心暖觸，無礙流通，諸漏既銷，生大寶焰，登無上覺，斯為第一。
E040: 持地菩薩，即從座起，頂禮佛足，而白佛言：我念往昔，普光如來，出現於世，我為比丘，常於一切要路津口，田地險隘，有不如法，妨損車馬，
      我皆平填。或作橋梁，或負沙土。如是勤苦，經無量佛，出現於世。或有眾生，於闤闠處，要人擎物，我先為擎，至其所詣，放物即行不取其值。
      毗捨浮佛，現在世時，世多飢荒，我為負人，無問遠近，唯取一錢，或有車牛，被於泥溺，我有神力，為其推輪，拔其苦惱。
E041: 時國大王，延佛設齋。我於爾時，平地待佛。毗舍如來，摩頂謂我，當平心地，則世界地；一切皆平。我即心開，見身微塵，與造世界，所有微塵，
      等無差別。微塵自性，不相觸摩，乃至刀兵，亦無所觸。我於法性，悟無生忍，成阿羅漢，回心今入菩薩位中。聞諸如來，宣妙蓮華，佛知見地；
      我先證明，而為上首。佛問圓通，我以諦觀，身界二塵，等無差別，本如來藏，虛妄發塵，塵銷智圓，成無上道，斯為第一。
E042: 月光童子，即從座起，頂禮佛足，而白佛言：我憶往昔，恒河沙劫，有佛出世，名為水天。教諸菩薩，修習水觀，入三摩地。
      觀於身中水性無奪，初從涕唾，如是窮盡津液精血，大小便利，身中旋復水性一同。見水身中，與世界外浮幢王剎，諸香水海，等無差別。
      我於是時，初成此觀，但見其水，未得無身。
E043: 當為比丘，室中安禪，我有弟子，窺窗觀室；唯見清水，遍在室中，了無所見。童稚無知，取一瓦礫投於水內，激水作聲，顧盼而去。我出定後，
      頓覺心痛，如舍利弗，遭違害鬼。我自思惟：今我已得阿羅漢道，久離病緣，云何今日，忽生心痛，將無退失？爾時童子，捷來我前，說如上事。
      我即告言：汝更見水，可即開門，入此水中，除去瓦礫。童子奉教，後入定時，還復見水，瓦礫宛然，開門除出。我後出定，身質如初。
E044: 逢無量佛，如是至於山海自在通王如來，方得亡身，與十方界，諸香水海，性合真空，無二無別。今於如來，得童真名，預菩薩會。
      佛問圓通，我以水性，一味流通，得無生忍，圓滿菩提，斯為第一。
E045: 琉璃光法王子，即從座起，頂禮佛足，而白佛言：我憶往昔，經恒沙劫，有佛出世，名無量聲。開示菩薩，本覺妙明；觀此世界，及眾生身；
      皆是妄緣，風力所轉。我於爾時，觀界安立，觀世動時，觀身動止，觀心動念，諸動無二，等無差別。我時覺了，此群動性，來無所從，去無所至，
      十方微塵，顛倒眾生，同一虛妄。如是乃至，三千大千，一世界內，所有眾生，如一器中，貯百蚊蚋，啾啾亂鳴；於分寸中，鼓發狂鬧。
E046: 逢佛未幾，得無生忍。爾時心開，乃見東方不動佛國，為法王子，事十方佛；身心發光，洞徹無礙。
      佛問圓通，我以觀察風力無依，悟菩提心，入三摩地，合十方佛，傳一妙心，斯為第一。
E047: 虛空藏菩薩，即從座起，頂禮佛足，而白佛言：我與如來，定光佛所，得無邊身。爾時手執四大寶珠，照明十方，微塵佛剎，化成虛空。
      又於自心，現大圓鏡，內放十種微妙寶光，流灌十方，盡虛空際。諸幢王剎，來入鏡內，涉入我身，身同虛空，不相妨礙。
      身能善入，微塵國土，廣行佛事，得大隨順。此大神力，由我諦觀：四大無依，妄想生滅，虛空無二，佛國本同，於同發明，得無生忍。
      佛問圓通，我以觀察虛空無邊，入三摩地，妙力圓明，斯為第一。
E048: 彌勒菩薩，即從座起，頂禮佛足，而白佛言：我憶往昔，經微塵劫，有佛出世；名日月燈明。我從彼佛，而得出家，心重世名，好遊族姓。
      爾時世尊，教我修習，唯心識定，入三摩地。歷劫已來，以此三昧，事恒沙佛，求世名心，歇滅無有。
E049: 至燃燈佛，出現於世；我乃得成，無上妙圓，識心三昧。乃至盡空如來國土，淨穢有無，皆是我心變化所現。世尊，我了如是，唯心識故；
      識性流出，無量如來，今得授記，次補佛處。佛問圓通，我以諦觀，十方唯識，識心圓明，入圓成實，遠離依他，及遍計執，得無生忍，斯為第一。
E050: 大勢至法王子，與其同倫五十二菩薩，即從座起，頂禮佛足，而白佛言：我憶往昔，恒河沙劫，有佛出世，名無量光，十二如來，相繼一劫，
      其最後佛，名超日月光；彼佛教我，念佛三昧，譬如有人，一專為憶，一人專忘，如是二人，若逢不逢，或見非見。
      二人相憶，二憶念深，如是乃至從生至生，同於形影，不相乖異。
E051: 十方如來，憐念眾生，如母憶子；若子逃逝，雖憶何為？子若憶母，如母憶時，母子歷生，不相違遠。
      若眾生心憶佛念佛，現前當來，必定見佛，去佛不遠。不假方便自得心開，如染香人，身有香氣；此則名曰：香光莊嚴。
E052: 我本因地，以念佛心入無生忍。今於此界，攝念佛人歸於淨土。佛問圓通，我無選擇，都攝六根，淨念相繼，得三摩地，斯為第一。

F001: 爾時觀世音菩薩，即從座起，頂禮佛足，而白佛言：世尊！憶念我昔無數恒河沙劫，於時有佛，出現於世，名觀世音。
      我於彼佛，發菩提心，彼佛教我，從聞、思、修，入三摩地。
F002: 初於聞中，入流亡所。
F003: 所入既寂，動靜二相，了然不生。
F004: 如是漸增，聞所聞盡。
F005: 盡聞不住，覺所覺空。
F006: 空覺極圓，空所空滅，生滅既滅，寂滅現前。
F007: 忽然超越，世出世間，十方圓明，獲二殊勝。一者上合十方諸佛本妙覺心，與佛如來，同一慈力；二者，下合十方一切六道眾生，與諸眾生，同一悲仰。
F008: 世尊！由我供養，觀音如來，蒙彼如來，授我如幻、聞薰聞修、金剛三昧，與佛如來，同慈力故，令我身成三十二應，入諸國土。
F009: 世尊！若諸菩薩，入三摩地，進修無漏，勝解現圓，我現佛身而為說法，令其解脫。若諸有學寂靜妙明，勝妙現圓；
      我於彼前，現獨覺身而為說法，令其解脫。若諸有學，斷十二緣，緣斷勝性，勝妙現圓；我於彼前，現緣覺身而為說法，令其解脫。
      若諸有學，得四諦空，修道入滅，勝性現圓；我於彼前，現聲聞身而為說法，令其解脫。
