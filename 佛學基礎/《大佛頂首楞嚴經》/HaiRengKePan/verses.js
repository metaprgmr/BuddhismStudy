C045: 此識為復，因耳所生，以耳為界；因聲所生，以聲為界。
C046: 阿難，若因耳生，動靜二相，既不現前，根不成知；必無所知，知尚無成，識何形貌？若取耳聞，無動靜故，聞無所成。
