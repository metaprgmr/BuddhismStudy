`爾時富樓那彌多羅尼子，在大眾中，即從座起。偏袒右肩，右膝著地，合掌恭敬而白佛言：大威德世尊。善為眾生敷演如來第一義諦。世尊常推說法人中，我為第一。今聞如來微妙法音，猶如聾人，逾百步外，聆於蚊蚋，本所不見，何況得聞。佛雖宣明，令我除惑，今猶未詳斯義究竟，無疑惑地。世尊，如阿難輩，雖則開悟，習漏未除。我等會中登無漏者，雖盡諸漏，今聞如來所說法音，尚紆疑悔。世尊。若復世間一切根塵陰處界等，皆如來藏清淨本然。云何忽生山河大地諸有為相。次第遷流，終而復始。又如來說，地水火風，本性圓融，周遍法界，湛然常住。世尊。若地性遍，云何容水。水性周遍，火則不生。復云何明水火二性俱遍虛空，不相陵滅。世尊。地性障礙，空性虛通，云何二俱周遍法界。而我不知是義攸往。惟願如來，宣流大慈，開我迷雲，及諸大眾。作是語已，五體投地，欽渴如來無上慈誨。

爾時世尊告富樓那，及諸會中漏盡無學諸阿羅漢：如來今日普為此會，宣勝義中真勝義性。令汝會中定性聲聞，及諸一切未得二空迴向上乘阿羅漢等，皆獲一乘寂滅場地，真阿練若，正修行處。汝今諦聽，當為汝說。富樓那等，欽佛法音，默然承聽。

佛言。富樓那。如汝所言，清淨本然，云何忽生山河大地。汝常不聞如來宣說，性覺妙明，本覺明妙。富樓那言。唯然，世尊。我常聞佛宣說斯義。佛言，汝稱覺明，為復性明，稱名為覺。為覺不明，稱為明覺。富樓那言。若此不明名為覺者，則無所明。佛言。若無所明，則無明覺。有所非覺，無所非明。無明又非覺湛明性。性覺必明，妄為明覺。覺非所明。因明立所。所既妄立，生汝妄能。無同異中，熾然成異。異彼所異，因異立同。同異發明，因此復立無同無異。如是擾亂，相待生勞。勞久發塵，自相渾濁。由是引起塵勞煩惱。起為世界。靜成虛空。虛空為同。世界為異。彼無同異，真有為法。

覺明空昧，相待成搖，故有風輪，執持世界。因空生搖，堅明立礙，彼金寶者，明覺立堅，故有金輪，保持國土。堅覺寶成，搖明風出，風金相摩，故有火光，為變化性。寶明生潤，火光上蒸，故有水輪含十方界。火騰水降，交發立堅，濕為巨海，乾為洲潬。以是義故，彼大海中火光常起，彼洲潬中，江河常注。水勢劣火，結為高山。是故山石，擊則成燄，融則成水。土勢劣水，抽為草木，是故林藪遇燒成土，因絞成水。交妄發生，遞相為種。以是因緣，世界相續。

復次富樓那。明妄非他，覺明為咎。所妄既立，明理不踰。以是因緣，聽不出聲，見不超色。色香味觸，六妄成就。由是分開見覺聞知。同業相纏，合離成化。見明色發，明見想成。異見成憎，同想成愛。流愛為種，納想為胎。交遘發生，吸引同業。故有因緣生羯羅藍遏蒲曇等。胎卵濕化，隨其所應。卵唯想生。胎因情有。濕以合感。化以離應。情想合離更相變易。所有受業，逐其飛沈。以是因緣，眾生相續。

富樓那。想愛同結，愛不能離，則諸世間父母子孫，相生不斷，是等則以欲貪為本。貪愛同滋，貪不能此，則諸世間卵化濕胎，隨力強弱，遞相吞食，是等則以殺貪為本。以人食羊，羊死為人，人死為羊，如是乃至十生之類，死死生生，互來相噉，惡業俱生，窮未來際，是等則以盜貪為本。汝負我命，我還汝債，以是因緣，經百千劫，常在生死。汝愛我心，我憐汝色，以是因緣，經百千劫，常在纏縛。唯殺盜婬三為根本。以是因緣，業果相續。

富樓那。如是三種顛倒相續。皆是覺明，明了知性，因了發相，從妄見生。山河大地，諸有為相，次第遷流。因此虛妄，終而復始。

富樓那言：若此妙覺本妙覺明，與如來心不增不減。無狀忽生山河大地，諸有為相。如來今得妙空明覺，山河大地，有為習漏，，若彼所生何當復生。佛告富樓那。譬如迷人，於一聚落，惑南為北，此迷為復因迷而有，因悟所出。富樓那言：如是迷人，亦不因迷，又不因悟。何以故？迷本無根，云何因迷。悟非生迷，云何因悟。佛言：彼之迷人，正在迷時。焂有悟人指示令悟。富樓那。於意云何。此人縱迷，於此聚落，更生迷不。不也世尊。富樓那。十方如來亦復如是。此迷無本，性畢竟空。昔本無迷，似有迷覺。覺迷迷滅，覺不生迷。亦如翳人見空中華，翳病若除，華於空滅。忽有愚人，於彼空華所滅空地，待華更生。汝觀是人為愚為慧。富樓那言：空元無華，妄見生滅。見華滅空，已是顛倒，敕令更出，斯實狂癡。云何更名如是狂人為愚為慧。佛言：如汝所解，云何問言諸佛如來妙覺明空，何當更出山河大地。又如金礦雜於精金。其金一純，更不成雜。如木成灰，不重為木。諸佛如來菩提涅槃，亦復如是。

富樓那。又汝問言：地水火風，本性圓融，周遍法界。疑水火性不相陵滅。又徵虛空及諸大地，俱遍法界，不合相容。富樓那。譬如虛空，體非群相，而不拒彼諸相發揮。所以者何。富樓那。彼太虛空，日照則明，雲屯則暗，風搖則動，霽澄則清，氣凝則濁，土積成霾，水澄成映。於意云何。如是殊方諸有為相，為因彼生，為復空有。若彼所生，富樓那，且日照時，既是日明，十方世界同為日色，云何空中更見圓日。若是空明，空應自照，云何中宵，雲霧之時，不生光耀。當知是明，非日非空，不異空日。觀相元妄，無可指陳。猶邀空華，結為空果。云何詰其相陵滅義。觀性元真，唯妙覺明。妙覺明心，先非水火。云何復問不相容者。真妙覺明亦復如是。

汝以空明，則有空現。地水火風，各各發明，則各各現。若俱發明，則有俱現。云何俱現。富樓那。如一水中現於日影。兩人同觀水中之日，東西各行，則各有日隨二人去。一東一西，先無準的。不應難言，此日是一，云何各行。各日既雙，云何現一。宛轉虛妄，無可憑據。富樓那，汝以色空相傾相奪於如來藏。而如來藏隨為色空。周遍法界。是故於中，風動空澄，日明雲暗，眾生迷悶，背覺合塵，故發塵勞，有世間相。我以妙明不滅不生合如來藏。而如來藏，唯妙覺明，圓照法界。是故於中，一為無量，無量為一。小中現大，大中現小。不動道場，遍十方界。身含十方無盡虛空。於一毛端現寶王剎。坐微塵裏轉大法輪。滅塵合覺，故發真如妙覺明性。

而如來藏本妙圓心。非心非空。非地非水。非風非火。非眼非耳鼻舌身意。非色非聲香味觸法。非眼識界，如是乃至非意識界。非明無明，明無明盡。如是乃至非老非死，非老死盡。非苦非集非滅非道。非智非得。非檀那，非尸羅，非毗梨耶，非羼提，非禪那，非般剌若，非波羅密多。如是乃至非怛闥阿竭，非阿羅訶，三耶三菩。非大涅槃。非常非樂非我非淨。以是俱非世出世故。即如來藏元明心妙。即心即空。即地即水。即風即火。即眼即耳鼻舌身意。即色即聲香味觸法。即眼識界，如是乃至即意識界。即明無明，明無明盡。如是乃至即老即死，即老死盡。即苦即集即滅即道。即智即得。即檀那，即尸羅，即毗梨耶，即羼提，即禪那，即般剌若，即波羅密多。如是乃至即怛闥阿竭，即阿羅訶，三耶三菩。即大涅槃。即常即樂即我即淨。以是俱即世出世故。即如來藏妙明心元，離即離非，是即非即。如何世間三有眾生，及出世間聲聞緣覺，以所知心測度如來無上菩提，用世語言入佛知見。譬如琴瑟箜篌琵琶，雖有妙音，若無妙指，終不能發。汝與眾生，亦復如是。寶覺真心，各各圓滿。如我按指，海印發光。汝暫舉心，塵勞先起。由不勤求無上覺道，愛念小乘，得少為足。

富樓那言：我與如來寶覺圓明，真妙淨心，無二圓滿。而我昔遭無始妄想，久在輪迴。今得聖乘，猶未究竟。世尊，諸妄一切圓滅，獨妙真常。敢問如來，一切眾生何因有妄，自蔽妙明，受此淪溺。

佛告富樓那。汝雖除疑，餘惑未盡。吾以世間現前諸事，今復問汝。汝豈不聞室羅城中，演若達多。忽於晨朝以鏡照面，愛鏡中頭眉目可見。瞋責己頭不見面目。以為魑魅無狀狂走。於意云何。此人何因無故狂走。富樓那言：是人心狂，更無他故。佛言：妙覺明圓，本圓明妙，既稱為妄，云何有因。若有所因，云何名妄。自諸妄想，展轉相因。從迷積迷，以歷塵劫。雖佛發明，猶不能返。如是迷因，因迷自有。識迷無因，妄無所依。尚無有生，欲何為滅。得菩提者，如寤時人說夢中事。心縱精明，欲何因緣取夢中物。況復無因，本無所有。如彼城中演若達多，豈有因緣自怖頭走。忽然狂歇，頭非外得。縱未歇狂，亦何遺失。

富樓那。妄性如是，因何為在。汝但不隨分別世間、業果、眾生三種相續。三緣斷故，三因不生。則汝心中演若達多，狂性自歇，歇即菩提。勝淨明心，本周法界。不從人得。何藉劬勞、肯綮、修證。譬如有人於自衣中繫如意珠，不自覺知。窮露他方，乞食馳走。雖實貧窮，珠不曾失。忽有智者指示其珠。所願從心，致大饒富。方悟神珠非從外得。

即時阿難在大眾中，頂禮佛足，起立白佛。世尊現說殺盜婬業，三緣斷故，三因不生。心中達多狂性自歇。歇即菩提，不從人得。斯則因緣皎然明白。云何如來頓棄因緣。我從因緣心得開悟。世尊。此義何獨我等年少有學聲聞。今此會中大目犍連及舍利弗須菩提等，從老梵志聞佛因緣，發心開悟得成無漏。今說菩提不從因緣。則王舍城拘舍梨等，所說自然成第一義。惟垂大悲，開發迷悶。

佛告阿難。即如城中演若達多，狂性因緣，若得滅除。則不狂性自然而出。因緣自然，理窮於是。阿難。演若達多，頭本自然。本自其然，無然非自。何因緣故，怖頭狂走。若自然頭因緣故狂。何不自然因緣故失。本頭不失，狂怖妄出。曾無變易，何藉因緣。本狂自然，本有狂怖。未狂之際，狂何所潛。不狂自然，頭本無妄，何為狂走。若悟本頭，識知狂走，因緣自然，俱為戲論。是故我言，三緣斷故，即菩提心。菩提心生，生滅心滅，此但生滅。滅生俱盡，無功用道。若有自然，如是則明，自然心生，生滅心滅，此亦生滅。無生滅者，名為自然。猶如世間諸相雜和，成一體者，名和合性。非和合者，稱本然性。本然非然。和合非合。合然俱離，離合俱非。此句方名無戲論法。菩提涅槃尚在遙遠。非汝歷劫辛勤修證。雖復憶持十方如來十二部經，清淨妙理如恒河沙，祇益戲論。汝雖談說因緣自然決定明了。人間稱汝多聞第一。以此積劫多聞熏習，不能免離摩登伽難。何須待我佛頂神咒，摩登伽心婬火頓歇，得阿那含，於我法中，成精進林。愛河乾枯，令汝解脫。是故阿難。汝雖歷劫憶持如來秘密妙嚴，不如一日修無漏業，遠離世間憎愛二苦。如摩登伽宿為婬女，由神咒力銷其愛欲，法中今名性比丘尼。與羅侯母耶輸陀羅同悟宿因。知歷世因貪愛為苦。一念熏修無漏善故，或得出纏，或蒙授記。如何自欺，尚留觀聽。

阿難及諸大眾，聞佛示誨，疑惑銷除，心悟實相。身意輕安，得未曾有。重復悲淚，頂禮佛足，長跪合掌而白佛言：無上大悲清淨寶王，善開我心。能以如是種種因緣，方便提獎，引諸沈冥出於苦海。世尊。我今雖承如是法音，知如來藏妙覺明心遍十方界，含育如來十方國土，清淨寶嚴妙覺王剎。如來復責多聞無功，不逮修習。我今猶如旅泊之人，忽蒙天王賜與華屋，雖獲大宅，要因門入。惟願如來不捨大悲，示我在會諸蒙暗者，捐捨小乘，畢獲如來無餘涅槃本發心路。令有學者，從何攝伏疇昔攀緣，得陀羅尼，入佛知見。作是語已，五體投地。在會一心，佇佛慈旨。

爾時世尊，哀愍會中緣覺聲聞，於菩提心未自在者。及為當來佛滅度後，末法眾生發菩提心，開無上乘妙修行路。宣示阿難及諸大眾。汝等決定發菩提心，於佛如來妙三摩提，不生疲倦。應當先明發覺初心二決定義。

云何初心二義決定。

阿難。第一義者，汝等若欲捐捨聲聞，修菩薩乘入佛知見，應當審觀因地發心，與果地覺為同為異。阿難。若於因地以生滅心為本修因，而求佛乘不生不滅，無有是處。以是義故，汝當照明諸器世間可作之法，皆從變滅。阿難。汝觀世間可作之法，誰為不壞。然終不聞爛壞虛空。何以故？空非可作，由是始終無壞滅故。則汝身中，堅相為地，潤濕為水，煖觸為火，動搖為風。由此四纏，分汝湛圓妙覺明心，為視為聽為覺為察。從始入終，五疊渾濁。

云何為濁。

阿難。譬如清水，清潔本然。即彼塵土灰沙之倫，本質留礙。二體法爾，性不相循。有世間人，取彼土塵，投於淨水。土失留礙，水亡清潔。容貌汩然，名之為濁。汝濁五重，亦復如是。

阿難。汝見虛空遍十方界。空見不分。有空無體。有見無覺。相織妄成。是第一重，名為劫濁。汝身現摶四大為體。見聞覺知，壅令留礙。水火風土，旋令覺知。相織妄成。是第二重，名為見濁。又汝心中憶識通習。性發知見。容現六塵。離塵無相。離覺無性。相織妄成。是第三重，名煩惱濁。又汝朝夕生滅不停。知見每欲留於世間，業運每常遷於國土。相織妄成，是第四重，名眾生濁。汝等見聞元無異性。眾塵隔越，無狀異生。性中相知。用中相背。同異失準。相織妄成。是第五重，名為命濁。

阿難。汝今欲令見聞覺知，遠契如來常樂我淨。應當先擇死生根本，依不生滅圓湛性成，以湛旋其虛妄滅生，伏還元覺，得元明覺無生滅性為因地心，然後圓成果地修證。如澄濁水，貯於靜器，靜深不動，沙土自沈，清水現前，名為初伏客塵煩惱。去泥純水，名為永斷根本無明。明相精純，一切變現，不為煩惱，皆合涅槃清淨妙德。第二義者，汝等必欲發菩提心，於菩薩乘生大勇猛，決定棄捐諸有為相，應當審詳煩惱根本。此無始來，發業潤生，誰作誰受。

阿難。汝修菩提，若不審觀煩惱根本，則不能知虛妄根塵，何處顛倒。處尚不知，云何降伏取如來位。阿難。汝觀世間解結之人，不見所結，云何知解。不聞虛空被汝隳裂。何以故？空無形相，無結解故。則汝現前眼耳鼻舌，及與身心，六為賊媒，自劫家寶。由此無始眾生世界，生纏縛故，於器世間不能超越。

阿難。云何名為眾生世界。世為遷流。界為方位。汝今當知東、西、南、北、東南、西南、東北、西北、上、下、為界。過去、未來、現在、為世。方位有十。流數有三。一切眾生織妄相成。身中貿遷，世界相涉。而此界性設雖十方，定位可明。世間祇目東西南北，上下無位，中無定方，四數必明。與世相涉，三四四三，宛轉十二。流變三疊，一十百千。總括始終。六根之中，各各功德有千二百。

阿難。汝復於中，克定優劣。如眼觀見，後暗前明。前方全明。後方全暗。左右旁觀三分之二。統論所作，功德不全。三分言功。一分無德。當知眼唯八百功德。如耳周聽，十方無遺。動若邇遙。靜無邊際。當知耳根圓滿一千二百功德。如鼻嗅聞，通出入息。有出有入，而闕中交。驗於鼻根，三分闕一。當知鼻唯八百功德。如舌宣揚，盡諸世間出世間智。言有方分，理無窮盡。當知舌根圓滿一千二百功德。如身覺觸，識於違順。合時能覺。離中不知。離一合雙。驗於身根，三分闕一。當知身唯八百功德。如意默容，十方三世一切世間出世間法，唯聖與凡，無不包容，盡其涯際。當知意根，圓滿一千二百功德。

阿難。汝今欲逆生死欲流，返窮流根，至不生滅。當驗此等六受用根，誰合誰離，誰深誰淺，誰為圓通，誰不圓滿。若能於此悟圓通根，逆彼無始織妄業流，得循圓通，與不圓根，日劫相倍。我今備顯六湛圓明，本所功德，數量如是。隨汝詳擇其可入者。吾當發明，令汝增進。十方如來，於十八界一一修行，皆得圓滿無上菩提。於其中間，亦無優劣。但汝下劣，未能於中圓自在慧。故我宣揚，令汝但於一門深入。入一無妄，彼六知根，一時清淨。

阿難白佛言：世尊。云何逆流，深入一門，能令六根一時清淨。

佛告阿難。汝今已得須陀洹果。已滅三界眾生世間見所斷惑。然猶未知根中積生無，始虛習。彼習要因修所斷得。何況此中生住異滅，分劑頭數。今汝且觀現前六根，為一為六。阿難。若言一者，耳何不見，目何不聞，頭奚不履，足奚無語。若此六根決定成六。如我今會，與汝宣揚微妙法門。汝之六根，誰來領受。

阿難言。我用耳聞。

佛言：汝耳自聞，何關身口。口來問義，身起欽承。是故應知非一終六，非六終一。終不汝根元一元六。阿難當知。是根非一非六。由無始來顛倒淪替，故於圓湛一六義生。汝須陀洹，雖得六銷，猶未亡一。如太虛空參合群器。由器形異，名之異空。除器觀空，說空為一。彼太虛空，云何為汝成同不同。何況更名是一非一。則汝了知六受用根，亦復如是。

由明暗等二種相形。於妙圓中黏湛發見。見精映色，結色成根。根元目為清淨四大。因名眼體，如蒲萄朵。浮根四塵，流逸奔色。由動靜等二種相擊。於妙圓中黏湛發聽。聽精映聲，卷聲成根。根元目為清淨四大。因名耳體，如新卷葉。浮根四塵，流逸奔聲。由通塞等二種相發。於妙圓中黏湛發嗅。嗅精映香，納香成根。根元目為清淨四大。因名鼻體，如雙垂爪。浮根四塵，流逸奔香。由恬變等二種相參。於妙圓中黏湛發嘗。嘗精映味。絞味成根。根元目為清淨四大。因名舌體，如初偃月。浮根四塵，流逸奔味。由離合等二種相摩。於妙圓中黏湛發覺。覺精映觸，摶觸成根。根元目為清淨四大。因名身體，如腰鼓顙。浮根四塵，流逸奔觸。由生滅等二種相續。於妙圓中黏湛發知。知精映法，攬法成根。根元目為清淨四大。因名意思，如幽室見。浮根四塵，流逸奔法。

阿難。如是六根，由彼覺明，有明明覺，失彼精了，黏妄發光。是以汝今離暗離明，無有見體。離動離靜，元無聽質，無通無塞，嗅性不生。非變非恬，嘗無所出。不離不合，覺觸本無。無滅無生，了知安寄。汝但不循動靜、合離、恬變、通塞、生滅、明暗，如是十二諸有為相。隨拔一根，脫黏內伏，伏歸元真，發本明耀。耀性發明，諸餘五黏，應拔圓脫。不由前塵所起知見。明不循根，寄根明發。由是六根互相為用。

阿難。汝豈不知今此會中，阿那律陀，無目而見。跋難陀龍，無耳而聽。殑伽神女，非鼻聞香。驕梵砵提，異舌知味。舜若多神，無身覺觸。如來光中，映令暫現。既為風質，其體元無。諸滅盡定，得寂聲聞。如此會中摩訶迦葉，久滅意根，圓明了知，不因心念。

阿難。今汝諸根若圓拔已，內瑩發光。如是浮塵及器世間諸變化相，如湯銷冰，應念化成無上知覺。

阿難。如彼世人聚見於眼。若令急合，暗相現前，六根黯然，頭足相類。彼人以手循體外繞，彼雖不見，頭足一辨，知覺是同。緣見因明，暗成無見。不明自發，則諸暗相永不能昏。根塵既銷，云何覺明不成圓妙。

阿難白佛言：世尊。如佛說言，因地覺心，欲求常住，要與果位名目相應。世尊。如果位中，菩提、涅槃、真如、佛性、菴摩羅識，空如來藏，大圓鏡智，是七種名，稱謂雖別，清淨圓滿，體性堅凝，如金剛王，常住不壞。若此見聽，離於明暗動靜通塞，畢竟無體。猶如念心，離於前塵，本無所有。云何將此畢竟斷滅以為修因，欲獲如來七常住果。世尊。若離明暗，見畢竟空。如無前塵，念自性滅。進退循環，微細推求，本無我心及我心所，將誰立因求無上覺。如來先說湛精圓常。違越誠言，終成戲論。云何如來真實語者。惟垂大慈，開我蒙悕。

佛告阿難。汝學多聞，未盡諸漏，心中徒知顛倒所因。真倒現前，實未能識。恐汝誠心猶未信伏。吾今試將塵俗諸事，當除汝疑。即時如來敕羅侯羅擊鐘一聲。問阿難言。汝今聞不。阿難大眾，俱言我聞。鐘歇無聲。佛又問言。汝今聞不。阿難大眾，俱言不聞。時羅侯羅又擊一聲。佛又問言。汝今聞不。阿難大眾，又言俱聞。佛問阿難。汝云何聞，云何不聞。阿難大眾俱白佛言：鐘聲若擊，則我得聞。擊久聲銷，音響雙絕，則名無聞。如來又敕羅侯擊鐘。問阿難言。汝今聲不。阿難大眾，俱言有聲。少選聲銷。佛又問言。爾今聲不。阿難大眾，答言無聲。有頃羅侯更來撞鐘。佛又問言。爾今聲不。阿難大眾，俱言有聲。佛問阿難。汝云何聲，云何無聲。阿難大眾俱白佛言：鐘聲若擊，則名有聲。擊久聲銷，音響雙絕，則名無聲。佛語阿難及諸大眾。汝今云何自語矯亂。大眾阿難，俱時問佛。我今云何名為矯亂。佛言：我問汝聞。汝則言聞。又問汝聲，汝則言聲。唯聞與聲，報答無定。如是云何不名矯亂。阿難。聲銷無響，汝說無聞。若實無聞，聞性已滅，同於枯木。鐘聲更擊，汝云何知。知有知無，自是聲塵或無或有。豈彼聞性為汝有無。聞實云無，誰知無者。是故阿難。聲於聞中自有生滅。非為汝聞，聲生聲滅。令汝聞性為有為無，汝尚顛倒，惑聲為聞。何怪昏迷，以常為斷，終不應言。

離諸動靜閉塞開通，說聞無性。如重睡人，眠熟床枕。其家有人，於彼睡時，擣練舂米。其人夢中聞舂擣聲，別作他物。或為擊鼓。或為撞鐘。即於夢時，自怪其鐘為木石響。於時忽寤，遄知杵音。自告家人，我正夢時，惑此舂音將為鼓響。阿難。是人夢中，豈憶靜搖開閉通塞。其形雖寐，聞性不昏。縱汝形銷，命光遷謝，此性云何為汝銷滅。以諸眾生從無始來，循諸色聲，逐念流轉。曾不開悟性淨妙常。不循所常，逐諸生滅。由是生生雜染流轉。若棄生滅，守於真常，常光現前，根塵識心應時銷落。想相為塵，識情為垢，二俱遠離。則汝法眼應時清明。云何不成無上知覺。`

