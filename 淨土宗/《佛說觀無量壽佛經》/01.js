'佛告阿難及韋提希：『若欲至心，生西方者，先當觀於一丈六像，在池水上。如先所說，無量壽佛，身量無邊，非是凡夫心力所及。然彼如來宿願力故，有憶想者，必得成就。但想佛像，得無邊福，況復觀佛，具足身相？阿彌陀佛，神通如意，於十方國，變現自在。或現大身，滿虛空中；或現小身，丈六八尺；所現之形，皆真金色。圓光化佛，及寶蓮華，如上所說。觀世音菩薩，及大勢至，於一切處，身同眾生。但觀首相，知是觀世音，知是大勢至，此二菩薩助阿彌陀佛普化一切。是為雜想，名第十三觀。作是觀者，名為正觀；若他觀者，名為邪觀。』',

