'如是我聞。一時佛住王舍城耆闍崛山中。與大比丘眾萬二千人俱。皆是諸大聲聞眾所知識。其名曰尊者阿若憍陳如。馬勝。大名有賢。無垢。須跋陀羅。善稱圓滿。憍梵缽提。優樓頻蠡迦葉。那提迦葉。伽耶迦葉。摩訶迦葉。舍利弗。大目揵連。摩訶迦旃延。摩訶劫賓那。摩訶注那。滿慈子。阿尼樓馱。離波多。上首王。住彼岸摩俱羅。難陀。有光善來。羅睺羅。阿難陀等。而為上首。復有菩薩摩訶薩眾。所謂普賢菩薩。文殊師利菩薩。彌勒菩薩。及賢劫中諸菩薩摩訶薩眾。前後圍繞。又與賢護等十六丈夫眾俱。所謂善思惟義菩薩。慧辯才菩薩。觀無住菩薩。善化神通菩薩。光幢菩薩。智上菩薩。寂根菩薩。慧願菩薩。香象菩薩。寶幢菩薩等。而為上首。咸共遵修普賢之道。滿足菩薩一切行願。安住一切功德法中。到諸佛法究竟彼岸。願於一切世界之中成等正覺。又願生彼兜率陀天。於彼壽終降生右脅見行七步。放大光明。普佛世界六種震動。而自唱言。我於一切世間。最為尊貴。釋梵諸天咸來親奉。又見習學書計曆數聲明伎巧醫方養生符印。及餘博戲擅美過人。身處王宮厭諸欲境。見老病死悟世非常。捐捨國位。踰城學道。解諸纓絡及迦尸迦。被服袈裟六年苦行。能於五濁剎中作斯示見。順世間故浴尼連河行趣道場。龍王迎讚。諸菩薩眾右繞稱揚。菩薩爾時。受草自敷菩提樹下。結加趺坐。又見魔眾合圍將加危害。菩薩以定慧力降伏魔怨。成無上覺。梵王勸請轉於法輪。勇猛無畏。佛音震吼擊法鼓吹法螺。建大法幢然正法炬。攝受正法及諸禪定。雨大法雨澤潤含生。震大法雷開悟一切。諸佛剎土普照大光。世界之中地皆震動。魔宮摧毀驚怖波旬。破煩惱城墮諸見網。遠離黑法生諸白法。於佛施食能受能消。為調眾生宣揚妙理。或見微笑放百千光。昇灌頂階受菩提記。或成佛道。見入涅槃。使無量有情皆得漏盡。成熟菩薩無邊善根。如是諸佛剎中皆能示見。譬如幻師善知幻術而能示見男女等相。於彼相中實無可得。如是如是諸菩薩等。善學無邊幻術功德故。能示見變化相應。能善了知變化之道故。示諸佛土見大慈悲。一切群生普皆饒益菩薩願行。成就無疆無量義門。通達平等一切善法具足修成。諸佛剎中平等趣入。常為諸佛勸進加威。一切如來識知印可。為教菩薩作阿闍梨。常習相應無邊諸行。通達一切法界所行。能善了知有情及土。亦常發趣供諸如來。見種種身猶如影像。善學因陀羅網能破魔網。壞諸見網入有情網。能超煩惱眷屬及魔侶魔人。遠出聲聞辟支佛地。入空無相無願法門。而能安住方便善巧。初不樂入二乘涅槃。得無生無滅諸三摩地。及得一切陀羅尼門。廣大諸根辯才決定。於菩薩藏法善能了知。佛華三昧隨時。悟入。具一切種甚深禪定。一切諸佛皆悉見前。於一念中遍遊佛土。周旋往返不異其時。於難非難邊能了諸邊。敷演實際。差別善知。得佛辯才住普賢行。善能分別眾生語言。超過世間一切之法。善知一切出世間法。得資具自在波羅蜜多。荷擔有情為不請友。能持一切如來法藏。安住不斷一切佛種。哀愍有情能開法眼。閉諸惡趣開善趣門。普觀有情能作父母兄弟之想。又觀眾生如己身想。證得一切。讚歎功德波羅蜜多。能善了知讚歎如來一切功德。及餘稱讚諸功德法。如是菩薩摩訶薩眾無量無邊。皆來集會。爾時尊者阿難。從坐而起整理衣服。偏袒右肩右膝著地。合掌向佛白言。大德世尊。身色諸根悉皆清淨。威光赫奕如融金聚。又如明鏡凝照光暉。從昔已來初未曾見。喜得瞻仰生希有心。世尊今者。入大寂定行如來行皆悉圓滿。善能建立大丈夫行。思惟去來現在諸佛。世尊何故住斯念耶。爾時佛告阿難。汝今云何能知此義。為有諸天來告汝耶。為以見我及自知耶。阿難白佛言。世尊。我見如來光瑞希有。故發斯念非因天等。佛告阿難。善哉善哉汝今快問。善能觀察微妙辯才。能問如來如是之義。汝為一切如來應正等覺。及安住大悲利益群生如優曇花希有大士出見世間。故問斯義。又為哀愍利樂諸眾生故。能問如來如是之義。阿難。如來應正等覺。善能開示無量知見。何以故。如來知見無有障礙。阿難。如來應正等覺欲樂住世。能於食頃住無量無數百千億那由他劫。若復增過如上數量。而如來身及以諸根無有增減。何以故。如來得三昧自在到於彼岸。於一切法最勝自在。是故阿難諦聽善思念之。吾當為汝分別解說。阿難白佛言。唯然世尊。願樂欲聞',

