'復次阿難。法處比丘。讚佛德已白言世尊。我今發阿耨多羅三藐三菩提心。惟願如來為我演說如是等法。令於世間得無等等成大菩提。具攝清淨莊嚴佛土。佛告比丘。汝應自攝清淨佛國。法處白佛言。世尊。我無威力堪能攝受。唯願如來說餘佛土清淨莊嚴。我等聞已誓當圓滿。爾時世尊。為其廣說二十一億清淨佛土具足莊嚴。說是法時經于億歲。阿難。法處比丘。於彼二十一億諸佛土中。所有嚴淨之事悉皆攝受。既攝受已。滿足五劫思惟修習。阿難白佛言。世尊彼世間自在王如來壽量幾何。世尊告曰。彼佛壽量滿四十劫。阿難。彼二十一俱胝佛剎。法處比丘所攝佛國超過於彼。既攝受已往詣世間自在王如來所。頂禮雙足右繞七匝。卻住一面白言世尊。我已攝受具足功德嚴淨佛土。佛言。今正是時。汝應具說令眾歡喜。亦令大眾皆當攝受圓滿佛土。法處白言。唯願世尊。大慈留聽。我今將說殊勝之願',

