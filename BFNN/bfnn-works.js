class BfnnWork {
  constructor(nums, title, isSutra) {
    if (Array.isArray(nums)) {
      this.id = nums[0];
      if (nums.length > 1) this.idEnd = nums[1];
    } else {
      this.id = nums;
    }
    this.title = title;
    this.isSutra = isSutra;
    if (this.id >= 9000) this.idCls = 'extra';
  }
  getTopic()    { return this.topic || (this.isSutra?'所有佛經':'其他文章'); }
  isTopic(tpc)  { return tpc == this.getTopic(); }
  getCategory() { return this.isSutra ? '佛經' : (this.topic || '其他文章'); }
  isCategory(cat) { return cat == this.getCategory(); }
  setToken(tkn) { this.token = tpc; return this; }
  isAvail()     { return this.token != '-' && this.token != '?'; }
  isToGet()     { return this.token == '?'; }
  setTopic(tpc) { this.topic = tpc; return this; }
  set(key, val) { key && val && (this[key]=val); return this; }
  isMulti()     { return !!this.idEnd; }
  numOfParts()  { return this.isMulti() ? (this.idEnd-this.id+1) : 1; }
  getSimpleData() {
    return { id:    this.idDisp(),
             token: this.token,
             title: this.title,
             topic: this.topic,
             maker: (this.author||this.translator||'').replaceAll('|','，')
           };
  }
  idDisp() {
    if (!this.isMulti()) return to4d(this.id);
    var a = to4d(this.id), b = to4d(this.idEnd), i;
    for (i=0; (i<4) && (a[i]==b[i]); ++i);
    if (i>0) b = b.substring(i);
    return `${a}~${b}`;
  }
  getSubfolder() {
    if (this.id <= 1000) return 'books';
    if (this.id <= 2016) return 'books2';
    if (this.id <= 2999) return 'books3';
    if (this.id >= 9000) return 'books9';
    throw `Don't know where to find ${this.id} work.`;
  }
  getIdAt(i) { return this.id + (i||0); }
  getURI(i) { return `${this.getSubfolder()}/${to4d(this.getIdAt(i))}.htm`; }
  getLinkDisp(i, lbl) {
    if (this.isAvail())
      return `<a class=${this.isSutra?'sutra':'orig'} href="book/${this.getURI(i)}">${lbl}</a>`;
    if (this.isToGet() && window['MY_BFNN'])
      return ` <a href="${MY_BFNN}/${this.getURI(i)}" target=ext>${lbl}</a>`;
    return lbl;
  }
  renderMultiPiece(buf) {
    var len = this.numOfParts(), buf1 = new Buffer(' ');
    for (var i=0; i<len; ++i) {
      var n = i+1;
      if (n <= 10) n = zNumber(n);
      buf1.w(' ', this.getLinkDisp(i, n));
      if (len >= 30 && i == 24) buf1.w('<br>');
    }
    if (len > 10) return buf1.render();
    buf.w(buf1.render(), '</td>');
    return null;
  }
  getTitleDisp() {
    var cls = this.isSutra ? 'sutra' : (this.isAvail() ? 'orig' : '');
    return `<span class="${cls}">${this.title}</span>`;
  }
  render(buf) {
    var ident = this.idDisp();
    switch (this.token) {
    case 'x': ident = `<WORKING>${ident}</WORKING>`; break;
    case 'f': ident = `<FOSSIL>${ident}</FOSSIL>`; break;
    case 'o': ident = `<u>${ident}</u>`; break;
    }
    var bgc = this.isToGet() ? ' bgcolor=lightgray' : '', nxtln;
    buf.w(`<tr${bgc}><td valign=top${toAttr('class', this.idCls)}>${ident}</td>`);
    var n = !this.note ? '' : (' ' + this.note);
    if (this.isMulti()) {
      buf.w(`<td nowrap valign=top>${this.getTitleDisp()}${n}`);
      nxtln = this.renderMultiPiece(buf);
    } else {
      var a = this.title.split(' ');
      for (var i=0; i<a.length; ++i)
        a[i] = this.getLinkDisp(i, a[i]);
      buf.w(`<td nowrap valign=top>${a.join(' 又名 ')}${n}</td>`);
    }
    buf.w(`<td valign=top align=right>${(this.author||this.translator||'').replaceAll('|','，')}</td></tr>`);
    nxtln && buf.w(`<tr${bgc}><td></td><td colspan=2>${nxtln}</td></tr>`);
  }

} // end of BfnnWork.

