function parseBfnnWorks(data) {
  var a = data.split('\n'), pgs=0, allPgs=0, items=0, cntOld=0;
  for (var i in a) {
    var ln = a[i], note = null, topic = null;
    var token = ln[0];
    if (token == 'o' || token == 'f') ++cntOld;
    ln = ln.substring(1);
    var idx = ln.indexOf('//');
    if (idx > 0) {
      note = ln.substring(idx+2).trim();
      ln = ln.substring(0,idx);
    }
    if (ln.trim().length == 0) // empty line or comment only
      continue;
    idx = ln.indexOf('#');
    if (idx > 0) {
      topic = ln.substring(0,idx).trim();
      ln = ln.substring(idx+1).trim();
    }
    // get the number(s)
    idx = ln.indexOf(' ');
    var nums = ln.substring(0, idx),
        segs = ln.substring(idx+1).split('|'),
        ttl = segs[0],
        ttlextra = null,
        author = segs[1],
        translator = null,
        isSutra = false;
    if (author && (author.indexOf('譯')>0)) { translator = author; author = null; }
    if (nums.endsWith('=!')) { isSutra = true; nums = nums.substring(0,nums.length-2); }
    if (nums.endsWith('~')) {
      nums = parseInt(nums.substring(0, nums.length-1));
      nums = [ nums, nums ]; // single container htm for multiple pages
    } else {
      var nums = nums.split('~');
      if (nums.length == 1) // a single page
        nums = parseInt(nums[0]);
      else { // a series of a few pages
        var len1 = nums[0].length, len2 = nums[1].length;
        if (len2 < len1)
          nums[1] = nums[0].substring(0, len1-len2) + nums[1];
        nums[0] = parseInt(nums[0]);
        nums[1] = parseInt(nums[1]);
      }
    }

    const ADHOC = 'ADHOC';
    var wk;
    if (Array.isArray(nums) && nums[0] == 119)
      wk = new Bfnn0119();
    else if (note && note.startsWith(ADHOC))
      wk = new BfnnWorkSpecial(nums, ttl, isSutra).set('note', note.substring(ADHOC.length).trim());
    else
      wk = new BfnnWork(nums, ttl, isSutra).set('note', note);
    wk.set('author',     author)
      .set('translator', translator)
      .set('token',      token)
      .set('topic',      topic);
    BFNN_WORKS.push(wk);
    allPgs += wk.numOfParts();
    if (wk.isAvail()) { ++items; pgs += wk.numOfParts(); }
  }
  console.log(`${items}/${BFNN_WORKS.length} items in ${pgs}/${allPgs} pages.`);
  console.log(`${cntOld} items in old style.`);
  STATS = `${items}/${BFNN_WORKS.length} items in ${pgs}/${allPgs} pages. ${cntOld} items in old style.`;
}

