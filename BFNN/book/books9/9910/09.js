請大家打開《講義》第十九頁，辛二、釋心生滅門

本論的宗旨，是要引導我們眾生能夠離一切苦，得究竟樂。怎麼樣才能夠成就究竟的離苦得樂？從本論的開導中，我們必須修學三個主要的法門：第一個是觀照力、第二個是誓願力、第三個是行動力。這三個法門的修學有一定的次第。也就是說，我們一定要先成就對生命的正確觀照，才能夠產生正確的希望，引導一個正確修學的行動。

所以在本論的解釋分中，剛開始就是引導我們能夠正確的觀察生命的真相。生命的真實相可以說是一個無止盡的水流，從過去流到現在，也會從現在流到未來，在這樣一個相續變化的水流中，我們應該如何來了解生命的真相？從馬鳴菩薩的開導，我們應該從兩個角度來觀察生命的真相。

/SECTION/一、從假入空

剛開始，我們應該是從假入空觀，就是依止我們現前這個身心世界的果報體，來觀察我們的身心世界，不管是外在的身相、或者是內心的思想，都是假藉種種業力的因緣變現出來的。所以我們說因緣所生法，我說即是空，透過緣起性空觀，把這些假藉因緣所生的身心世界撥開來；就觀照到我們內心深處的清淨本然，週遍法界的真如現前。在真如的理性中，是不生不滅、不垢不淨、不增不減的一種絕待平等的生命，我們稱為所謂的整體生命，或者叫真如門。也就是說我們在觀察這個水流時，我們先觀察在整個波浪的變化中，那個不生不滅、不增不減、不垢不淨的濕性，就看到整個生命總相的一個整體生命。

/SECTION/二、從空出假

第二個方法，我們可以從空出假觀，從絕待平等的生命中，再回到現實的個體生命。可以了解到：我們由於過去某一個時空所創的業力，這個業力就出現一個暫時流轉的個體生命。你現在的生命有一個外表的形狀，這個外表的形狀在內部有很多的受想行識──很多的感受、很多的思想、很多的了別作用，這個都是你在過去的某一個因緣中所創造的業力變現出來的。就是說你從一種平等的法界，又回歸到你個體生命的觀察。我們就是從個體生命的觀察又回到本體生命，從本體生命又回到個體生命；從真如而觀察生滅，從生滅而觀察真如。你能夠從真如跟生滅這二個角度來觀察生命，你就是正確而不顛倒的了解到生命真的實相，也就是你已經具足大乘的觀照力。

前面一科主要是針對於整個流轉生命中的真如門，一個平等的生命來說明；以下心生滅門就講到我們在流轉中，由於業力變現的個體生命。

