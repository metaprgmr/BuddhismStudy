前面是一個總標，這以下把覺跟不覺這兩個內涵各別加以解釋，這當中分兩科，先看寅一、釋覺義，先解釋覺悟的功能及相貌。覺中分成三小科，先看卯一、總立本始二覺。這個覺有兩個：第一個是本覺，第二個是識始覺，先把這兩個差別的內涵說出來。我們先看第一段：

/VERSE/所言覺義者，謂心體離念。離念相者，等虛空界，無所不遍。法界一相，即是如來平等法身。依此法身，說名本覺。

/SECTION1/什麼叫做覺？

先解釋什麼叫做本覺。在講本覺之前，先解釋什麼叫做覺？我們平常說：你這個人是覺悟了。那什麼叫覺悟呢？它的定義，簡單的講就是心體離念。平常我們一般人的心就打很多的妄想，以自我意識為中心，變造很多善的妄想、或者邪惡的妄想。當我們內心中有一種功能生起，它能夠離念，這個離念簡單的說就是遠離一切的妄念，佛法的名詞叫做對治。就是說你內心中，或者修止、或者念佛、或者持咒、或者觀想修觀，總之你對你心中的妄想有對治的功能，當這個功能生起的時後，我們安立你內心已經安住在覺悟的內涵中。

外道也會做慈善事業，蓋個醫院、或者放生，但是這樣不能講覺；因為他只是在阿賴耶識中成就一個善業，但這個善業它不能夠離念的。雖然造了善業，但是你的妄想還是很粗重──弟子心不安，你那個不安穩性依舊存在，所以不能夠叫覺。也就是說，當你覺悟的功能生起時，對你的妄念有遠離、有對治的功能，佛法才定義做覺，覺是這個意思，這個跟積集善業是不同。解釋本覺之前，我們先解釋什麼叫覺？有一種能夠遠離妄念的功能，這叫做覺。知道這個覺以後，就可以解釋什麼叫本覺了。

離念相者，等虛空界，無所不遍。法界一相，即是如來平等法身。依此法身，說明本覺。什麼叫本覺呢？這裡先講一個譬喻：等虛空界，用虛空來形容本覺的相貌。這裡法藏大師解釋說：為什麼用虛空呢？因為虛空有兩層的意思：

第一個、週遍義

無所不遍。就是說不管你是好人、壞人，你都是活在虛空中，沒有一個人能夠遠離虛空而安住的；也就是說本覺它是週遍在整個十方一切凡聖的眾生中，皆平等具足，一切的凡聖都平等具足的，所以它有週遍義。

第二個、無差別義

虛空是法界一相沒有差別。我們之前講過什麼叫無差別呢？簡單的講有六層的無差別：不生、不滅、不垢、不淨、不增、不減，它遠離生滅的差別，遠離垢淨的差別，遠離增減的差別，它是無差別的。你造善業，本覺也是這樣；你造惡業，本覺也是這樣：它不會因為你造業而有所變化。簡單的說它是週遍義、是無差別義，它是如來清淨的平等法身，依止這個平等法身，我們安立一個名稱叫做本覺，本來具足的一個覺性。

就是說我們一般的個體生命，就是一個五蘊身心，你由過去在阿賴耶識中某一期的業力：假設有些人是善業強，他這一期的個體生命特別殊勝，外相特別的莊嚴健康；他的內心世界也特別強悍，有堪忍性，心力強，內心中快樂的時間也多。他不斷的造善業，他的五蘊身心就是特別的不斷的變化，來生會更好。假設有些人是罪業強，他所變現的五蘊身心，這個個體生命就比較差，外相看起來也比較丑陋；他的內心世界一天到晚經常感到昏昏沉沉的，要思惟東西、要打坐，這個心提不起功能，這個五蘊差。五蘊個體生命這個修行的工具，是過去罪業所招感的，就比較差。

所以業力是主導我們五蘊身心的變化。但是這裡本覺不是這個意思，它是不變化的，無所不遍，法界一相。到這裡，我們應該都有一些觀念，就是你看到一些不變化的東西一定是真如門，真如門有如實空跟如實不空：它如實空，離一切相，覺跟不覺都是離的，如實空是離一切的相；如實不空，就是如來藏具足無漏性功德得故，這個就是本覺，以本具的覺悟來總攝一切的無漏功德。

所以這裡是說明真如門不空這一部分叫本覺。

/VERSE/何以故？本覺義者，對始覺說。以始覺者，即同本覺。始覺義者，依本覺故，而有不覺。依不覺故，說有始覺。

