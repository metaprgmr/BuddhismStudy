第一段、「本覺義者，對始覺說。以始覺者，即同本覺。」

把本覺跟始覺的關係講出來，說本覺的安立是相對於始覺而安立的。這個我們解釋一下。這個本覺是眾生本具的，不必你去造作，它就法爾存在我們內心的深處，有一種覺悟的功能等待我們去啟發它，所以叫做本，這個本就是本來具。

這個始就是有個開始，就是本來沒有，始覺不是每個眾生都有的，這一定要有個開始，有個開始當然就是生滅相。就是說我們凡夫無量劫來是隨順於一念的無明，無明一動了以後，眾生就一念不覺開始向外攀緣。這個一念的妄動以後，糟糕了！停不下來，就一直動下去了。

當然我們在無量的生命中，大部分的時間都是跟著感覺走，我這一念心跟環境接觸時，產生一個感覺、一個心情。我現在的心情想做什麼事就去做了，我現在的心情想要做那件事情也就去做了，從來沒有用道德理性來約束自己的感情，就是跟著感覺走，這樣在個體生命中，就招感很多痛苦的果報。我們在痛苦中就產生一個慚愧心：可能跟三寶有一點結緣，個體生命中就生長在有三寶住世的地方；看到佛像的莊嚴，生歡喜心；參加法會，唱唱爐香讚，就刺激內心本覺的功能；這時發心修行、斷惡修善。

當我們開始發心修行、斷惡修善時，內心中那些妄念開始對治，我們開始用佛法的道理來檢查我們內心的活動──有些該做、有些不該做，開始對治，這時我們安立你始覺的智慧正式生起、正式啟動。

所以本覺義者是針對始覺而安立的，它們兩個是相對的，一個是本來具的，一個是你要發心才有的。下面這一段很重要，「以始覺者，即同本覺。」始覺跟本覺不同，但是它即同本覺，這個同就是隨順，它有開顯本覺的功能，就是說始覺是從本覺而引生的。就是我們的本覺遇到三寶因緣的刺激，從本覺裡面引生始覺，這個始覺當然慢慢擴大。始覺的擴大，也就不斷的開顯本覺的功能，所以講以始覺者，即隨順於本覺的體性，這是第一段的回答。

