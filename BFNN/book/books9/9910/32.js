當無住法門生起時力量很大的！

金剛三昧，或者叫首楞嚴王三昧，這個首楞嚴王就是畢竟堅固，不可破壞。所以說，這個三昧剛開始修時，你要花很多時間去建立大乘的正見，依止正見去隨順觀察，這樣花很多的時間。但是你一上路以後，這個成佛之道是遲早的問題，你要精進就早一點，你要懈怠就要慢一點；但最怕就是你還沒有上路。那這裡就是你要趕快用功，趕快找到這個路上去。

