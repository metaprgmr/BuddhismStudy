為什麼起觀，或者歸依三寶就能夠遠離諸業障呢？我們修行時，一定要有一個基本觀念：邪不勝正。邪惡是黑暗，正是光明，光明能夠破除黑暗，不是黑暗能夠破除光明。那為什麼邪不勝正，還有些人會著魔呢？因為他內心沒有正氣，他沒有一直正念。就是說這個房間為什麼有黑暗呢？因為你根本沒有點起光明，所以只要你肯做一點點小小的努力，這個邪就消失掉，就怕你自己起顛倒，那就沒辦法了。所以說只要我們能夠當勤正念，不取不著，這些障礙根本就不是障礙，他自然能夠遠離，因為邪不勝正。以下就把這個外道的三昧作一個總結：

/VERSE/應知外道所有三昧，皆不離見愛我慢之心，貪著世間名利恭敬故。

這個天魔外道所產生的三昧、這種邪定，從內心的相貌來說，皆不離見愛我慢煩惱，就是愛見的煩惱，簡單的說就是一種有所得的攀緣心，所以這是一個非常重要的判定標準。就是說當你用功以後，你的我見愈高，對世俗的貪念愈深，那保證你這個方法是錯誤的，你已經沒有走在道上了。這是一個很容易判斷的標準。從外在的形狀來說，貪著世間名利恭敬，這裡也是一種向外攀緣的相貌。就是說，本來我們修真如三昧是深伏煩惱，增長大乘的信心；結果你卻增長我愛、我見，貪著世間名利，那你這個方向就錯誤了。這個錯誤也可能是我們自己的方法操作錯誤，也可能是有外力干擾，你自己要作一些調整與檢討。

