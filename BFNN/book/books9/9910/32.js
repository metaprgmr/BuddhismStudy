這裡是說，怎麼知道我們是正確的走在路上了呢？第一個不住見相，第二個不住得相。從一個修學次第，我們剛開始先不住得相，就是我們對於所緣的境界相，觀察它是唯心所現，如夢如幻，遠離外在的境界，這是不住得相。不住見相，就是我們能念的這一念心，內心的妄想也是念念不生、念念不滅，遠離外在的境界，遠離內心的妄想。

當你內心中感覺到你這一念心跟境界接觸時，慢慢能夠見相、離相，當然真如三昧不是說都沒有境界相，那你這個人就沒有明瞭性。就是說這個境界對你來說，它不能誘惑你、不能干擾你，乃至於你內心中的妄想對你的干擾也是慢慢淡薄，這就對了。在定中如是，乃至出定以後，你的內心中沒有懈怠、高慢之心，所有的煩惱就漸漸的輕薄了。

