當然無住以後，後面講到修觀，要你生心，生起智慧的觀照，生起大悲心，那是後面的內涵。當然你連無住都沒有達到，後面的觀就沒有意義了。所謂沒有意義是說：從成佛之道來說，你就不相應。所以這裡跟無想是差太遠了。第一個，無想它沒有起觀；第二個無想是有所住，它是以無想為住，這裡不同。

【學員提問】儻欲入真如門，一定要修論中的止觀法，若具大乘正見，而修我空、法空觀，是否亦能至之，只是遲速不同？

【師父答疑】本論的無住法門，它遠離外境的執取，遠離內心的妄想，它這是一個方便。不管什麼法門都有無量方便，不是說一定要依止本論來修學。如果你有大乘正見，而修我空觀、法空觀，也能夠趣入真如。不過這裡，我們注意一下：我空觀、法空觀當然是大乘共法，但是智者大師的判教：這個我空觀、法空觀是通於大乘的三系，通、別、圓都是修我空觀、法空觀。

為什麼中觀的學者跟唯識的學者，他們沒有真實的契入真如三昧呢？就是說你這個我空觀、法空觀是向內、或者向外的差別。如果你是心外求法，離開了一念心性，另外有一個我空觀、法空觀，那這樣就落入別教、通教的思想。如果你這個我空觀、法空觀的修行，是慢慢慢慢趣向於一心三昧，你是向內的安住，所破的是內心的執取，向內的安住，這樣就對了。這裡，以向內跟向外的觀察來作一個判斷。

我們可以回憶一下：在前面觀察真如它說：是故一切諸法，從本以來，離言說相、離文字相、離心緣相，畢竟平等，無有變異，不可破壞，唯是一心，故名真如。當然離言說相、離文字相、離心緣相，畢竟平等，無有變異，不可破壞，這個都是共通教、別教，但是不共的地方──唯是一心！

所以在《教觀綱宗》中有一個觀，就是你觀一切法空時，你那個空有沒有明瞭性，這是關鍵。如果你那個空是個理性，那這是別教；如果你那個空是安住在心性，唯是一心，那這個是圓教。

所以唯識學的真如是不受熏的。諸位知道，唯識的真如是一個法，它怎麼受熏呢？它是一個道理。圓教的真如是依止一心安立，它是一個明瞭的心性安立真如，它是向內的。所以唯識學他不同意每一個人都有真如，因為真如是心外的一個道理，你要去追求才有，所以它真如不能受熏。

大乘佛法是認為：真如它受熏，因為顛倒的因緣，它變成無明；因為覺悟的因緣，它產生萬德莊嚴。你剛剛也說的對，你要依止大乘正見，你依止一心來修習一切法門，你的出發點是依止現前一念心性，來觀察這些果報的差別，一念心性真如受熏。你用殺盜淫妄的業來熏真如，真如現出地獄刀山油鍋；你用五戒、十善的業來熏真如，真如就不思議心，不思議變現出人天果報。你用四諦、十二因緣熏真如，出現偏空涅槃；你用六波羅蜜，出現萬德莊嚴。你這樣的思考，然後用我空觀、法空觀來作一種調伏煩惱的方便，以無所得為方便，那這樣你的我空觀、法空觀肯定趣向真如。

就是你的根本思想是依止一念心性起修，那就對了──稱性起修。如果你真如的概念是離開一念心性，另外有一個真如，你必須去追求，向外追求，你最後的功德是一個權教的功德、別教的功德。我們不能夠從你所修的法門來判定你的種性，就是你對所有法門的依止點。所以《金剛經》說：一切賢聖，皆以無為法而有差別。就是你依止無為的那個真理，會影響到你未來的果證，這裡是個問題。

【學員提問】論中提起求生淨土，是為為求正信而怯弱的眾生所施設的勝異方便，這是後面會講到。為什麼法藏大師在論初的因緣分中，把修習止觀判作下品的中品；而把專念的念佛，判作下品的上品。就是念佛比止觀還高？

【師父答疑】這裡我們說明一下：這個念佛法門，套一句蕅益大師的話是：淺者見淺，深者見深，三根普被。念佛法門我們如果把它詳細的展開來，根據蕅益大師《大勢至菩薩念佛圓通章》的開示：念佛法門具足來說有三種差別：

第一個、專念他佛：這個人他不能明白萬法唯是一心的道理，他是心外求法，依止有所得的無明來修習念佛。當然有所得的念佛，他就會思考：我是業障凡夫，我是生命中一個有自性的個體；阿彌陀佛是萬德莊嚴，他也是一個個體。我們二個之間是完全沒有交集的，沒有交集但是沒關係，我是業障凡夫，我乞求彌陀的救護，這樣也非常好。但是這樣，當你在滅惡生善時，力量會比較薄弱；萬一有障緣出現時，你要滅除障礙會比較辛苦。入道多辛苦，這個叫做事持。但這樣依止信心，也可以往生，依止信願力往生，這個就是專念他佛，專念他佛以比較高的標準來看是心外求法。

第二個、專念自佛：就是我們說的真如三昧。阿彌陀佛就是我一念心性本具的，你就修真如無住法門，念念迴光返照，你跟阿彌陀佛就不二了。但是你也不念佛、也不求生淨土，蕅益大師說那這個就不是淨土法門，那是自力法門，專修專念自佛。

第三個、自他兼念：自他兼念就是說你在念佛之前，能夠很認真的學習所有的大乘經論，真實的明白：自知我是未成之佛，諸佛是已成之佛，其體無別。把阿彌陀佛的萬德莊嚴，這些如夢如幻的因緣所生法，把它無住，把它空掉，看到阿彌陀佛內心中現前一念心性；也把凡夫煩惱障、業障、報障這些無始劫以來，依止顛倒所熏習的這些煩惱罪業，這些如夢如幻的因緣所生法，也把它空掉，我們也出現了現前一念心性，結果我們發覺一如無二如。依止這個平等不二、自他不二的思考來念佛，所謂的托彼名號，顯我自性。這樣能念的心不可思議，所念的佛也不可思議，一念不可思議，百千萬句念念之間不可思議，滅惡生善，這樣叫做自他不二的念佛法門。

法藏大師他的判教，把這個專修念佛方便判作止觀之上，那他的念佛法門，應該就是自他不二。你不但修真如三昧，還有一個他力的救護。所以諸位如果知道的話，在密宗的最高的法門是本尊相應，當然它那個本尊相應不是我們說的我是業障凡夫，文殊菩薩是萬德莊嚴，以一種有所得的心去通身靠倒。密宗要求所有的修本尊相應、大圓滿、大手印的人，都要通達十年的顯教基礎，就是要讓你知道：你一念的心性跟十方諸佛是平等不二的。平等不二這是理觀，我們也不廢事修。假藉持大悲咒的因緣，開顯你自性本具的觀世音菩薩的功德。所以達賴喇嘛在作觀世音菩薩灌頂時，他作一個重要的開示：所謂的灌頂，就是讓你明白一個道理。你持大悲咒之前，你要相信你這念打妄想的心，跟觀世音菩薩的心是平等不二的。第二個你相信觀世音菩薩的功德，是你內心本具的。你只是假藉持大悲咒的因緣，來開顯你本性的功德，這樣你就成就了本尊灌頂，你有資格來修這個觀世音菩薩法門。如果你還是心外求法，就不能構成灌頂了。

所以法藏大師他把這個專修念佛判作是下品中的上品，修習止觀是下品中的中品，他這個念佛應該是依止自他不二的實相法門來念佛，就是自他不二這樣的一個平等思考來念佛。

