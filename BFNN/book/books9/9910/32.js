真如三昧的一個問題，我們也說過：這個真如三昧你完全無住，是嚴重的違背我們的人性，嚴重的違背！我們的心一定要動，所以後面馬鳴菩薩慈悲，我們在修真如三昧時，不妨兼修念佛三昧。就是說無住，那當然是最高境界，無相三昧的無住法門，就像禪宗說的相續也太難。你要一二秒鐘十秒鐘的相續不難，但是你要長時間的相續無相三昧很難；那找一個相狀讓你住，就修一相三昧，專修念佛也是不錯，先從一相三昧修起。

