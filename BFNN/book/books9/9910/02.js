所以在整個生滅門中第一個相貌是我相的產生，就是以你當下的果報體，產生一個主宰的我相。

/SECTION/（二）人相

第二個是人相。生滅門的第二個相貌是人相，這個人相就是果報，果報叫做人。什麼叫做果報呢？就是你所受用的這些色聲香味觸法的果報相。有些人前生的善業強，他所受用的這些果報──看到的顏色是美妙的顏色、聽到的音聲都是悅耳的音聲，他所受用的這些色聲香味觸法的果報特別的莊嚴美妙。有些人他過去中罪業強，他所受用的果報相是卑賤丑陋的色聲香味觸法。前面的我相是能受用，這個人相是所受用的果報。

/SECTION/（三）眾生相

第三個是眾生相。這個眾生相就是眾緣和合，就是我們一般說的造業。在生滅門的個體生命中，以我相來受用果報的人相，我們又創造一個新的業力：假設我們遇到了三寶，我們布施、持戒，就創造一個新的善業；有些人在果報中迷惑顛倒，就創造一個新的罪業：這個就是所謂的眾生相，你一方面承受過去業力所招感的果報，一方面又創造一個新的業力，這個新的業力叫眾生相。

/SECTION/（四）壽者相

有了眾生相這個業力以後，就創造了所謂的壽者相，一種所謂相續的存在；由這個業力又去招感來生的果報，使令這個個體生滅門的生命，雖然不斷生滅，但是又相續下去。就像流水一樣，每一個水接一個水，這個流水它不是線狀，是點狀，有無量個點把它串起來；我們生命亦復如是，我們的生命不是一條線，是無量的小點剎那生剎那滅、剎那滅剎那生構成生命的水流，它雖然變化，但是相續，相續又變化，這個就是所謂的生滅門。

生滅門中所創造的我相、人相、眾生相、壽者相，很容易讓我們產生一種自性執。

當我們產生自性執時，我們就在個體生命中，以我相為主體，就跟眾生產生一個對立──我跟你不同，你跟我也不同。在生命中，我要保護自己，有我相就會產生我愛、我癡，愛著自我、保護自我的私心，一個自私的心態就出現了。由這個私心以後，就帶動了所有煩惱的活動，也就創造了無量無邊的業力。所以說我們今天整個心識就失掉了本來面目。

