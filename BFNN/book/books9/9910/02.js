本論把生命的現象分成兩部分：

第一個是生滅門：所謂我們現在所變現出來的一個個體生命，可能是男人，或者是女人，這個就是我們的生滅門。在無量的因緣中，我們暫時出現了一個果報體，這個果報體是會變化的，這叫生滅門，叫個體生命。

第二個是真如門：當我們修我空觀、法空觀時，觀察因緣所生法，我說即是空，就把個體生命的現象消失掉，回歸到我們本性所謂的真如門──整體生命。回到整體生命一段時間，我們又開始從空出假，又回到現前的個體生命。

生命就是兩個觀察：一個個體生命的觀察，一個整體生命的觀察。

在我們佛學院大殿前面，經常有很多的鳥類，有時候同學慈悲就買米持往生咒來施食跟鳥類結緣，希望它們栽培佛法的善根。在施食的過程中，我們也觀察到眾生的個體生命的確是有差別。我們山中的鳥有兩大類：一個是燕子，一個是麻雀。這個燕子它的心胸廣大，它叼了很多的草，好不容易築了一個巢，麻雀它不會築巢，它看到燕子築的巢很不錯，就去搶燕子的巢。燕子說：好！你要就給你。它又到其他的地方去築一個巢，它也無所謂。

吃東西時，我們把米灑出去，如果是燕子先來吃，麻雀後來，燕子會讓出一個地方，讓麻雀來吃。如果今天我們灑出去的米，是麻雀先來吃，它看到燕子來吃，就把燕子趕走。所以我們觀察眾生的我相、人相、眾生相、壽者相是有差別的，這個麻雀的四相特別的狹隘，它那個我胸量狹小；而燕子雖然也有我愛執，但是比麻雀的我相大了一點。我們可以從這裡判斷，燕子在這一期的生命中，它成就快樂的因緣會比麻雀來得好，它會快樂。

就是我們眾生都活在自己的四相──我相、人相、眾生相、壽者相。當然這個四相你跟我不同，我跟你也不同；不要說你我不同，就是同一個人，昨天的四相和今天的四相也不同，生滅門是不斷的變化。你如果聽了大乘佛法，你的四相會不斷的開闊；你修空觀，這四相就會消滅掉。

所以這裡的大，就是說我們都活在自己的妄想中，當然這個大不是你現在打妄想那種狹小胸量的我相，那個不能叫大。所謂的大就是絕待無外，當我們修習我空觀、法空觀，把這種不平等對立相消滅，從生滅的個體生命回歸到平等真如門時，這時叫做大。這裡的大是這個意思——離諸對待，故名為大。接下來解釋乘：

/SECTION/第二小段、釋乘

乘以運載為義，即是直指眾生現前介爾心性，法爾運載至於佛地，自利利他，無休息故，名為乘也。

前面的大是一個法體，這個乘是一個譬喻。大這個法有什麼樣的功德呢？有運載的功德。在苦惱的此岸，我們依止大能夠到達安樂的彼岸，它能夠運載，就像車子能夠從此方運載到彼方，所以它是一個乘。

這個乘是什麼意思呢？就是眾生的介爾心性，能夠把我們從一個苦惱的凡夫，而運載到萬德莊嚴的佛地，成就自利利他的功德，盡未來際的不休息，所以叫做乘。這個乘的意思也就是說：我們以前不知道什麼叫做大，只知道什麼叫做我相，活在自我意識，內心中心隨妄轉。我們打什麼妄想，心就跟著妄想，跟著感覺走了，這就是為什麼我們無量劫的流轉中，生生世世都不能解脫的一個主要因素，就是心隨妄轉。

現在我們的心不再隨逐無明妄想而轉，就隨順於真如而轉，就是本論說的正念真如。我們經常思惟，我這個心是具足體相用三大，何期自性本自清淨，何期自性本自具足，何期自性能生萬法。你不斷的去憶念、不斷去隨順你的清淨本性，這時你的生命就有變化，就能夠從生死苦惱的此岸，到達萬德莊嚴的彼岸，所以它有乘的意思。所以這個乘是讚歎前面的大所表現出的功德，有乘的功德，所以叫做大乘。

前面的大乘，一個是法，一個是喻，我們作各別說明；這以下我們把它合起來說明。

/SECTION/第三小段、合釋大乘

一、體大：只此現前介爾之心，隨緣不變，全體真如，名為體大。

現前一念心性就是我們的本來面目，它隨緣不變，雖然隨順因緣而變現種種的個體生命，但是本質是不變的，它的本質就是清淨本然，所以叫做大，這個大的意思就是清淨，就是我空、法空的意思。

我們修學菩薩道有三大主題，所謂的斷惡、修善、度眾生。一個菩薩的生命，你要做三件事，你的生命其實主要是做三件事：第一個誓斷一切惡，第二個誓修一切善，第三個誓度一切眾生。

站在斷惡的角度，何期自性本自清淨是非常重要的。天台宗的懺悔法門，智者大師說：你造了一個重大的罪業，假設你不能觀察業性本空，饒你百般懺悔，你這個罪不能懺清淨，懺不清淨。就是說你要深深的相信，你所造的罪業是因緣所生法，它沒有真實的染污我們的清淨本性，它只是一個如夢如幻的因緣相，既然假藉因緣生，也能夠假藉我們清淨的懺悔把它消滅。你深信罪業是可以懺除的，這個在修懺的過程中，是一個非常重要的關鍵，會影響到你懺悔的成敗。

所謂的體大，從我空、法空觀中，消滅自我意識，達成滅惡的功能。

二、相大：只此全妄即真體中，本具恆沙稱性功德，在凡不滅，在聖不增，名為相大。

前面的體大偏重在清淨，這個相大是偏重在具足。這個清淨的本性它具足了恆沙（這個恆沙就是無量無邊）的稱性功德，這個稱性功德，不會因為我們的顛倒而減少，也不會因為聖人的戒定慧而增加，所以叫做相大。

相大是偏重在生善。這裡我們說明一下：

本論主要是講大乘起信，對大乘法生起信心，那這個大乘法有體大、相大、用大。這個相大是說我們這一念心的本來面目是善良的、是功德的。這裡就會有一點問題，說我的心本性是善良的，為什麼我會起邪惡的功能？我應該表現出來的都是善良的功能！我的心本性是善良，為什麼會起貪瞋癡的煩惱，這是怎麼回事呢？

這裡在大乘佛法的修學很重要，本論在後面會告訴你說：因為你所起的邪惡功能是虛妄的，本來不存在的，是我們自己捏造出來的，說是達妄本空，知真本有。

就是說我們這一念心本性是善良的，所以你修習戒定慧是正常的，所以修善是正常的，因為你本性的這個功能本來就是讓你去修善；你把善良的本性用來造惡，這叫不正常。這就是為什麼我們造惡以後，會有愧疚的心態，因為你的本性是善良的，結果你把善良的本性拿去造惡，這叫做不正常，佛法叫做顛倒。

所以你在修學大乘佛法的過程中，你要永遠相信你的本性是善良的，你修習戒定慧是正常的，你起貪瞋癡是不正常的，因為你的本性具足恆沙功德。本論並沒有告訴你，我們的本性具足恆沙的煩惱，沒有這樣講，所以煩惱是後來的顛倒所引生的虛妄相，這一點非常重要，後面的本論會詳細的發揮。

三、用大：只此心性體相，不變隨緣，出生十界染淨因果，達此緣生無性，便能翻染成淨，名為用大。

前面的體大、相大都是一個內在潛伏的功能，一個是何期自性本自清淨，一個是何期自性本自具足。我們的心性會隨順迷悟的因緣：假設你覺悟了，隨順你清淨的本性，就出現了清淨的功德；假設你顛倒了，隨順你虛妄的妄想，就出現了種種的過失。當我們能夠把過失的因緣轉成清淨的因緣，這個叫做用大，所謂的翻染成淨，讓我們清淨的本性如實的表達出來，這個就是所謂的用大。

這裡蕅益大師的註解上說：其實我們修習大乘，這個體大、相大、用大都名為大，都名為乘。這當中的修學次第：

修學次第：一、體大→二、相大→三、用大。

蕅益大師說：我們是先修習體大：剛開始時，你要先偏重在斷惡。當然所有罪業的根源，就是我愛執，就是我們凡夫的私心，所以先成就體大。

滅惡以後，你進一步的思惟這念心不但是滅惡，還能夠生善，因為你的心本來具足無量的功德，你相信你的心，能夠成就諸佛的功德莊嚴，成就相大。

第三個用大，你如實的把你具足的本性，透過佛法的法門，表現出布施、持戒、忍辱、精進、禪定等六波羅蜜的作用，這時叫做用大。

所以在整個三大的修學中，是先體大，滅惡；第二生善，相大；第三所謂的用大。這個體大、相大是偏重在理觀，用大是偏重在事修。這個就是我們講到大乘的內涵，跟它的修學次第。好，我們今天先講到這裡，迴向！

