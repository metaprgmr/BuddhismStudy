應觀過去所念諸法，恍惚如夢。先看過去的果報，我們過去得了很多很多的果報，也做了很多的事情，但是現在迴光返照，好像是一場夢。這個夢，法藏大師解釋是無體難追，它沒有自體。你在做夢時，好像真的一樣有它的感受作用；但是你醒過來呢，覓之了不可得。這過去如夢，所以它沒有一個真實的自我。

應觀現在所念諸法，猶如電光。就是在黑暗中，晚上的黑暗有這種閃電。這個光，法藏大師解釋：剎那無住。剎那剎那生滅，它沒辦法停止。現在，是剎那無住；那未來的果報體呢？

應觀未來所念諸法，猶如於雲，歘（迅速）爾而起。就像這個浮雲，它只是暫時的積聚。這個雲，突然間變成一隻兔子，突然間變成一隻馬，它迅速的變化；我們對來生，根本沒辦法去預測它的一個相貌。

所以我們知道：我們的生命沒有一個主宰的我。就是說前面的真如三昧，它是對真如理的一種無分別住，這時我們從無分別中，生起一個善的分別，修不淨觀、修無常觀，這兩個觀基本上就是修出離心。前面等於是修空正見，這裡是從空出假來修出離心。再看下一段。

