不過這裡我們說明一下：藏傳佛教強調聖道三要──出離心、菩提心、空正見，就是說你一定要先觀察自身的苦，產生出離心；再觀察眾生的苦，生大悲心。這個次第不能錯謬，為什麼呢？假設我們不觀自身的苦，就直接觀眾生的苦生大悲，會有什麼過失呢？因為你沒有修自身的四念處，你就不能調伏你的愛見煩惱。就說你沒有創造一個消滅愛見煩惱的因緣，所以你的愛見煩惱就存在，你修大悲心時，你這個大悲心就夾雜著愛見的煩惱，叫做愛見大悲。愛見大悲在起動時，你會積集功德，你也會帶來一些過失，就是這個問題，你這個功德就不清淨！

所以這裡的次第，我們絕對不能夠錯謬。你一定是先觀自身的苦，內心中產生出離的想；然後再觀察一切眾生的苦，說你應該修習善法度化眾生。所以我們在修善度眾生時，大乘佛法很強調是以出世的精神，去做入世的工作。當然這個出世的精神的生起，就是觀四念處而產生出離。所以這裡在修觀時的次第是大家要注意的。

