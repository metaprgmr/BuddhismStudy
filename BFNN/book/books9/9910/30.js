這是忍辱。我們先把這個忍辱作一個定義，龍樹菩薩在《大智度論》上說：什麼叫忍辱呢？於諸境緣，內心不動。你內心跟境界接觸時，內心不動。龍樹菩薩說：這個禪定也是內心不動，忍辱也是內心不動，這兩個有什麼差別呢？龍樹菩薩說：這是粗、細的不同。禪定的不動是比較精細的，這個忍辱的不動是比較粗糙的，但是本質都是不動。好，我們看忍辱的內涵：

/VERSE/云何修行忍門？所謂應忍他人之惱，心不懷報。亦當忍於利、衰、毀、譽、稱、譏、苦、樂等法故。

這個忍辱中有兩個所緣境：第一個是生忍，第二個是法忍。

一、生忍：所謂應忍他人之惱，心不懷報。

忍辱的基本定義就是於諸緣境，內心不動。這第一個境緣就是一個有明瞭性的眾生，這個眾生對我們產生一種非理的惱害，身為一個菩薩對於這種非理的惱害，最低的標準是心不懷報。

菩薩戒的意思：身為一個菩薩，內心安住在菩薩種性，我們依止大乘信心建立的菩提心，當然有菩提心的眾生，也會起瞋心，這很正常。當菩薩遇到境界刺激時起瞋心，沒有結罪；但是你把這個瞋心累積成恨，就結罪了，所以菩薩瞋心的最低的底線是終不結恨。說你發脾氣，這是可以理解的，因為我們這個菩提心也夾雜很多煩惱，你不可以刺激他的，你刺激時起瞋心，這個菩薩就應該趕快修習止觀加以對治。而這個菩薩沒有對治，把這個瞋心轉成一個長時間的恨，乃至對方來懺悔，我們都不能接受，這樣就是結罪了。所以第一個，我們對眾生的惱害，起碼不結恨，要趕快化解自己的瞋心，這是所謂的生忍。

二、法忍：對於這個因緣所生法——世間八法的安忍：

利、衰：這個衰是逆境，我們初學者在修學佛法時，有很多很多的損耗、侵害這種逆境，我們應該安忍不動。慢慢慢慢地，你經過長時間的懺悔業障、積集資糧，這個衰境界消失了，變成了利，對於財富榮耀的境界這種順境，這時你也要安忍不動，你要告訴自己：這些路旁的小花，只是一個小資糧的境界，你追求的是無上菩提的廣大的功德，你應該繼續往前走。所以我們剛開始對於利、衰的順逆境界安忍不動，跟著我們當初的生命規劃繼續的往前走。

毀、譽：譭謗跟稱譽，這個毀、譽跟稱、譏很類似。這個毀、譽是超越之法，就是你沒有這種情況，他講得超過了，不管是好、是壞都是超過了。

稱、譏：是如實之法，你原來就是這樣，那別人也是講這樣，不管是稱讚、不管是譏嫌，這都是如實之法。前面的六個法，都是偏重在內心感受的刺激，不管是苦、不管是喜、不管是憂，總是偏重在心受。

苦、樂：是偏重在色身的感受。就是我們在修行的過程中，遇到了炎熱、寒冷等等的這樣一個環境逼迫所產生的苦受跟樂受，我們都應該安忍不動。

忍辱的功德，從內在的善根，能夠對治瞋恚成就安忍。一個人能夠安忍的心，他的一個特點在《瑜伽師地論》上說：一個人成就安忍以後，內心心無憂悔。你看有些人的心，經常感到憂愁，產生悔恨、懊惱，他修止觀就靜不下心，憂悔，這個悔箭穿心，就是他不能忍。所以我們成就忍辱，能夠心無憂悔。從外在的福德，能夠功德不失，你已經成就的功德，能夠不失掉。

龍樹菩薩說：佛陀講完布施、持戒以後，為什麼講忍辱呢？他講一個譬喻說：譬如一個大富長者，積聚很多的珍寶，他接下來要做一個倉庫，把這個珍寶加以保存下來。我們菩薩亦復如是，我們透過布施、持戒以後，積集很多很多的資糧跟善根，積集了無貪的善根，也積集了這個善業的善根，這時應該用忍辱來加以保護，否則這個善根就容易退失掉了。

佛法的忍辱，它不是強忍，它是用智慧來疏導。蕅益大師說：我們遇到逆境刺激時，有兩個觀想方法能，夠把這樣的一個負面的情緒加以疏導：

一、隨緣消舊業想：當我們內心跟這個逆境接觸時，我們可能會產生瞋恚，這是很自然的，但是你接下來要趕快觀想：這個境界會在我的生命出現，一定是我心中有這個業力，我內心沒有這個業力而招感這個果報，那佛法因果的真理就不能成立。所以我們信仰佛陀，對佛法歸依，我們相信因為我們修行的緣故，重報輕受，既然是我自己造的業，我們應該要安忍不動，這樣你就能從這個逆境中悄悄的走過去。有些人他障礙現前，他走不過去，他就是沒有智慧的觀照；有些人他就是如夢如幻一樣的走過去，不當一回事的走過去。所以我們應該要隨緣消舊業想，當然這是比較消極。

二、當善知識想：那這樣就很重要。就是說我們有些功德是在順境中成就，心平氣和時能夠禮拜、讚歎、修止修觀；但是有的功德是在逆境中成就。譬如：我們對一切眾生的寬恕、包容，而寬恕、包容是修大悲心的基礎。寬恕、包容這種功德，沒辦法在順境中成就，一定是在逆境成就。你因怨家的刺激，才能夠生起寬恕跟包容，這樣這個逆境對你來說是非常重要的，因為你的寬恕跟包容是從這裡建立起來的。

也就是說：在成佛之道，我們所面對的境界，就像我們吃飯菜一樣，酸甜苦辣一個都不能缺少。一個人在修學的過程中，你完全是順境，你這個人不吉祥，營養不良，你老是只吃某一道菜，你可能某一種功德會生起，某一種功德你就沒辦法生起。所以我們應該把逆境，當作你的善知識想，因為他對你的寬恕跟包容有增長的力量。

