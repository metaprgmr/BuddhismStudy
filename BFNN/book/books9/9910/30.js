一般的在家居士，多分就是修習福報，他的內心基本上是向外追求，積聚一點善業，做來世福報的一個根本；但是內心這些雜染的因緣，沒有辦法去調伏。但是我們出家眾不行，我們追求解脫，我們也修習善業，但更重要的是，我們要向內觀照來折伏煩惱。折伏煩惱，這當中講出兩個方法：

