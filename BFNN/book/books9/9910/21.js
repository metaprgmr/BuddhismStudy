這一大科是解釋分，就是解釋生命的真實相。從大乘佛法的角度來說，我們對生命的觀照，主要就是迴光返照，觀照我們一念的心性。觀照心性的方法，本論分成兩個部分：第一個是真如門，第二個是生滅門。這兩門最大的一個差異點：真如門是平等的，離諸對待的；生滅門是對待的，有覺悟的對待、染淨的對待、增減的對待，各種的對待。

