第一段、體大——如實空

/VERSE/復次，真如自體相者。一切凡夫、聲聞緣覺、菩薩諸佛，無有增減。非前際生，非後際滅，畢竟常恆。

我們講到一心真如的自體相，什麼叫做體大呢？我們剛開始從凡夫，或者二乘人，或者菩薩諸佛，在因地時，我們剛開始修學，就是懺悔業障、積集資糧，內心是有增減。比如說：你今天跟昨天的生命有所增減。怎麼說呢？你內心的煩惱罪業減少了，你的福德智慧的功能增長，所以你的心跟昨天的心是有增減。但是我們從體大來說，它是無有增減的。就是我們要看到沒有增減的那個內心的相貌，它從空間上來說是沒有增減的，說是在凡不減、在聖不增，它沒有增減相，這第一個相貌。

第二個從時間上來說，是非前際生，非後際滅，這裡講到它是無始無終。蕅益大師說：這段的意思是無始無終。無始無終就是等於不生，也是不滅，這個是約時間上來說。

以下作一個總結，畢竟常恆。我們剛開始在調伏內心時，是用對立的，以善來破惡，以空來破有，這是非常的正常，剛開始都是對治觀法。但是我們慢慢地要從對治中，去體驗我們一念心性的本性是畢竟常恆，這個常就是從時間上來說，它是不生也不滅的；這個恆是從空間上來說，是不增也不減，當然也包括了不垢也不淨。這個是講到真如體大悟入的方法。這個一心三大中，我們前面也說過這個體大是最重要，你體大沒辦法悟入，相大、用大都不可能成就，所以剛開始你就是要去找到體大的相貌，就是無有增減、沒有生滅、畢竟常恆的一個內心的狀態。這是告訴我們悟入的一個觀照方法。

