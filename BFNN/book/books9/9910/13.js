三、約相續：第三個是約相續。就是你這個業本來是一個很薄弱的業，但是你數數現行都無慚愧，每天每天的造作，這個業也會變得強大。說你每天拜《八十八佛》，雖然拜時也打妄想，打妄想也拜《八十八佛》，你的心力不是很強大。但是你每天拜《八十八佛》，拜了三年、拜了十年就不可思議了。為什麼呢？數數現行。就是你每天都造作這個業，這個業對你會產生一個非常大的力量，你每天都會造作的（就是這個次數），這個次數也會使令這個業變得強大。所以說從一個有漏位來說，也是各式各樣，我們凡夫的心發動的這種性染幻也是各式各樣的差別，這是講到我們凡夫的差異相。

這個差異相我們作一個總結：

從論文上來看，我們講到這個差異相有隨染幻差別跟性染幻差別。從生滅門來說，我們身為一個有情眾生，一定會造作很多事情，我們不像阿羅漢安住在不生不滅涅槃，我們是在有為法中活動。你所做的每一件事情只有兩種可能：第一個是依止願力，第二個是依止煩惱。比如說：你有因緣做個方丈，做方丈和尚的因緣也只有兩個，你不是依止願力，就是依止你的慾望，沒有第三種情況。就是說這個方丈和尚這一期做下來，你是成就功德，還是成就過失呢？跟你因地的動機有非常絕對的關係。

說我做方丈，我依止的是我的誓願力，我要安僧辦道。你的動機是以覺悟發動的，雖然在這個過程中有很多不圓滿的過失，但是你的本質是覺悟，這個功德大方向來說本質就是清淨的，就是個安樂的果報。說你做方丈的目的是增長自己的名聞利養，不管你這個過程中做什麼事，這一期的方丈對你的生命來說，是一個負面的力量，因為你的本質是依止不覺發動的。本質是染污，那所有的果報、所有的業力都是染污。做方丈是這樣的，做法師、做什麼事都是這樣的。這所謂的差別相就是依止願力、依止慾望這兩種差別。這裡是把生滅門的同相、異相作一個總結。

我們把同相的道理再解釋一下。這個同相在大乘佛法來說，是蠻重要的一個觀念，這個同相的道理在修行上的應用有兩個重點：

第一個是講到這個成佛的可能性。就是說我們現在是一個惑業苦的凡夫，你怎麼知道你可以成佛呢？就是依止這個同相的道理，就是把這些生滅的作用會歸到所謂的一心真如，看到了心、佛、眾生三無差別。所以在大乘菩薩戒裡面，很強調自知我是未成之佛，諸佛是已成之佛，其體無別，所以你的菩薩種性的成立，剛開始就是依止這樣的信心，信根本。就是說我們這一念心跟諸佛的心，本質是沒有差別的，因為本質沒有差別，你才有可能成佛。說我的本質是染污的，我用沙去煮飯，煮半天還是個沙。所以這裡同相的道理，讓我們知道成佛的可能性。

第二個是我們在修行淨土中的感應道交，就是解釋凡夫跟諸佛為什麼能夠感應道交，這個觀念也是建立在同相的道理。因為我們這一念心跟諸佛的本性是沒有差別的，所以我們在憶念佛陀時，能夠得到佛陀的加持。所以同相的道理，在修行上的應用也是很重要。

