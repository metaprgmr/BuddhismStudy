前面是修善；這以下是度眾生，以大悲願力來平等救護眾生。我們應該發願，發什麼願？

在時間上是盡於未來。所以我們在受戒時，你要受聲聞戒，你的所觀境是要期誓願。你在受聲聞戒時，你不能說你生生世世要受持聲聞戒，因為聲聞戒是今生今世，你來生你不一定要出家的，你不一定能出家，所以聲聞戒它的要期是今生；但菩薩戒的所觀境是時空無盡的，你可以規劃到未來，就是說你不管來生是出家、在家，你還是要趣向無上菩提，所以你在發願時，是盡於未來。

在空間上是化度一切有情眾生，不能有所簡別。用什麼樣的功德來攝受眾生呢？使無有餘，皆令究竟無餘涅槃。使令眾生的三界果報，都能夠窮盡，而成就大乘的無餘涅槃。比如說離苦得樂，這個眾生他沒有飯你給他一碗飯吃，他沒有衣服穿你給他衣服穿，當然這都是一個暫時的離苦得樂。真實的離苦得樂，你應該要開顯佛法的道理給他了解，使令他生起光明的智慧，趣向無上菩提，這才是究竟。因為隨順法性無斷絕故，法性廣大，遍一切眾生，所以我們對眾生是平等不二，沒有自他的差別，成就究竟的寂滅。這裡是講度眾生，斷惡、修善、度眾生。

度眾生這句話說明一下：在宗大師修大悲心的開導中說到：我們在發願時要平等。比如說你得菩薩戒體，你面對的是十法界有情無情的眾生；你要是說針對某一個怨家，你不能夠對他斷惡、修善、度眾生，那你這個菩薩戒就不能夠成就，這個因種要圓滿。所以在理觀上，他是平等不二；但是在實踐上，有他的次第。他在次第上，一定先度化有緣的眾生，然後再對中庸的眾生，然後再對怨家的眾生，他是有次第的修。

佛在世時，外道就譏嫌佛陀說：佛陀啊！你的心是平等，但是你花很多時間為佛弟子說法，你花很少的時間為外道說法？這外道就譏嫌佛陀不平等。佛陀說：假設你有三畝田。一塊是最肥沃的田，你只要少分的耕種，就產生很多的水果；第二塊是中等的田；第三塊是貧瘠的田，你怎麼種就長不出水果。這樣的話，你應該要先耕種那一畝田？外道說：我這個精神體力有限，我應該先耕種肥沃的田。佛陀說：佛陀在度化眾生，亦復如是。當然是對於善根已經成熟的眾生，先加以攝受。所以佛陀花大部分時間對出家眾說法，花少部分的時間為居士說法，花極少數的時間為外道說法，那是因為眾生的善根不同而安立的。所以我們在發願時是不能有所簡別，但行動上有它的因緣差別，這個是在度眾生的方便。

這裡是講到修習菩提心的方便，在根本上安住菩薩種性，你要先把你的種性建立起來，依止這個種性修行三種的加行──斷惡、修善、度眾生的加行，使令我們從種性中，慢慢的開花結果，成就種種的功德莊嚴。這就是成就菩提心的四種方便，一個是根本法門，後面三種是枝末法門。我們今天就講到這裡。

