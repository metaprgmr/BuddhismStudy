請大家打開《講義》第五十五頁，癸二、釋發心。

在大乘的修學中，我們成就的第一個功德是信成就發心。信成就發心前面一科，先講到信心的成就，就是我們依止內外善根的熏習，成就所謂的菩薩種性，對於大乘菩薩的功德，對於一心具足體相用三大的功德，真實的信心而不退轉。這以下說：我們依止這樣的大乘種性發起菩提心，它的相貌是什麼？

/KEPAN/癸二、釋發心
/KEPAN/子一、釋所發之心
/KEPAN/丑一、正釋

這當中有三科，子一、釋所發之心，先說明菩提心的相貌是什麼，它果地的相貌；下一科再說明它因地修行的方法。先講果地的功德相，再說明因地的修學相。這當中有兩科：先正釋，再釋疑。先正式的說明：

/VERSE/復次，信成就發心者，發何等心？略說有三種。云何為三？一者直心，正念真如法故；二者深心，樂集一切諸善行故；三者大悲心，欲拔一切眾生苦故。

一個初住的菩薩，他已經依止了堅固的菩薩種性，一個有菩薩種性的人，他一定會發起菩提心，因為由信能夠引導我們的願力。一個有菩薩種性的人是什麼相貌呢？他的心跟凡夫心有什麼差別呢？這當中講出了三種差別：

一、菩提心的三種相貌

第一個、直心：這個直心就是正念真如法故。這個直心，在法藏大師的解釋中，是判作根本行，是整個菩薩道的根本。就是說你這個東西沒有根本，下面的修善、度眾生就變成有漏的業行。

一個初住菩薩的內心，他是正念真如法故。我們這一念的心性有兩個相貌，有真如跟無明兩種相貌，都是無始的在心中同時活動。無明主要的、最粗顯的相貌，就是自我意識。我們一般凡夫的心跟境界接觸時，都是心隨妄轉，跟著自己的感覺、跟著自己的習氣來活動，就是我完全靠直覺來活動，我現在想要做什麼就做什麼。

一個住位的菩薩，他的內心中，他的心能夠正念真如，這個念就是他有隨順真如的力量。他的心跟境界接觸時，當然也可能會起妄想，也可能不起妄想，但是他起妄想時，他有對治力，能夠把妄想的因緣撥開來，回歸到他內心的真如理體，一種清淨本然的理體，他能夠把心帶回家。所以這裡就是說他不再隨順妄想而活動，他念念之間，開始隨順真如法，以真如法來對治無始的妄想，這叫直心，這是整個發菩提心的根本。這裡是約著斷惡來說，他所修學的重點是修習內心的體大，就是真如平等無增減故。

第二個、深心：樂集一切諸善行故。前面的心，它的所觀境是約著惡法來加以對治；這裡的心，它所修的所觀境是善法。什麼叫善法？就是能夠生起此生、他生安樂果報的，都叫做善法。在佛法中的善法，有六波羅蜜──布施、忍持、戒辱，乃至於禪定、智慧。住位的菩薩他對於一切的善法好樂的修學，這個是成就了自利行，來成就內心的莊嚴，所謂的如是一心中，方便勤莊嚴，修習六度，以福德、智慧二種的資糧來莊嚴他的一心。前面是講直心，但直心只是斷惡，缺乏莊嚴；所以我們應該積極善法，以福德、智慧兩種的資糧來莊嚴我們的心。

第三個、大悲心：欲拔一切眾生苦故。我們不是只是求自己的受用跟莊嚴，我們還有一切如母的有情在三界中過著痛苦的生活，所以我們應該要以種種的善巧方便來度化眾生，這個是利他行，成就一心的用大。

所以真實信心成就的菩薩，他內心中是具足直心、深心跟大悲心，這樣我們就安立做發起的菩提心。他明瞭的心跟境界接觸時，具足直心、深心、大悲心，我們就安立說這個人的菩提心真實生起了。這是一個菩提心的三種相貌。

/KEPAN/丑二、釋疑

/VERSE/問曰：上說法界一相，佛體無二。何故不唯念真如，復假求學諸善之行？

我們前面在說理論時，講到真如門是法界一相，十法界從真如的體性是無差別的，所謂的心、佛、眾生三無差別。既然無差別，為什麼我們在修學菩提心時，我們不直接的唯念真如就好？我們可以經常在佛堂中，觀察摩訶般若波羅蜜，依止我空、法空的智慧，來消滅我們心中的妄想，安住在我們清淨平等的真如就好了，為什麼要去修善？為什麼要去度眾生呢？就是因為法界是一相，就提這個出問難，看回答。

/VERSE/答曰：譬如大摩尼寶，體性明淨，而有礦穢之垢。若人雖念寶性，不以方便種種磨治，終無得淨。

講一個譬喻：我們這個摩尼寶珠能夠所求如願，就表示我們的菩提心，雖然它的體性是光明清淨，但是它無始劫來有污垢。如果我們在一直憶念摩尼寶珠的體性是清淨的，但是我們沒有假藉種種抹布、清潔劑的方便來加以磨治，它還是不能恢復清淨的本性。也就是說這個菩提心的修學，應該是理事無礙。在理上，我們應該要能正念真如，生起觀照；但在事上，我們也應該要修善，乃至於度化眾生的行動：這樣才能夠達到目足雙運的理事無礙。這是譬喻，這以下合法。

/VERSE/如是，眾生真如之法，體性空淨，而有無量煩惱染垢。若人雖念真如，不以方便種種熏修，亦無得淨。

就是說眾生的真如之法，雖然本性是清淨，但是從作用上來說，我們在三界中，無量次的受生打滾，熏染了很多貪瞋癡煩惱的習氣，假設我們只是憶念真如，二空真如，而不假藉種種的六度方便加以熏修，我們是不能恢復它的清淨光明。為什麼？

/VERSE/以垢無量，遍一切法故；修一切善行，以為對治。若人修行一切善法，自然歸順真如法故。

因為我們眾生的煩惱污垢是無量，他煩惱污垢的生起因緣，是遍一切法故。我們要還滅一個東西，你要先找到他生起的因緣。我們的煩惱剛開始生起因緣，是普遍在一切的有情無情的境界中，去造惡而熏習的煩惱。所以我們從對治義的還滅角度來說，我們也必須在有情無情的境界中，去修一切善來加以對治。所以作一個總結：若人修習一切的善法，才能夠歸順於真如法故，不能夠執理廢事的。

這裡，我們可以回顧一下前面一心二門三大的理論：

我們前面講到現前一念心性時，講到我們內心中有真如跟無明。這當中真如是無始的，無明也是無始的，這表示我們內心的本性是真妄和合。所以你修行不能只是生善，不斷的生起你對真如的歸依就可以了，你還得破惡，生善跟破惡是缺一不可的。

你對真如的觀照很強，但是你無量無邊的煩惱習氣沒有對治，真如還是不能開顯。所以這裡就是說，我們內心中應該正念真如，對真如生起歸依的心；但是我們要假藉布施來對治慳貪、假藉持戒對治我們的罪業、假藉忍辱來對治我們的瞋恚，乃至於假藉禪定、智慧來對治惡法生起善法。就是事相的修學都是不能免的，這樣才能夠真實的歸依，歸依到一心真如的功德。這裡是說明我們在發心時，必須要發三種心的理由。因為你正念真如只是理觀，深心、大悲心的事修都不可以缺乏。

/KEPAN/子二、釋發心之行

前面是講到菩提心的相貌，這以下是說明成就菩提心的方便，它的方法是什麼？

/VERSE/略說方便有四種。云何為四？

我們成就菩提心的方便有四種，先看第一種。

/VERSE/一者行根本方便。謂觀一切法，自性無生，離於妄見，不住生死。觀一切法，因緣和合，業果不失。起於大悲，修諸福德，攝化眾生，不住涅槃。以隨順法性無住故。

這裡的根本，其實就是前面說的大乘信心，就是安住菩薩種性，它是根本，它等於是把信心再加以加強。當然信心主要是靠智慧的觀照引生，不能假藉外在的因緣，我們前面說過的。我們在成就菩提心時，第一個要加強我們的根本功德，所謂的安住菩薩種性，這當中有兩種的觀察：

一、觀察真如的如實空義：就是說我們經常觀察一切的有為法是自性無生，而離開凡夫的妄見，使令我們能夠不住生死，對治我們對生死果報的執取。

自性無生，就是我們觀察的重點，簡單的說就是自性空。大乘佛法修空觀，它的一個切入點，不是從無常，大乘佛法是觀因緣所生法，是從因緣上觀進去的。因緣所生法，在天台的止觀中，智者大師乃至於蕅益大師的觀法，就是從過去、現在、未來的觀察。怎麼樣觀察因緣所生法呢？比如說：我們觀察現在這個講堂，這個講堂是一個因緣所生法，它有過去、現在、未來。就是說這個講堂它過去是不存在的，因緣沒有成立時是不存在的；這時假藉因緣，它暫時剎那剎那現前；等到有一天因緣結束時，這個講堂又消失掉了。所以我們透過過去、現在、未來觀察，我們知道它是自性空。這是觀察依報的莊嚴。

我們也觀察我們的正報：你的生命有一個外在的色身，有一個內在明瞭分別的心識，有受、想、行、識，你有各式各樣的感受、各式各樣的想法等等。你這個身心世界怎麼去觀因緣所生法？就是未生無潛處，我們還沒有從父母親生出來之前，你這個身心世界是不存在的，它不是本來就存在。正生無住處，現在就算存在，也剎那剎那生滅，它也沒有一個安住的處所；等到我們死掉以後，我們來生又換一個色身、又換一個心識。你來生可能是一個男人的心識，或者一個天人的心識，那你那個心識跟現在這個心識，完全不一樣，你外表的色身也完全不一樣，所以生已無去處。你等到幾百年後，我們再觀察你這個身心世界，也覓之了不可得。

所以你從現在、過去、未來觀察因緣所生法，每一個法都可以了解到自性無生。這樣我們對於三界的生死果報，這種一時的得失，就會慢慢慢慢就會輕薄。就是這個生命它只是一個暫時的夢幻泡影，我們的重點在於過程，我們從這一期的生命中，如何的積功累德如此而已。我們從夢幻泡影中，假藉夢幻泡影的因緣，來滅惡生善，這是非常重要的菩薩空觀。你一定要修空觀，你的心才能夠從現前的因緣中，跳脫出來。這個是觀察它的如實空義，觀察我們真如的如實空義。

二、觀察真如的如實不空：我們不能老是在空義裡面安住，我們應該從空出假，觀一切法，只要是因緣的和合，這業果就會出現。你今天有五戒、十善的因緣，就出現人天的果報；你有四諦、十二因緣，出現二乘的偏空涅槃；你依止菩提心、六波羅蜜的因緣，成就菩薩的萬德莊嚴。你用假觀時，你就生起大悲心，把你這個成就聖道的心，普及在一切如母有情的眾生，為了有情的緣故，廣泛的修習福德種種的資糧，來攝受眾生而不住涅槃。前面是以空觀來成就心中的智慧，這裡是修假觀來成就我們的大悲，以悲智雙運的因緣，來隨順真如法性的平等，而使令我們無住。這裡的無住，包括了不住凡夫的生死，也包括了不住二乘的涅槃，而順入於中道的思想。

這裡的菩薩種性，我們說明一下：

菩薩種性的修學，是悲智雙運，上求下化，名菩提心。我們理觀時，應該要空有雙照，保持悲智的平等。事實上從一個實踐的角度，菩薩是有所差別的。有一類的菩薩是悲增上菩薩，他在觀照時會偏重在假觀，悲眾生苦，他會偏重修習福德資糧，先成就福德的莊嚴，再成就聖道的莊嚴。另外一種是智增上菩薩，他在修觀時，會偏重在觀察諸法的自性無生，對福德的修習，會比較不主動。他生命的因緣，會先成就聖道，然後再成就福報。所以菩薩在實踐上的確有所偏重，有悲增上、智增上的差別，有悲增上、智增上的差別，但是你不能夠完全缺乏一個。

就是說這兩個可能會有所偏重，但這兩個都應該有，這樣才能夠成就根本方便，所謂依止正念真如法故，來安住菩薩種性，這是我們的根本。就像你種蘋果，你一定用蘋果的種子、然後灌溉，你才能夠產生蘋果；你用葡萄的種子，一定長成葡萄。所以你要成就諸佛的功德莊嚴，你剛開始的種性，這個種性就是你的宗見，你對生命的知見是什麼，就影響到你的種性。你的種性有偏差，後面的行動力那這個方向就會偏差。所以剛開始先強調根本方便，先端正你的菩薩種性；二三四等於是依止這個種性再加以加行。

/VERSE/二者、能止方便。謂慚愧悔過，能止一切惡法，不令增長，以隨順法性離諸過故。

當我們對生命能夠空有雙照而順入中道以後，我們應該產生什麼行動呢？產生改過的行動。當然要改過，一定要有慚愧心，要增上慚愧。這個慚愧心的生起，就是你要能夠如實的了知惡法的過失，你要能夠怖畏當來的果報，就是你要持戒。你要把戒持得好，那這個戒法是由心來推動的，你對惡法的過失不生怖畏，那你持戒沒有什麼力量，因為這個戒法是由心來操作的。這個刀子它能夠把樹砍掉，但是你力量太少了，這棵樹就砍不斷。

所以我們在斷惡時，你一定要能夠對惡法生起怖畏心，這時以怖畏心來修習攝律儀戒，就能止住我們身口意的惡法，使令我們的惡法慢慢的減少不再增長，這樣是隨順真如法的離諸過故。

總之，我們在安住菩薩種性以後，第一個行動力，先求寡過，這個很重要。雖然菩薩的行動力有三聚淨戒，但是以斷惡作基礎。因為你這個過失不先調伏，你就直接去度化眾生，你所修度化眾生的善業都帶有雜染，所以剛開始先求寡過，這個就是我們第一個行動，修這個止，止惡的方便。

/VERSE/三者、發起善根增長方便。

前面在修習菩提心的第一個是止惡；這個是修善，修習種種的善根。

/VERSE/謂勤供養禮拜三寶，讚歎隨喜，勸請諸佛。以愛敬三寶，淳厚心故，信得增長，乃能志求無上之道。又因佛法僧力所護故，能消業障，善根不退。以隨順法性離癡障故。

我們在斷惡的基礎之下，應該要主動的修善，成就福德、智慧二種莊嚴。大乘佛法應該怎麼修善呢？這當中有兩個重點：

第一個是勤供養禮拜三寶，讚歎隨喜，這個是成就大乘的信心。

我們剛開始修善，它的所緣境是佛法僧三寶，這應該怎麼修？包括身業的禮拜、口業的稱名讚歎、內心的歸依隨喜，就是這樣修。以這個身口意的歸依，來修學我們大乘的善根。為什麼我們在修學歸依時，要面對三寶的境界呢？古德解釋說：三寶有二義：一者救護義。菩薩道不只是一生一世的修學，我們必須要生生世世能夠遇到大乘的三寶，我們能夠遇到三寶的因緣，就是我們必須要對三寶經常的憶念。《大勢至菩薩念佛圓通章》說：佛菩薩的心情，就像母親憶念兒子，他經常憶念我們，他經常對我們釋放他的善意。但是我們要能夠跟諸佛菩薩感應，這不是單方面的，我們自己也要釋放出對三寶的憶念。這樣雙方都有互相憶念，我們在生命中就能夠產生一種交集。這就是為什麼有些人容易生長在三寶的地方，有些人很難生長在有三寶的地方，就是你根本不修歸依。你不修歸依，你就算福報很大，你也沒辦法遇到三寶的住持。就是說三寶的心是平等的，就像月亮普照一切有情眾生，但是你要把水準備好，千江有水才能夠千江月。所以我們第一個修學善根，是對三寶的歸依，這是修信心。當然這是一個基礎，祈求三寶的救護。

