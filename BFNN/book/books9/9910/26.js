【學員提問】法師慈悲！法師曾提到修行的次第，是從業力的修學而到心念的修學；也曾提到大小乘空觀的不同，小乘是把心性完全的否定而滅色取空，大乘有中道的智慧來保留心性本具的妙用。在實際的修行上，當我們在修習空觀時，是先修小乘的空觀，再修大乘的空觀？還是兩者同時的並行？

【師父答疑】我們修大乘佛法，可以從兩個角度來修學。第一個就是根本法門，就是安住菩薩種性，所謂的正念真如。就是你的宗見，你能夠觀察一切法自性無生而不住生死；也能夠觀察一切法因緣生不住涅槃，來積功累德，空有雙照，安住中道實相。當然這個是不能有差錯的。當你在建立菩薩種性時，還要有一些加行對治，就是斷惡、修善、度眾生。這裡的空觀，主要是約斷惡來說。

斷惡時，當然眾生惡法的熏習各式各樣。有些人根利遮輕，他大乘善根強，你跟他他講一心真如平等的法門，他就能夠接受；而且他煩惱輕薄，他的貪煩惱也輕薄、瞋煩惱也輕薄、高慢心也輕薄，所以他就不用太重的藥，他只要觀因緣所生法，我說即是空，就可以把他煩惱的相續活動給消滅掉。但是有些人根利遮重，他善根強，但是他煩惱也粗重，他面對煩惱時，觀因緣所生法，我說即是空，他這個藥就太輕了。所以聲聞法的好處，諸位讀聲聞法你會發覺：它的藥下得很重，它是對立法門。所以無常觀，有時候是治亂世用重典。

所以有時也一再強調修死亡，就是死亡也是無常觀，就是三界的果報。你說我們對三界果報的執取，觀自性無生，那也是可以；但是如果你觀察我們總有一天要死亡，死亡時萬般帶不去，只有業隨生，這個無常觀就有一點滅色取空。它這個滅色，這裡在觀時，就要注意：小乘的學者在觀察無常時，他是把整個三界的果報加以厭惡，所以一切有為法他一觀下去，一切有為法經過無常觀一抉擇下去，沒有一個東西有價值的。

所以你看《法華經》《維摩結經》聲聞弟子就很坦白跟佛陀說：我觀察這一切的法，不管是生死的法、不管是諸佛菩薩的功德莊嚴，都是不生不滅，無大無小，無常敗壞，不生好樂，他對有為法沒有感覺。菩薩在觀無常時，他主要是依止一心而修，他就是對治內心的障礙，這一點要注意，就是迴光返照。就是說我們也會用聲聞法的這個重藥來對治，但是他所對治的，是依止一心來對治心中的愛取，我們對三界的果報不會產生那麼怖畏，以後你往生淨土，要回入娑婆度有情，你就不會那麼困難。

