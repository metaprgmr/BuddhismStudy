當這個無常、苦的宗旨安立以後，馬鳴菩薩就善巧的說：你說生命是無常、是苦，難道國王、王后他們的生命也是無常、是苦嗎？他們是非常的快樂。這時，佛教的論師因為礙於國王個性的暴惡，在氣勢上有所衰竭，就輸掉了。

所以脅尊者就以其人之道反制其人，第二天就隨順國王的心情，安立了一個宗旨，他說：我今天安立一個宗旨，請你來破斥，我們的宗旨就是當令天下太平，國王長壽，國土豐樂，無諸災患，請你破斥。

