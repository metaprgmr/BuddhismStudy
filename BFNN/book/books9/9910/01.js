脅尊者是一個大阿羅漢，具足三明、六通、八解脫，在辯論的前一天晚上，他在寮房入於禪定三昧中，觀察這個成敗的關鍵。他發覺在諸多的佛教論師跟馬鳴菩薩的辯論中，成敗的關鍵，主要是國王的因素。就是大部分比較重要的辯論，國王都會在場，而這個國王性情非常的暴惡，個性非常的固執。

