外道的修學，內心沒有一種平等的法界觀，這樣只是一個人天福報。就是說他這樣的一個善業力，不能夠產生返妄歸真、轉凡成聖的力量，所以內心的觀照力非常重要。就是同樣是一個行持，你內心中缺乏那個眼睛的引導，你這樣的善業就不能夠成就所謂的乘，波羅蜜不能夠到彼岸，這只是一個善業力。

