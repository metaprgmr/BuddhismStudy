別釋中先講人我見：

/VERSE/人我見者，依諸凡夫，說有五種。云何為五？

依止這個生死凡夫，說有五種。先看第一個：

/VERSE/一者、聞修多羅說：如來法身，畢竟寂寞，猶如虛空。以不知為破著故，即謂虛空是如來性。云何對治？明虛空相是其妄法，體無不實。以對色故有，是可見相，令心生滅。以一切色法，本來是心，實無外色。若無色者，則無虛空之相。所謂一切境界，唯心妄起故有。若心離於妄動，則一切境界滅。唯一真心，無所不遍。此謂如來廣大性智究竟之義，非如虛空相故。

這裡是分成兩大段，先提出我們執取的相貌，再說明對治的方法。先看

第一大段、執取相貌

一、大乘的經典上說：如來法身，畢竟寂寞，猶如虛空。佛陀說：佛陀所依止的身（這個身就是依止處），就是佛陀清淨明瞭真如平等的心，他也要有一個依止處。佛陀的心依止什麼？依止清淨的法身，不是我們凡夫這種老病死的業報身，佛陀真如平等的心所依止的法身他的一個相貌，是畢竟寂寞的，沒有這種生滅變化的相貌、沒有這種老病死，他是寂靜而不生滅，這樣的一個情況，猶如虛空。一般我們在了解譬喻，要取其義，不能取其相。

虛空有二義：第一個是週遍義，它是週遍的，每一個角落都有虛空；第二個是無差別義，它沒有差別相的。你說房子、桌子、椅子，有各式各樣的差別相，但是虛空沒有差別相。佛陀就用虛空來比喻佛陀清淨的真如心所依止的法身，是無差別義、是週遍義，所以名之為法身。我們凡夫不知道佛陀講虛空是為了破我們這種有所得的執著，就把虛空當做佛陀法身的體性，以虛空的相狀來當做體性。當然這樣就是一個障礙。

第二大段、對治方法

云何對治？我們看對治，

第一段、總說

明虛空相是其妄法，體無不實。以對色故有，是可見相，令心生滅。

虛空其實是一個虛妄的生滅法，它的體性是虛妄性的，也就是說它是一個依他起性，是一個因緣所生法。怎麼說呢？以對色故有，是可見相，令心生滅，它是相對於一切的色法而存在的。我們說這是一個色法，色法以外都叫虛空，所以它是依止色法的相對而安立的，它是眼睛可見之色，可以用眼識加以了別的，所以它能夠使令我們心產生生滅的作用。這是總說，它本身也是一個因緣所生法。

第二段、別明

以一切色法，本來是心，實無外色。若無色者，則無虛空之相。

其實一切的色法，這裡的色法包括有相的色法、無相的虛空，所以虛空也是色法，都是一心真如所顯現，實無外色。這裡就是破除虛空的虛妄性。什麼叫做本來是心，實無外色呢？假設虛空是真實的，當然真實的意思就是它非因緣而改變。比如說：我們人去看虛空，看到的是一個無形相的東西；但是科學家說狗看虛空是灰色的；餓鬼道看虛空也是有不同的形像。

假設虛空是有真實的體性，那每一個眾生看到的虛空應該是無差別，所以說虛空它本來是心，它是心中的業力所顯現出來的，沒有一個真實的色法。若無色者，則無虛空之相，所以離開了心外的法，就沒有虛空可得。

第三段：總結

所謂一切境界，唯心妄起故有。若心離於妄動，則一切境界滅。唯一真心，無所不遍。

所以一切的境界都是我們這一念心的妄動，這個動就是業力的造作，才產生的果報。你這個業力消失了，果報就消失了，唯一真心，無所不遍。

此謂如來廣大性智究竟之義，非如虛空相故。

所以諸佛的清淨心是清淨廣大的，它不像虛空是一個有為的生滅法。這個是以虛空為法身，我看佛弟子很少有這種思想，一般都是外道。就是說外道他也沒辦法聽聞佛陀所開顯的這些大乘真理，他就依止自己的胡思亂想，在胡思亂想中得到一個結論：我們這念心是擾動相、是苦惱相。所以他怎麼辦？

他就想辦法把內心中的擾動相停止下來，去修這個禪定。當然禪定一定要有一個所緣境，依止一個特定的所緣境，心於所緣專一安住，心於所緣相繼安住，他整個心就透過專注集中，慢慢地慢慢地就從一個躁動的粗重相，到了一個寂靜輕安的相貌，就從欲界的心態提升到色界，這時的色法是清淨微細的色法，所以他有殊勝的輕安樂。

但是這時，當然因緣所生法都是有所得，就是這個有所得的果報體，這裡會有個問題，就是長者生厭。你看佛陀長時間安住在真如，他也不會有厭煩的心。凡夫的心、這個果報體、這個因緣所生法，它是對立的，有好就會有壞，所以他在這個寂靜色界的禪定中，他覺得這個色法是一個障礙，如鳥在籠，不得自在，所以他一直想要去超越色法的障礙。佛法認為色法不是障礙，障礙我們的是無明。

他怎麼破滅色法？他就去攀緣無邊的虛空，以虛空為他生命的歸依處，他不斷地去觀取虛空以後（你這念心怎麼造作，就會出現什麼果報），慢慢慢慢地，粗重的業果色法就消失掉了，只有寂靜、只有精緻的定果色，這時就從色界而趨入無色界。因為他所依止的虛空，還是一個有為的生滅法，所以他禪定消失了以後，就繼續受三界的輪迴了。所以他剛開始時，以虛空為法身，這裡是一個錯誤的知見，因為虛空也是心中的一個影像，是這個意思的。所以我們在後面，他有很多的執取。就是說，「一切賢聖，皆以無為法而有差別」。

