換句話說，若能觀察，知心無念，即得隨順入真如門故。所以我們在修學有相、無相行中，你不要改變你的內涵，再加一個法門知心無念，即得隨順入真如門故。你觀察我們內心中，不管對安樂果報的執取、對空相的執取，這樣執取的心是覓之了不可得，這樣就能夠隨順於悟入真如門。

這裡的隨順入，法藏大師蕅益大師的註解都有說明。法藏大師把這一段分成隨順跟悟入兩個次第，法藏大師說：我們剛開始修學，依止有所得的心，不管是凡夫、不管是二乘，凡夫執著有相、二乘執著無相的空性，雖然他沒有進入到真如門，他也可以說是隨順真如門。隨順，離真如門的距離比較近。但是真實的悟入，那你就必須修知心無念，入平等法界。

這個入平等法界，法藏大師倒是沒有解釋它的方法；不過蕅益大師就有詳細的說明。他說：我們今天要從一個生滅門進入到真如門，最重要的關鍵點──你要能夠深觀動心，即不生滅。就是觀察你這念生滅擾動的心，它的本性就是不生滅，就是自他不二，妄想的本性它的本質就是真如。

我們諸位回憶一下：我們在受菩薩戒時，說是得上品戒體。當然上品戒體不是你向外追求能夠得到的，是你能發願的那一念心，你的心情，在受菩薩戒時，能夠自知我是未成之佛，諸佛是已成之佛，其體無別。雖然你這念心打妄想，但是你能夠很真實的肯定：你這個打妄想的心，它的本質就是一心真如；依止一心真如的心，發起斷惡、修善、度眾生的誓願，而得上品戒體。因為你這個平等法界沒有簡別，在因地就種下了一個圓滿的因種。

這個觀念在《法華經》也非常強調，《法華經》在《信解品》講出一個譬喻說：有一個大富長者，他有很多的珍寶。這個大富長者只有一個獨生子，這獨生子在小時候突然間發狂，離家出走再也找不到了，在外面流浪做了乞丐，展轉經過五十年。這個小兒子慢慢長大了，他已經忘掉了他的家在哪裡，就在外面做乞丐，有一頓沒有一頓的。這個乞丐有一天回到大富長者的家，他已經不認識這個家了，他看到這是一個非常富有的家庭，也許能夠施捨我一點飲食。他在門外偷看時，剛好被他父親看到了，他父親就說：這是我流失的兒子。就叫侍衛去追，這小兒子他流浪太久了，害怕就暈倒了。暈倒以後，這個大富長者就想：現在不是宣布他是我兒子時，因為他流浪太久了，個性卑劣，要廣設方便。

