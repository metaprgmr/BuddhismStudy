請大家打開《講義》第四十八頁，庚三、結示證入。

馬鳴菩薩在因緣分講到造作本論的宗旨：使令一切眾生離一切苦，得究竟樂。換句話說，本論語言文字的安立，它不只是一種知識學術的研究，而是能夠真實的改造我們的生命，使令我們的生命從痛苦的因緣，而成就安樂的因緣。

離苦得樂這樣的一個宗旨，在本論中的修學重點有兩個：第一個是生滅門的修學。生滅的意思簡單的說就是一種對立，這種對立的修學有兩個內涵：第一個是有相行，第二個是無相行。

一、有相行

我們剛開始所修學的是一種有相行，有相行就是說依止一念的信心，歸依三寶、斷惡修善。有相行的修行所緣境有兩個：

第一個、三寶的所緣境：就是我們剛開始面對清淨莊嚴的佛法僧三寶，禮拜、讚歎、咒願，透過身業的禮拜、口業的稱名讚歎、意業的觀想歸依，滅除我們重大的罪障；

第二個、所緣境是眾生法界：我們有時候也離開佛堂，面對雜染的眾生法界來修學布施、持戒、忍辱，透過持戒修福、安忍不動來積集廣大的福德資糧。

因為我們修學剛開始的重點，不管是三寶的所緣境、或者是眾生的所緣境，都是一個有相的境界，所以叫做有相行。透過有相行，能夠滅除罪障、積集資糧，使令我們的生命能夠遠離三惡道，而趨向於人天安樂的果報。

但是這個安樂的果報，我們內心在受用時，會感到躁動不安，就是內心中有這種愛取的煩惱。所以這樣的果報就在安樂的過程中產生了障礙。因此在有相行的階段，我們希望能夠從有相行而趨向於無相行，尋求更殊勝的安樂。

二、無相行

無相行它所觀的境界是一個無相的真理，跟前面的三寶、眾生所緣境不同，它是一個無相的真理。就是說我們從前面的斷惡修善，開始去修無常觀、無我觀，觀察這些種種安樂的果報，它的相貌是生滅變化。這個生滅變化的意思，就是無常變化給我們一種不安穩性；這不安穩性當然就是苦，無常所以是苦，我們沒有安全感，我們不知道所擁有的這些安樂果報，什麼時候會失壞。所以說無常故苦，從苦中我們了解到這是虛偽無主的，我們對於果報沒有主宰性的，無我故空。所以這時，從有相的果報，就進入到一種無相的空性，假藉無常、無我的引導，從一種有相的境界中，進入到無相的真理去了。因為所緣的境界是一個空性的真理，所以安立做無相行。

不管有相行、無相行，內心中都是有所得相應，一個是對有相的執取，一個是對空性的執取；既然有所執取，當然它的方向都是向外攀緣，有這種所謂的生滅相、垢淨相、增減相，所以在本論中全部判作生滅門，當然這兩個都是成佛之道一個非常重要的過程。

