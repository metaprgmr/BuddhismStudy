這個十惡業的業性呢，當然業的力量主要是由心來帶動的，說心為業主。當然業的觀點，有很多不同的觀點。但是這裡我們是在修習大乘的善根，我們就從大乘菩提心的觀點，來判定這個業性。《瑜伽師地論》上所說，是就著引生菩提心的角度來說。

