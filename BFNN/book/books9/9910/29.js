一切眾生他來跟我們乞求財物，或者是飲食、或者是生活的種種資具，這樣的飲食跟財物資具，我們本身有多餘的情況，就是我們有布施的能力，這時菩薩應該要隨順我們的能力，來布施給對方。這樣的布施有什麼好處呢？有兩個功德：一、從成就內在的善根來說，自捨慳貪，能夠對治我們的慳貪；二、從外在的福德來說，能夠令彼歡喜，能夠成就廣結善緣，成就我們來世的福報受用。

其次，我們在修習六度的著眼點，當然是成就內在的善根──自捨慳貪。這個慳貪，《瑜伽師地論》上說：一個人修布施時，你要能夠成就內在無私的善根，你必須依止清淨心來布施。什麼叫依止清淨心來布施呢？《瑜伽師地論》上說：你要具足三個法門：

一、施前歡喜：就是這個布施的因緣還沒有成就之前，你想到你即將要布施，內心充滿了期待歡喜──你即將要因為布施，而成就一個善根；你的生命會因為這個法的操作而更加的離苦得樂，這時你充滿了期待歡喜。

二、施時清淨：當你正式布施時，你內心不求回報，並沒有因為這樣的布施，而祈求對方的回報；你只是希望透過布施的操作，來栽培你內心無貪的善根，依止無貪的善根來加強你的菩提心，所以是不求回報的。

三、施後無悔：布施的法門也剎那剎那的生滅變化過去，當這個布施的法結束時，你回憶你過去布施的因緣，你內心不生悔恨。當然這裡你要量力而為，你要念念的對布施的法門，產生隨順歡喜而不生悔恨。

而當你能夠施前歡喜、施時清淨、施後無悔時，你所作的每一個布施，不管布施的東西是什麼，都能夠自捨慳貪，成就內在的善根。這是講到財施。

第二個、無畏施

若見厄難、恐怖危逼，隨己堪任，施與無畏。

前面的布施，偏重在外在資具的缺乏，這是屬於外在色身感受的痛苦；這裡的布施，針對的是內心有憂愁的眾生，他內心有障礙。我們若見厄難、恐怖危逼，眾生在人世間活動，他遇到了很多的災難，或者遇到一些恐怖，由於這個災難跟恐怖的危逼，他內心憂愁不安，心有千千結。菩薩遇到這樣的眾生，應該隨順自己的能力，施與無畏。這裡的施無畏，主要的意思是佛法去安慰、開導眾生。

這個無畏施，在《瑜伽師地論》上說：有兩個法門能夠使令眾生遠離怖畏：

一、依止無常、無我的道理來開導眾生。就是眾生會產生憂愁，主要的就是指妄為真，把這些生滅變化的因緣法當作是真實的，產生執著，所以他的生命就通不過去。就是說這個事情已經過去了，但是他內心中揮之不去，這時你應該以諸行無常、諸法無我的道理來開導他，使他從內心的執著裡面解脫出來，這是第一個。

二、應該讚歎三寶的功德，使令他能夠憶念三寶，祈求救護。這時他就能夠從生命的災難中，得到安穩，這是無畏施。

財施所破除的是眾生外在資具的缺乏，無畏施所破除的是眾生內心的障礙。

第三個、法施

若有眾生來求法者，隨己能解，方便為說。

如果這個眾生他已經具足了聽法的善根，而且具足了求法的儀軌，來向我們求法，我們應該用種種五乘的解脫法門來為他宣說。在《瑜伽師地論》上說：在所有布施中，功德最為殊勝的就是法施。因為前面的財施跟無畏施，它只能夠使令眾生暫時的離苦得樂。他沒有飯吃，你給他一碗飯；他得到了憂鬱症，你用佛法的道理開導他，從憂鬱症裡面解脫出來，當然這都非常好。

但是他的根本問題沒有解決。眾生一切痛苦的根源來自於內心的愚癡，所以愚癡是生命中最大的罪業。就是說你今天能夠為他說法，他在黑暗的愚癡中產生光明，讓他知道什麼樣的因緣是一個功德相、什麼樣的因緣是過失相，你為什麼會痛苦？你為什麼會有快樂出現？你讓他能夠抉擇是非。這樣在他的生命中，就能夠有一個努力的方向。他這樣的利益，是生生世世的受益，這樣才能夠究竟離苦得樂。所以在《瑜伽師地論》中，讚歎法施的功德能夠利及來世，前面的財施跟無畏施只能夠利及今世。這裡是講到布施的三個內涵，這以下說明布施內心的依止處。

不應貪求名利恭敬，唯念自利利他，迴向菩提故。

我們在施捨時，應該要遠離名利恭敬，應該用這樣的心情用來對治慳貪，令彼歡喜而迴向無上菩提。大乘的布施法門，功德主要有兩類：

一、從內在的善根來說，是破除慳貪，成就施捨的心。我們在修菩提心時，除了我們能夠正念真如這個根本法門以外，我們要注意這個障礙菩提心的法。就是你在內心的妄想中，建立一個菩提心的幼苗，你要適當的保護；會障礙菩提的，這裡所說的就是貪慾的煩惱，你對於資具、對於佛法的慳貪，會障礙你菩提心的增長。所以我們以施捨的法門，來破除這個慳貪的障礙，所以這是第一個善根，成就無貪的一個施捨心。

二、成就廣大的福報受用，這是兼帶外在的福德。這裡我們說明一下。本論所說的六度法門，跟我們過去的六度可能會有所不同，這裡的六度，它的施設完全是以成就大乘善根而施設的，簡單一句話就是要你莫向外求，就是藉事修心，假藉六度的修學來成就你大乘的善根，依止大乘的善根而增長你的菩提心。所以你每一個施捨的過程中，每一個六度的過程中，你就要生起觀照力，否則只是成就人天的福報。

/KEPAN/壬二、釋戒門

在戒門中，先解釋持戒的定義。什麼叫做大乘的持戒？宗喀巴大師說：遮止損害他，令欲起厭離。就是我們去傷害眾生的心，要加以遮止；對於這種損害他的心，內心產生厭離之想，這叫做持戒。這裡我們說明一下。持戒的意思，不是說你在非常順利的人事，今天也沒什麼事，你心平氣和，這個叫做持戒，這不是，這只是緣缺不生。就是我們的內心，在各式各樣的人事因緣中，我們內心能夠產生一種防非止惡的功能。

就是有這人事的境界來刺激你，我們第一個反應當然是起煩惱，一開始會顛倒，顛倒以後會起煩惱，一般人起煩惱就一直起下去了，這種人就是沒有持戒的功德了。有持戒的人，他也會生起煩惱，但是他生起煩惱時，會產生一個警覺的功能，他內心中有一種自動的警覺訊號產生，這種警覺功能生起時，就會產生防非止惡。當你遇到障礙現前時，你內心能夠起動你的警覺功能──不可以，這個警覺功能的生起，就表示你有持戒的功德。所以這裡，我們先把持戒作一個定義。好，我們看持戒的內涵：

/VERSE/云何修行戒門？所謂不殺、不盜、不淫、不二舌、不惡口、不妄言、不綺語，遠離貪嫉、欺詐、諂曲，瞋恚、邪見。若出家者，為折伏煩惱故，亦應遠離憒鬧，常處寂靜；修習少欲、知足、頭陀等行。乃至小罪，心生怖畏，慚愧改悔。不得輕於如來所制禁戒。當護譏嫌，不令眾生妄起過罪故。

