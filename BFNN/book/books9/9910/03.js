就是說這個大乘的功德：有內在的歸依，歸依我們的清淨心；有外在的歸依，歸依我們的住持三寶，佛法僧三寶。這裡順便一提，後面的論文也會提到，有人問馬鳴菩薩說：既然我們這念心本自清淨，那我們不斷的憶念我們的清淨心就好了，為什麼還要去歸依三寶呢？馬鳴菩薩說：譬如明珠，然有污垢，須假方便，方得清淨。就是我們這一念心是本自清淨，但從因緣法上說，我們過去有很多雜染的造作，使令這個明珠產生染污，所以要假藉佛法僧的方便，一方面禮拜諸佛、聽聞佛法，一方面依眾靠眾，種種的方便，才能夠消滅這些污垢。

所以一定要有內因力跟外緣力的和合，才能夠真實的生起大乘的功德。這個就是所謂的信。能信的心，是一種隨順好樂；所信的是根本、佛、法、僧，有四個法是所信。

