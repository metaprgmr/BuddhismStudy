釋論─問辯征析，剖斷開示，令得決定謂之論。

所謂的論：是以問答的方式來作詳細的分析，讓我們對義理產生信解；第二點就是剖斷開示，由不同的角度來開示，使令我們破除疑惑，它有斷疑生信的功能，產生堅定的信解，叫做論。佛陀是說經；菩薩把佛陀的經文加以發揮，安立宗旨，使令這個宗旨能夠產生斷疑生信的功能，這叫做論：就是佛陀說經，菩薩造論。

蕅益大師在解釋經題時，講到這個信心，他在《大乘起信論裂網疏》上說：大乘的功德若詳細論之，有信、解、行、證四種功德。大乘的信解行證，每一個法都非常重要，馬鳴菩薩在大乘法的修學為什麼強調信心呢？大乘起信為什麼不說大乘解行呢？蕅益大師說：因為所有的功德都必需有根源──但得本，不愁末。就是說你能夠對大乘佛法生起信心，你自然能夠產生正確的理解，也就能夠正確的修行而成就大乘的功德。

所有大乘的功德是從信心生起，就像你種樹，你為什麼有蘋果吃呢？因為有蘋果樹。為什麼有蘋果樹呢？因為它有根，因為這棵蘋果樹有根，這個樹根能夠照顧它、堅固它，一定會產生枝葉花果。所以探本求源，你只要有信心，你後面的問題就不難解決。所以在本論中，以這個信先作一些紮根的工作，把大乘的功德先做紮根的工作。

