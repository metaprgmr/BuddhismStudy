在蕅益大師的《靈峰宗論》中講到一個公案：

在明末時有一個老居士，年輕時就學佛了。學佛以後，除了自己用功以外，他非常的護持三寶，經常在三寶的境界裡面修習福業，護持三寶五十年，後來在一次生病中往生了。往生以後，因為他是一個積善之人，所以閻羅王對他非常的尊重，就問這個老居士說：仁者啊，你來生想要投胎做什麼？想到哪一個地方去呢？這老居士說：我來生想要做一個出家人。閻羅王聽了以後非常驚訝的說：仁者，你修何聖業啊，望此高位？你是修什麼福德，怎麼內心中望此高位，能夠做這種奢望啊？就把他的功過簿子拿出來看，說：你修福五十年，這是一個很大的福報，但是你頂多能夠做千戶之長；你要做出家人，這個福報是不夠的。他說：那這樣好了，我不要說是做一個能夠去禪堂參禪聽經的出家人，我做一個燒火僧，做一個卑下的出家眾，整天為大眾師燒柴火，可以吧！閻羅王再算一算，說：沒辦法，抱歉！這個老居士堅持來生一定要做出家人，閻羅王被他的誠心感動，就說：那這樣好了，再給你陽壽幾年，你再繼續修學，看看是不是能夠完成你成就出家人的福德。後來他就從死亡中醒過來。所以說是寧國有一老者，修福五十餘年，求來世作燒火僧而不可得。

