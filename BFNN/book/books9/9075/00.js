或餓鬼充滿，幽魄長叫喚，
或純現地獄，碾磑受苦楚。
諸佛普住持，隨所宜說法。

彌陀大願王，發心取淨土，
成就安樂剎，十方莫能比。
備諸珍寶性，有情所愛樂。
正覺華化生，聞法悟無上。
他方諸眾生，起心信慕者，
佛力悉加護，命終得往生。

嗟彼無智人，聞言盡狂惑，
不知是淨土，我心所畢具。
凡夫一念閒，諸佛悉炳現，
香水無邊剎，光明互徧滿，
念佛便見佛，求生便往生。
如取自家珍，東西非窒礙。

良哉諸上人！善能了實相，
知諸法如夢，而不趨寂滅。
隨順修多羅，嚴淨佛國土，
我今普歸依。讚歎并隨喜。
廼以至誠心，深心回向心，
願捨此堪忍，疾生清淨域。

佛力不可說，心力不可說，
轉彼穢濁居，悉作蓮華藏。
我今說偈已，頂禮諸賢聖，
願舒白毫光，攝盡微塵眾，
華開彈指頃，畢入菩提場。
