至哉！淨土之教，其諸聖人所由以踐形者乎？孟子曰：形色，天性也。知形色之為天性，則不容離土以言心；知天性之為形色，則不容外心以求土。離土以言心，是以天性為有外也，其所謂心，一介然者而已矣；外心以求土，是以形色為有外也，其所謂土，一塊然者而已矣。是皆不明乎踐形之說者也。華嚴圓教，理事無礙，事事無礙。普賢偈曰：一切剎海劫無邊，以一方便皆清淨。此踐形之極則也。又曰：願我臨欲命終時，盡除一切諸障礙，面見彼佛阿彌陀，即得往生安樂剎。此踐形之正軌也。或以毗盧遮那徧一切處，何得偏指西方，有乖全體？曾不知華藏海中，一塵一毛，具足十方無邊器界，極樂莊嚴，亦復如是。然則但見阿彌陀一佛，即已徧見十方諸佛；但生西方極樂一土，即已徧歷十方佛土。何以故？即一即多，無二無差別故。唯此法門，我釋迦尊金口所宣，十方恒河沙諸佛所共讚，諸大菩薩流通護持，轉轉無盡。自大教東來，單傳直指，外以念佛得度者，若緇若白，未易悉數。際清素服儒風，兼修淨行，常欲薈萃舊聞，用資警策，而日力倉卒，因循至今。會兄子希涑，初發信心，願成此錄，以堅向往。因為標指體要，載稽經論，次支那著述，續以耳目所及，斟酌損益，勒成一編，名之曰淨土聖賢錄。庶幾見聞隨喜，得預法流，一念歸誠，同登彼岸，豈不善哉！詩有之：緡蠻黃鳥，止于邱隅。夫淨土亦聖賢之邱隅也，隨其心淨，則佛土淨，得所止矣。而下學之功，莫先於知止，故曰於止知其所止，可以人而不如鳥乎？經云：彼國常有白鶴、孔雀、鸚鵡、舍利、迦陵頻伽、共命之鳥，晝夜六時，出和雅音，其音演暢五根、五力、七菩提分、八聖道分，如是等法。其土眾生，聞是音已，皆悉念佛、念法、念僧，心邪？土邪？形色邪？天性邪？直下知歸，絕諸戲論，蓮華種子，只在當人。覽是錄者，其諸有樂於踐形之說矣乎！踐形者，往生之謂也。

