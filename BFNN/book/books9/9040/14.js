爾時，善財童子因善知識教，不顧身命，不著財寶，不耽五欲，不戀眷屬，不重王位，自在安樂，唯願化導一切眾生，令其成熟；唯願莊嚴諸佛剎土，普遍清淨；唯願供養一切如來，心無疲厭；唯願證知諸法實性，分明顯現；唯願隨順甚深法界，普遍無礙；唯願修集一切菩薩大功德海，終無退轉；唯願恒於一切劫中修菩薩行；唯願普入一切如來眾會道場；唯願入於一三昧門，普現諸三昧門自在神力；唯願於佛一法輪中，普持一切如來法輪；唯願於佛一毛孔中，見一切佛心無厭足；唯願證得智慧光明，能持一切諸佛法藏；專求此等諸佛菩薩所有功德。漸次遊行，至廣博國及其聚落，普遍尋求鬻香長者。

見已前詣，頂禮其足，遶無數匝，合掌而立，白言：「聖者！我已先發阿耨多羅三藐三菩提心，欲求一切佛平等智慧、欲滿一切佛無量大願、欲淨一切佛最上色身、欲見一切佛清淨法身、欲知一切佛廣大智身、欲淨治一切菩薩諸行、欲照明一切菩薩三昧、欲安住一切菩薩總持、欲堅牢一切菩薩功德、欲除滅一切所有障礙、欲遊行一切十方世界，而未知菩薩云何學菩薩行？云何修菩薩道而能出生一切種智？」

長者告言：「善哉！善哉！善男子！汝乃能發阿耨多羅三藐三菩提心。善男子！我善別知一切諸香；所謂：一切香、一切燒香、一切塗香、一切末香，亦知調合如是香法；又知如是一切香王所出之處；又善了知天香、龍香、夜叉香、乾闥婆、阿脩羅、迦樓羅、緊那羅、摩睺羅伽、人、非人等所有諸香；又善了知治諸病香、斷諸惡香、除憂惱香、生染愛香、增煩惱香、滅煩惱香、令於有為生樂著香、令於有為生厭離香、捨諸驕逸香、聞法歡喜香、發心念佛香、證解法門香、聖所受用香、一切菩薩差別香、一切菩薩地位香。如是等香及調和法，形相生起，出現成就，清淨安隱，方便境界，威力業用，所從根本，隨處、隨人種類差別，如是一切我皆了知。善男子！人間有香名曰龍藏，因龍鬪生；若燒一丸大如油麻，即能興起大香焰雲，猶如帝網彌覆王都，於七日中雨細香雨；若著身者身即金色，若著衣服皆悉變成拘蘇摩華色，若因風吹入宮殿中，一切臺殿、樓閣、堂宇皆悉變為拘蘇摩華色；眾生嗅者，七日七夜歡喜充滿，身心快樂，惑熱清涼，無有諸病及諸煩惱，不相侵害，離諸憂苦，不驚不怖，不迷不亂，慈心相向，志意清淨；我知是已，如應說法，令其決定發阿耨多羅三藐三菩提心。善男子！摩羅耶山出栴檀香，名曰牛頭，若以塗身，設入火阬，火不能燒；善男子！海中有香，名無能勝，若以塗鼓及諸螺貝，其聲發時一切敵軍悉皆退散；善男子！阿那婆達多池邊，出沈水香，名蓮華藏，若燒一丸如麻子大，香氣普熏閻浮提界，眾生聞者，離一切罪戒品清淨；善男子！雪山有香，名具足明相，若有眾生嗅此香者，其心決定離諸染著，我為說法，莫不皆得離垢圓滿清淨三昧；善男子！羅剎界中有香，名海藏，其香但為轉輪王用，若燒一丸，香氣所熏王及四軍皆騰虛空遊止自在；善男子！善法堂中有香，名香性莊嚴，若燒一丸熏彼天眾，普令發起念佛之心；善男子！須夜摩天有香，名淨藏性，若燒一丸熏彼天眾，莫不雲集彼天王所，恭敬聽聞王所說法；善男子！兜率天中有香，名信度嚩囉，於一生所繫菩薩座前，若燒一丸，興大香雲，遍覆法界，普雨一切諸供養具，供養一切如來道場菩薩眾會；善男子！妙變化天有香，名奪意性，若燒一丸，於七日中普雨一切不可思議諸莊嚴具。善男子！我唯知此調和香法，如諸菩薩摩訶薩遠離一切諸惡習氣為無垢香，永斷煩惱眾魔羂索，超諸有趣，得如幻智，於諸世間皆無染著，具足圓滿無所著戒，淨無著智，行無著境，於一切處悉無有著，其心平等無著、無依；而我何能知其妙行、說其功德、顯其所有清淨戒品、示其所作無過失業、闡其所有無過失智、辨其離染三業行門？善男子！於此南方有一大城，名曰樓閣；彼有船師，名婆施羅；汝詣彼問菩薩云何學菩薩行，修菩薩道。」

時，善財童子禮長者足，遶無數匝，慇懃瞻仰，一心戀慕，辭退而去。

