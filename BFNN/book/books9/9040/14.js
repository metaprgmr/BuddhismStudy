爾時，善財童子一心正念，向樓閣城，隨順思惟，觀察道路；所謂：觀道高卑、觀道夷險、觀道淨穢、觀道安危、觀道曲直。漸次而行，作如是念：「我當親近彼善知識；善知識者是成就修行諸波羅蜜增勝道因，善知識者是成就修行普入法界無礙道因，善知識者是成就修行攝取一切眾生智道因，善知識者是成就修行令一切眾生不墮險惡道因，善知識者是成就修行令一切眾生離邪業道因，善知識者是成就修行令一切眾生滅煩惱道因，善知識者是成就修行令一切眾生除惡慧道因，善知識者是成就修行令一切眾生離憍慢道因，善知識者是成就修行令一切眾生拔惡刺道因，善知識者是成就修行令一切眾生捨惡見道因，善知識者是成就修行令一切眾生至一切智城道因。何以故？於善知識處得一切善法故，依善知識力得一切智道故；善知識者難見難遇。」於善知識如是思惟，勤求作意，漸次前行。到彼城已，見彼船師在城門外海岸上住，百千商人大眾圍遶，說大海法，方便開示佛功德海；即前往詣，頂禮其足，遶無數匝，一心合掌；白言：「聖者！我已先發阿耨多羅三藐三菩提心，而未知菩薩云何學菩薩行，云何修菩薩道，我聞聖者善能誘誨，願為我說。」

船師告言：「善哉！善哉！善男子！汝已能發阿耨多羅三藐三菩提心，今復能問生大智因、斷除一切生死苦因、往一切智大寶洲因、成就不壞趣大乘因、遠離二乘怖生死因、住諸三昧生智慧因、遍行菩薩微細行因、乘大願車遊菩薩行清淨道因、以菩薩行莊嚴無壞智清淨道因、普觀一切十方諸佛皆無障礙清淨道因、速能趣入一切諸佛甚深智海清淨道因。善男子！我在海岸樓閣大城，淨修菩薩大悲幢行。

「善男子！我普觀察閻浮提內貧窮眾生，為饒益故修種種行，隨其所願悉令滿足；先以世間珍寶、飲食充足其意，復施法財令其歡喜，令修福行，令生智道，令增善根力，令發菩提心，令淨菩提願，令堅大悲力，令修能滅生死道，令起不捨生死行，令攝眾生海，令修功德海，令照諸法海，令見諸佛海，令入種智海。善男子！我住於此如是思惟、如是作意、如是勤求、如是利益、如是安樂一切眾生。善男子！我知海中一切寶洲、一切寶處、一切寶性、一切寶根、一切寶種、一切寶類；我又能知淨一切寶、鑽一切寶、出一切寶、作一切寶、一切寶器、一切寶用、一切寶境界、一切寶光明。我又能知一切龍宮、一切夜叉宮、一切羅剎宮、一切部多宮，如是一切差別住處，皆善迴避，免其諸難。亦知大海漩澓淺深，波濤遠近，水色好惡，種種不同；亦善別知日月星辰列宿迴轉，運行度數，變異災祥，晝夜晷漏，時分延促；亦知其船鐵木堅脆，機關澁滑，水之大小，風之逆順，左右旋轉安危之相，可行則行，可止則止。我以如是種種方便，常能利益一切眾生。善男子！我以好船運諸商眾，行安隱道，示其寂靜無恐怖相；復為說法令其歡喜，隨所樂欲引至寶洲與諸珍寶咸使充足，然後將領還閻浮提。善男子！我將大船如是往來，從昔至今未曾損壞，若有眾生得見我身聞我法者，令其永不怖生死海，必得入於一切智海，必能竭盡諸愛欲海，能以智光照三世海，能盡一切眾生苦海，能淨一切眾生心海，速能嚴淨一切剎海，普能往詣十方佛海，普知一切眾生根海，普了一切眾生行海，普順一切眾生性海。善男子！我唯得此大悲幢行，若有眾生見聞憶念與我同住，皆悉不空，如諸菩薩摩訶薩，善能遊涉生死大海，能不染著諸煩惱海，能捨一切諸妄見海，能觀一切諸法性海，能以四攝攝眾生海，能善安住一切智海，能滅一切眾生著海，能平等住一切時海，能以神通度眾生海，能以其時調眾生海，而我云何能知、能說彼功德行？善男子！於此南方有一城邑，名樂瓔珞；中有長者名為最勝，汝詣彼問菩薩云何學菩薩行，修菩薩道。」

時，善財童子禮船師足，遶無數匝，慇懃瞻仰，悲泣流淚；求善知識心無厭足，於善知識增戀慕心、增愛樂心，一心觀察，辭退而去。
