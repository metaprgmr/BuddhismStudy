漸次遊行，或至人間城邑、聚落，或經曠野、巖谷、險難，無有疲懈，未曾休息。然後乃至妙光大城，而問人言：「妙光大城在於何所？」人咸報言：「妙光城者，今此城是，是大光王之所住處。」

時，善財童子歡喜踴躍，作如是念：「我善知識在此城中，我今必當親得奉見，聞諸菩薩所行之行，聞諸菩薩出要之門，聞諸菩薩所證之法，聞諸菩薩不思議功德，聞諸菩薩不思議自在，聞諸菩薩不思議平等，聞諸菩薩不思議勇猛，聞諸菩薩不思議境界廣大清淨。」作是念已，入妙光城。

見此大城，以金、銀、琉璃、玻璃、真珠、硨磲、碼瑙七寶所成，七寶深塹，七重圍遶；八功德水盈滿其中，底布金沙，優缽羅華、波頭摩華、拘物頭華、芬陀利華遍佈其上；寶多羅樹七重行列，七種金剛以為其垣各各圍遶，所謂：師子光明金剛垣、無能超勝金剛垣、不可沮壞金剛垣、不可毀缺金剛垣、堅固無礙金剛垣、勝妙網藏金剛垣、離塵清淨金剛垣，悉以無數摩尼妙寶間錯莊嚴，種種眾寶而為埤堄。其城縱廣一十由旬，周迴八方，面開八門，皆以七寶周遍嚴飾，毘琉璃寶以為其地，種種莊嚴甚可愛樂。

其城之內，十億衢道，一一道間，皆有無量萬億眾生於中止住。有無數閻浮檀金樓閣，毘琉璃摩尼網羅覆其上；無數銀樓閣，赤真珠摩尼網羅覆其上；無數毘琉璃樓閣，妙藏摩尼網羅覆其上；無數玻璃樓閣，無垢藏摩尼王網羅覆其上；無數光照世間摩尼寶樓閣，日藏摩尼王網羅覆其上；無數帝青摩尼寶樓閣，妙光摩尼王網羅覆其上；無數眾生海摩尼王樓閣，焰光明摩尼王網羅覆其上；無數金剛寶樓閣，無能勝幢摩尼王網羅覆其上；無數黑栴檀樓閣，天曼陀羅華網羅覆其上；無數無等香王樓閣，種種華網羅覆其上。

其城復有無數摩尼網、無數寶鈴網、無數天香網、無數天華網、無數寶形像網，無數寶衣帳、無數寶蓋帳、無數寶樓閣帳、無數寶華鬘帳之所彌覆，處處建立寶蓋、幢、幡。

當此城中，有一樓閣，名：正法藏，阿僧祇寶以為莊嚴，光明赫奕最勝無比，眾生見者心無厭足，彼大光王常處其中。

爾時，善財童子於此一切珍寶妙物，乃至男女、六塵境界，皆無愛著，但正思惟究竟之法，一心願樂見善知識。

漸次遊行，見大光王去於所住樓閣不遠四衢道中，坐如意摩尼寶蓮華藏廣大莊嚴師子之座，紺琉璃寶以為其足，金繒為帳，眾寶為網，上妙天衣以為茵蓐。其王於上結跏趺坐，二十八種大人之相、八十隨好而以嚴身；如真金山，光色熾盛；如淨空日，威光赫奕；如盛滿月，見者清涼；如梵天王，處於梵眾；亦如大海，功德法寶無有邊際；亦如雪山，相好樹林以為嚴飾；亦如大雲，能震法雷，啟悟群品；亦如虛空，顯現種種法門星象；如須彌山，四色普現眾生心海；亦如寶洲，種種智寶充滿其中。

於王座前，有金、銀、琉璃、摩尼、真珠、珊瑚、琥珀、珂貝、璧玉諸珍寶聚，衣服、瓔珞及諸飲食無量無邊種種充滿。復見無量百千萬億上妙寶車、百千萬億諸天妓樂、百千萬億天諸妙香、百千萬億病緣湯藥資生之具，如是一切悉皆珍好。無量乳牛，蹄角金色；無量千億端正女人，上妙栴檀以塗其體，天衣、瓔珞種種莊嚴，六十四能靡不該練，世情禮則悉皆善解，隨眾生心而以給施。

城邑、聚落、四衢道側，悉置一切資生之具。一一道傍皆有二十億菩薩，以此諸物給施眾生，為欲普攝眾生故，為令眾生歡喜故，為令眾生踴躍故，為令眾生心淨故，為令眾生清涼故，為滅眾生煩惱故，為令眾生知一切義理故，為令眾生入一切智道故，為令眾生捨怨敵心故，為令眾生離身、語惡故，為令眾生拔諸邪見故，為令眾生淨諸業道故。

時，善財童子五體投地，頂禮其足，恭敬右遶，經無量匝，合掌而住，白言：「聖者！我已先發阿耨多羅三藐三菩提心，而未知菩薩云何學菩薩行？云何修菩薩道？我聞聖者善能誘誨，願為我說！」

時，王告言：「善男子！我淨修菩薩大慈幢行，我滿足菩薩大慈幢行。善男子！我於無量百千萬億乃至不可說不可說佛所，問難此法，思惟觀察，修習莊嚴。

「善男子！我以此法為王，以此法教敕，以此法攝受，以此法隨逐世間，以此法引導眾生，以此法令眾生修行，以此法令眾生趣入，以此法與眾生方便，以此法令眾生熏習，以此法令眾生起行，以此法令眾生安住思惟諸法自性，以此法令眾生安住慈心，以慈為主，具足慈力；如是，令住利益心、安樂心、哀愍心、攝受心、守護眾生不捨離心、拔眾生苦無休息心。我以此法令一切眾生畢竟快樂，恆自悅豫，身無諸苦，心得清涼，斷生死愛，樂正法樂，滌煩惱垢，破惡業障，絕生死流，入真法海，斷諸有趣，求一切智，淨諸心海，生不壞信。善男子！我已住此大慈幢行，能以正法教化世間。

「善男子！我國土中一切眾生，皆於我所無有恐怖。善男子！若有眾生貧窮困乏，來至我所而有求索。我開庫藏恣其所取，而語之言：『莫造諸惡，莫害眾生，莫起諸見，莫生執著。汝等貧乏，若有所須，當來我所及四衢道，一切諸物種種具足，隨意而取勿生疑難。』

「善男子！此妙光城所住眾生，皆是菩薩發大乘意，隨心所欲，所見不同，或見此城其量狹小，或見此城其量廣大；或見土沙以為其地，或見眾寶而以莊嚴；或見聚土以為垣牆，或見寶牆周匝圍遶；或見其地多諸瓦石高下不平，或見無量大摩尼寶間錯莊嚴平坦如掌；或見屋宅土木所成，或見殿堂及諸樓閣、階墀、窗闥、軒檻、戶牖——如是一切無非妙寶。

「善男子！若有眾生其心清淨，曾種善根供養諸佛，發心趣向一切智道，以一切智為究竟處，及我昔時修菩薩行曾所攝受，則見此城眾寶嚴淨；餘皆見穢。

「善男子！此國土中一切眾生，五濁世時樂作諸惡。我心哀愍而欲救護，入於菩薩大慈為首隨順世間三昧之門。入此三昧時，彼諸眾生所有怖畏心、惱害心、怨敵心、諍論心，如是諸心，悉自消滅。何以故？入於菩薩大慈為首順世三昧，法如是故。善男子！且待須臾，自當現見。」

時，大光王即入此定。其城內外六種震動，諸寶地、寶牆、寶堂、寶殿、臺觀、樓閣、階砌、戶牖，如是一切咸出妙音，悉向於王曲恭敬禮。妙光城內所有居人，靡不同時歡喜踴躍，俱向王所舉身投地。村營、城邑一切人眾，咸來見王，歡喜敬禮。

近王所住，鳥獸之屬，互相瞻視，起慈悲心，咸向王前恭敬禮拜。一切山原及諸草樹，莫不迴轉向王敬禮。陂池、泉井及以河海，悉皆騰溢，流注王前。十千龍王起大香雲，激電震雷，注微細雨。有十千天王，所謂：忉利天王、夜摩天王、兜率陀天王、善變化天王、他化自在天王……如是等而為上首，於虛空中作眾妓樂。無數天女歌詠讚歎，雨無數華雲、無數香雲、無數寶鬘雲、無數寶衣雲、無數寶蓋雲、無數寶幢雲、無數寶幡雲，於虛空中而為莊嚴，供養其王。伊羅婆拏大象王，以自在力，於虛空中敷布無數大寶蓮華，垂無數寶瓔珞、無數寶繒帶、無數寶鬘、無數寶嚴具、無數寶華、無數寶香，種種奇妙以為嚴飾，無數采女種種歌讚。

閻浮提內復有無量百千萬億諸羅剎王、諸夜叉王、鳩槃荼王、毘捨闍王，或住大海，或居陸地，飲血噉肉，殘害眾生；皆起慈心，願行利益，明識後世，不造諸惡；恭敬合掌，頂禮於王。如閻浮提，餘三天下，乃至三千大千世界，乃至十方百千萬億那由他世界中，所有一切毒惡眾生悉亦如是。

時，大光王從三昧起，告善財言：「善男子！我唯知此菩薩大慈為首隨順世間三昧門。如諸菩薩摩訶薩為高蓋，慈心普蔭諸眾生故；為修行，下、中、上行悉等行故；為大地，能以慈心任持一切諸眾生故；為滿月，福德光明於世間中平等現故；為淨日，以智光明照耀一切所知境故；為明燈，能破一切眾生心中諸黑闇故；為水清珠，能清一切眾生心中諂誑濁故；為如意寶，悉能滿足一切眾生心所願故；為大風，速令眾生修習三昧入一切智大城中故。而我云何能知其行，能說其德，能稱量彼福德大山，能瞻仰彼功德眾星，能觀察彼大願風輪，能趣入彼甚深法門，能顯示彼莊嚴大海，能闡明彼普賢行門，能開示彼諸三昧窟，能讚歎彼大慈悲雲？

「善男子！於此南方，有一王都，名曰：安住；有優婆夷，名曰：不動。汝詣彼問：菩薩云何學菩薩行、修菩薩道？」

時，善財童子頂禮王足，遶無數匝，慇懃瞻仰，辭退而去。

