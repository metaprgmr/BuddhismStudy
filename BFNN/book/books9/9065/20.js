彼聞父語知此所謂為女夜叉，於是恐怖翻心，向父請恕：「父！予再不往任何處所，請父恕我。」父又慰彼云：「青年！汝應善觀慈、悲、喜、捨。」作梵住觀之語，彼依其教實行，再得禪定。
