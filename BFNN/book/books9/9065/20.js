於是大臣等告阿西般羅迦曰：「卿！國王陛下巡行城中，行至卿家之門而還，入於御殿之中。」彼往自己之家呼溫瑪丹提近前：「汝之容姿使王得見耶？」「將軍！一大腹大牙之男人，乘車而來，予不知其為王或大臣，後聞為某高貴之人，予立窗前向之投花，彼立即回返而去。」彼聞此云：「予為汝陷於破滅。」翌日晨起，至王宮立於王之寢室門口，聞王為溫瑪丹提而歎息，彼思：「此王戀慕溫瑪丹提，如不得彼女，彼將死去。為王與予免此不德，予須救助王之生命。」彼還歸自己之家，呼通咒文之侍衛一人前來，彼如是云：「如是如是之處，有一內部空心之支提樹，汝勿使他人得知，於太陽沒時，往彼處坐於樹中。予向彼處攜供物，到達後，禮拜天人祈願：『天王！予之王行祭都城時，不作遊行歸還寢室，嘆息而臥，吾等不明其理由。王為天人等甚盡其心，每年向天人設千兩之供物，今王為如此如此之事而嘆息，請與指示，請對吾等王以生命之施。』予作此祈願，爾時汝變聲色云：『將軍！汝等之王非是疾病，王但戀慕汝妻溫瑪丹提，如王得彼女則可長生，否則即將死去。汝若欲王生，將溫瑪丹提，獻上與王。』汝須如此說話。」於是使彼練習，然後送出。

