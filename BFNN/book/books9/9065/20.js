昔日尸毘國阿利吒城尸毘王治國，菩薩出生於第一妃之胎，名曰尸毘王子。王之將軍亦生一子名阿西般羅迦，此二人為友，一同生長，十六歲時，同往得叉尸羅修習學藝而還。父王讓位與王子，新王任阿西般羅迦為將軍之職，彼依正義而治國。

仍於此同一都城有八十億財產之提利達瓦闍長者，此長者生有一女，容姿無上美好，達於極致，清淨相好，於命名之日，名之為溫瑪丹提。彼女十六歲時，超越人間之姿，如天女之美，凡見彼女，不論何人，皆情不自禁，如飲酒中毒，為煩惱之毒氣所迷惑而難以自持。於是其父提利達瓦闍往王之所云：「大王！予家出一女寶，只能與王合式相配，請遣善觀相好之巧者，前往觀察，是否如王之意。」王云：「甚善！」遣婆羅門等前往。婆羅門等赴長者之宅，被恭敬尊重，受粥之供養。爾時溫瑪丹提以諸類之瓔珞飾身，出現彼等之前；彼等見彼女，不能自持，為煩惱之毒氣所醉，忘卻彼等自己飲食之事，尚未終了，有者取少量食物竟載於頭上，有者或投入腋下，有者叩壁，皆現狂氣。彼女見彼等云：「此諸人前來觀察予之相，而何竟有如此醜態？可攫彼等之頸，出之於外。」彼等胡亂逃歸王宮，對溫瑪丹提甚抱反感，向王曰：「彼女乃一不吉之女，與吾君不相配合。」王因云為不吉之女，乃不對彼女迎聘。彼女聞此消息：「彼謂予為不吉之女，王不迎聘，所謂不吉，有如此之人耶？甚善，若予得見王時，將有所為之事。」彼女對王懷有怒意。爾後其父將彼女嫁與阿西般羅迦，彼對彼女甚為鍾愛。

然因如何果報，彼女得如斯之美，乃因施捨赤色著物之果報。據傳，彼女昔日於波羅奈都出生於一貧乏之家，彼女於祭日見積善根之婦女等著鬱金色之衣裳，及帶種種飾物戲樂。彼女自己亦欲著彼樣之著物出遊，向父母說明，父母曰：「女！予等貧家，予等如何能得如彼之著物？」彼女云：「請許我至富家之宅為得薪之工作，彼方主人知我之價值，予我以著物。」彼女得到兩親之許可，往某家庭謂曰：「如得鬱金色之著物，予將為作求薪之工作。」於是彼家之人等向彼女云：「汝為三年間工作，觀察有無價值，而後付與。」彼女同意：「甚善。」於是開始工作。彼等知彼女之價值，於三年未滿之間，與彼女深濃鬱金色之衣及其他之著物：「汝可著此與友人等一同浴水。」於是遣退彼女。彼女置其赤衣物於岸邊，與友人等前往浴水。

恰於此時，迦葉佛一弟子之法衣，破為寸斷，折樹枝為上下著，來至此地。彼女見彼自思：「此尊者衣物甚破，予因前生未作施捨，今生不易獲得衣物，今將予之衣物分割，以一分施與尊者。」彼女由水中出，著其下衣云：「尊師稍待。」女禮拜長老，分其一分施與長老。長老立於路傍隱秘之處，棄其折枝業之衣，以施得之一片為下衣，另一片為上衣，然後步出，然其衣物之光輝使彼全身如朝日之放光。彼女見此：「此尊者前無此光，而今如朝日之光輝，予將餘物亦與尊者。」於是將餘下部分亦均施與，而立願曰：「尊師！請使予後世，得無上之美容，使男人見予，失其原狀，世間再無比予美之人。」於是長老述謝禮之辭而去。其後彼女於天上界繼續轉生，此頃則出生於阿利吒都，如其願行。

於阿利吒城布告迦底迦月之祭，同月之十五日，全城盛大裝飾。阿西般羅迦往自己守備場所時，呼彼女謂曰：「溫瑪丹提！今日為迦底迦祭，國王陛下巡行城中，首先將來至予之家門。汝不可使王見汝姿，王如見汝，將失正氣。」彼女向出行之夫答曰：「予將注意。」彼去後，彼女命侍女云：「國王至此宅門，告予得知。」

爾後太陽西沉，滿月上昇，城中裝飾如天人之城，到處燈火通明。王身飾多種瓔珞，乘附駿馬之車，諸大臣一同隨行，於盛大光榮之中巡行都市，首先第一往阿西般羅迦之家門而來。其家以赤砒石色之牆圍繞裝飾，門上附有望樓，為一無上美麗之宮殿。此時侍女向溫瑪丹提通告，彼女持花籠，以緊那羅女之魅力，倚於窗口而立，向王之方投花。王仰見彼女，為煩惱之毒氣所醉，不能自持正氣，即如此為阿西般羅迦家之事，亦不能知。於是呼馭者近前詢問，唱次之二偈：

