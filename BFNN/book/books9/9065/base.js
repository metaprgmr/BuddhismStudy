class 品 {
  constructor(name,vol,firstNum,stories) {
    Object.assign(this,{ name,vol,firstNum,stories });
    this.firstNum = parseInt(firstNum);
  }
  renderTD(buf) {
    if (this.name) buf.w('<td>', this.name, '</td><td>');
    else buf.w('<td colspan=2>');
    for (var i=0; i<this.stories.length; ++i) {
      var n = this.firstNum + i, ndisp = toW(n, 3, '<inv>0</inv>');
      buf.w(`　<a href="?vol=${this.vol}&loc=s${to3d(n)}" title="${this.stories[i]}">${ndisp}</a>`);
    }
    buf.w('</td>');
  }
}

class 篇 {
  constructor(num) {
    this.num = num;
    this.pins = []; // 品；可以是無名者。
  }
  add(pin) { this.pins.push(pin); return this; }
} // end of 篇.

class 本生經 {
  constructor(num) {
    this.parts = [];
    for (var i=1; i<=26; ++i) this.parts[i] = new 篇(i);
  }

  renderTRs(buf) {
    var len = this.parts.length, prevPart, prevVol;
    for (var i=1; i<len; ++i) {
      var pt = this.parts[i], pins = pt.pins;
      for (var j=0; j<pins.length; ++j) {
        var p = pins[j];
        buf.w('<tr>');
        if (p.vol == prevVol) buf.w('<td></td>');
        else buf.w(`<td><a href="?vol=${p.vol}">卷${zNumber(prevVol = p.vol)}</a>&nbsp;</td>`);
        if (pt.num == prevPart) buf.w('<td></td>');
        else buf.w('<td>第', zNumber(prevPart = pt.num), '篇</td>');
        p.renderTD(buf);
        buf.w('</tr>');
      }
    }
    return this;
  }

} // end of 本生經.

