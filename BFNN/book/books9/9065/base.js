class Story {
  constructor(firstln) {
    var ln = firstln;
    if (ln[0] == '#') {
      if (ln[1] != '*')
        ln = ln.substring(1);
      else {
        ln = ln.substring(2);
        this.ignoreAnchor = true;
      }
    }
    var a = ln.split('\|');
    this.num = unznum(a[0]);
    this.ttl = a[1];
    this.incarnation = a[2];
    if (!this.num || !this.ttl || !this.incarnation)
      throw `Bad story line: ${firstln}`;
    this.parseState = 'prolog';
    this.prologue = [];
    this.main = [];
    this.epilogue = [];
  }
  add(ln) { // true meaning done.
    if (ln.startsWith('----')) {
      switch (this.parseState) {
      case 'prolog': this.parseState = 'main';   return false;
      case 'main':   this.parseState = 'epilog'; return false;
      default:       throw `Invalid ----: current state is "${this.parseState}".`;
      }
    }
    if (ln.startsWith('====')) {
      this.parseState = null;
      return true;
    }
    switch (this.parseState) {
    case 'prolog': this.prologue.push(ln); break;
    case 'epilog': this.epilogue.push(ln); break;
    case 'main':   this.main.push(ln);     break;
    }
    return false;
  }

  render(buf) {
    var anch = '';
    if (!this.ignoreAnchor) {
      anch = `:s${to3d(this.num)}`;
      console.log('Anchored at', anch);
    }
    buf.w(`/SECTION${anch}/${znum(this.num)}　${this.ttl}\n`,
          `/R/（菩薩＝<c339>${this.incarnation}</c339>）\n/KEPAN/序分\n\n`)
       .join(this.prologue, '\n')
       .w('\n\n/KEPAN/主分\n\n')
       .join(this.main, '\n')
       .wIf(this.epilogue.length, '\n\n/KEPAN/結分\n\n')
       .join(this.epilogue, '\n')
       .w(`\n\n/END/${znum(this.num)}　${this.ttl}\n`);
    return this;
  }
}
function parseStories(lines) {
  var a = lines.split('\n'), buf = new Buffer(), story;
  for (var i=0; i<a.length; ++i) {
    var ln = a[i];
    if (story) {
      if (story.add(ln)) { // done
        story.render(buf);
        story = null;
      }
    }
    else if (ln[0] == '#') {
      story = new Story(ln);
    }
    else {
      buf.w(ln, '\n');
    }
  }
  return buf.render();
}

